何以故？諸佛世尊，離一切想。」作是語已，爾時，世尊告具壽善現言：
「如是、如是！善現，若諸有情聞說如是甚深經典，不驚、不懼、無有怖畏，當知成就最勝希有！
何以故？善現，如來說最勝波羅蜜多謂般若波羅蜜多。善現，如來所說最勝波羅蜜多，無量諸佛世尊所共宣說，故名最勝波羅蜜多。如來說最勝波羅蜜多，即非波羅蜜多，是故如來說名最勝波羅蜜多。
復次，善現，如來說忍辱波羅蜜多，即非波羅蜜多，是故如來說名忍辱波羅蜜多。
何以故？善現，我昔過去世曾為羯利王斷支節肉。我於爾時都無我想、或有情想、或命者想、或士夫想、或補特伽羅想、或意生想、或摩納婆想、或作者想、或受者想。我於爾時都無有想，亦非無想。
