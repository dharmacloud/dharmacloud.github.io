何以故？善現，我憶過去於無數劫、復過無數，於然燈如來、應、正等覺先、復過先，曾值八十四俱胝那庾多百千諸佛，我皆承事！既承事已，皆無違犯！善現，我於如是諸佛世尊皆得承事，既承事已，皆無違犯！
若諸有情後時、後分、後五百歲，正法將滅時分轉時，於此經典受持、讀誦、究竟通利、及廣為他宣說開示、如理作意。
