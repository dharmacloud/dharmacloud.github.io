如是有情成就最勝希有功德！此地方所大師所住，或隨一一尊重處所若諸有智、同梵行者。」
^ck13【如法受持】
說是語已，具壽善現復白佛言：「世尊，當何名此法門，我當云何奉持？」
作是語已，佛告善現言：「具壽，今此法門名為『能斷金剛般若波羅蜜多』。如是名字，汝當奉持。何以故？善現，如是般若波羅蜜多，如來說為非般若波羅蜜多，是故如來說名般若波羅蜜多。」
