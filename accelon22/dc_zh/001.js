何以故？以諸賢聖補特伽羅皆是無為之所顯故。
^ck8【依法出生】
佛告善現：於汝意云何，若善男子或善女人，以此三千大千世界盛滿七寶，持用布施，是善男子或善女人，由此因緣所生福聚寧為多不？
