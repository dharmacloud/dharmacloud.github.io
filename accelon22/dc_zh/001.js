复次，善现，如是法门不可思议、不可称量！应当希冀不可思议所感异熟！
善現，如來宣說如是法門，為欲饒益趣最上乘諸有情故、為欲饒益趣最勝乘諸有情故！
善現，若有於此法門受持、讀誦、究竟通利，及廣為他宣說開示、如理作意，即為如來以其佛智悉知是人，即為如來以其佛眼悉見是人，則為如來悉覺是人！
如是有情一切成就無量福聚！皆當成就不可思議、不可稱量無邊福聚！善現，如是一切有情，其肩荷擔如來無上正等菩提！
何以故？善現，如是法門，非諸下劣信解有情所能聽聞！非諸我見、非諸有情見、非諸命者見、非諸士夫見、非諸補特伽羅見、非諸意生見、非諸摩納婆見、非諸作者見、非諸受者見所能聽聞！此等若能受持、讀誦、究竟通利、及廣為他宣說開示、如理作意，無有是處！
復次，善現，若地方所聞此經典，此地方所當為世間諸天及人、阿素洛等之所供養，禮敬右遶，如佛靈廟！
^ck16【能淨業障】
復次，善現，若善男子或善女人，於此經典受持、讀誦、究竟通利及廣為他宣說開示、如理作意，若遭輕毀、極遭輕毀。
