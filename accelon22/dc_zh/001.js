善現，我若具說當於爾時是善男子或善女人所生福聚，乃至是善男子是善女人所攝福聚，有諸有情則便迷悶、心惑狂亂！
是故，善現，如來宣說如是法門不可思議、不可稱量！應當希冀不可思議所感異熟！」 
^ck17【究竟無我】
爾時，具壽善現復白佛言：「世尊，諸有發趣菩薩乘者，應云何住、云何修行、云何攝伏其心？」
佛告善現：「諸有發趣菩薩乘者，應當發起如是之心，『我當皆令一切有情於無餘依妙涅槃界而般涅槃。雖度如是一切有情令滅度已，而無有情得滅度者。』
何以故？善現，若諸菩薩摩訶薩有情想轉，不應說名菩薩摩訶薩！所以者何？若諸菩薩摩訶薩不應說言有情想轉，如是命者想、士夫想、補特伽羅想、意生想、摩納婆想、作者想、受者想轉，當知亦爾！
