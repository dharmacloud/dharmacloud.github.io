何以故？善現，我憶過去五百生中，曾為自號忍辱仙人。我於爾時，都無我想，無有情想、無命者想、無士夫想、無補特伽羅想、無意生想、無摩納婆想、無作者想、無受者想。我於爾時都無有想，亦非無想。
是故，善現，菩薩摩訶薩遠離一切想應發阿耨多羅三藐三菩提心，不住於色應生其心，不住非色應生其心，不住聲、香、味、觸、法應生其心，不住非聲、香、味、觸、法應生其心，都無所住應生其心。
何以故？善現，諸有所住，則為非住！是故如來說：『諸菩薩應無所住而行布施，不應住色、聲、香、味、觸、法而行布施。』
復次，善現，菩薩摩訶薩為諸有情作義利故，應當如是棄捨、布施。
何以故？善現，諸有情想即是非想；一切有情，如來即說為非有情。
善現，如來是實語者、諦語者、如語者、不異語者。
復次，善現，如來現前等所證法、或所說法、或所思法，即於其中非諦非妄。
善現，譬如士夫入於闇室，都無所見。當知菩薩若墮於事，謂墮於事而行布施，亦復如是。
善現，譬如明眼士夫，過夜曉已，日光出時見種種色。當知菩薩不墮於事，謂不墮事而行布施，亦復如是。
復次，善現，若善男子或善女人於此法門受持、讀誦、究竟通利，及廣為他宣說開示、如理作意，則為如來以其佛智悉知是人，則為如來以其佛眼悉見是人，則為如來悉覺是人。如是有情，一切當生無量福聚。
^ck15【持經功德】
