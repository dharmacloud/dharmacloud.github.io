◆佛明五蘊皆空，首拈色蘊為例。
色與空的關係，本經用不異、即是四字來說明。
不異即不離義，無差別義。
色離於空，色即不成；空離於色，空亦不顯。
色空、空色二不相離，故說「色不異空，空不異色」。
有人聽了，以為空是沒有，色是有，今雖說二不相離而實是各別的，空仍是空，色仍是色。
為除此種計執，所以佛接著說：「色即是空，空即是色」。
即表示空色二不相離，而且相即。
◆佛法作如是說，有其特殊意義。
印度的一分學者，以為涅槃與生死，煩惱與菩提，是不相同的兩回事，離了生死才能證得涅槃，離了煩惱才能獲得菩提。
生死和煩惱是世間雜染法，涅槃、菩提是出世清淨法，染淨不同，何得相即？
這種見地，是從他們的宗教體驗而得來。
宗教體驗，世間的一般宗教，如耶、回、印度教等，也都有他們的體驗，如上帝、真宰、梵我等。
若說他們都是騙人的，決不盡然，他們確是從某種體驗，適應環境文化而表現出來的。
不過體驗的境地，有淺深，有真偽。
佛法的目的，在使人淨除內心上的錯誤——煩惱，體驗真理，得到解脫——涅槃。
一分學者依佛所說去持戒修定淨除煩惱，體驗得「超越」現象的，以此為涅槃。
於是，以為世間和涅槃，是不同性質的。
在修行的時候，對於世間法，也總是遠離它，放身山林中去，不肯入世作度生的事業。
這種偏於自了的超越境，是不究竟的，所以被斥為沈空滯寂者。
真正的涅槃空寂，是要在宇宙萬有中，不離宇宙萬有而即是宇宙萬有的。
因此，修行也不同，即於世間利生事業中去體驗真理，淨化自己。
古德說：「佛法在世間，不離世間覺」。
覺悟即在世間法而了達出世法，由此大乘能入世度生，悲智雙運。
有所得的小乘，體驗到偏於「超越」的，於是必然地走入厭離世間的道路。
龍樹菩薩在《智論》裡，講到「色不異空，空不異色；
色即是空，空即是色」時，即以《中論》生死涅槃無別去解說。
大乘的體驗，不妨說是「內在」的。
論到宗教的體驗，有人以為這是一種神秘經驗，既稱為神秘的，此中境界就不是常人所能了解。
因之，經驗的是否正確，也無從確論。
現見世間一般宗教，他們依所經驗到而建立的神、本體等，各不相同，如耶教的上帝，印度教的梵我，所見不同，將何以定是非？
依佛法，這是可判別的，一方面要能洗盡一切情見，不混入日常的計執；
一方面要能貫徹現象而無所礙，真俗二諦無礙的中道，即保證了佛法的究竟無上。
佛法是貫徹現象與本體，也是貫通宗教與哲學，甚至通得過科學的，所以有人說佛法是科學而哲學的宗教。

◆從理論上說，色（一切法也如此）是因果法，凡是依於因緣條件而有的，就必歸於空。
如把因果法看成是有實自性的，即不成其為因果了。
因法的自性實有，即應法法本來如是，不應再藉因緣而後生起；
若必仗因緣而能生起，那法的自性必不可得。
由此，一切果法都是從因緣生，從因緣生，果法體性即不可得，不可得即是空，故佛說一切法畢竟空。
反之，果法從因緣有，果法的作用形態又不即是因緣，可從因緣條件有，雖有而非實有，故佛說一切法緣起有。
可知色與空，是一事的不同說明：所以色即是空，空即是色。
常人於此不了解，以為空是沒有，不能現起一切有。
不知諸法若是不空，不空應自性有，即一切法不能生。
這樣，有應永遠是有，無應永遠是無。
但諸法並不如此，有可以變而為無，無的也可由因緣而現為有，一切法的生滅與有無，都由於無自性畢竟空而得成立。
性空——無不變性、無獨立性、無實在性，所以一切可現為有，故龍樹菩薩說：「以有空義故，一切法得成」。
本經所說：色即是空，空即是色，即說明此空色不相礙而相成的道理。
經中接著說：「受想行識，亦復如是」。
這是說：不但從色的現象說：色不異空，乃至空即是色，若從受的現象上說：也是受不異空，空不異受；
受即是空，空即是受的。
想與行識，都應作如是說。
空是一切法普遍而根本的真理，大至宇宙，小至微塵，無不如此，即無不是緣起無自性的。
能在一法達法性空，即能於一切法上通達了。
