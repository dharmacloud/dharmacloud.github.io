◆此是總標，以下即是解釋此三句的。
此三句中有人有法，有因有果。
觀自在菩薩，是能修般若法門者。
行深般若波羅蜜多時，照見五蘊皆空，即是所修觀法。
修般若波羅蜜多，通達五蘊皆空，即是因；
由此體達空性而能度一切苦厄，即是果。
◆「觀自在菩薩」，即般若觀慧已得自在的菩薩，不一定指補怛落迦的觀世音菩薩。
菩薩是依德立名的，有某種特殊功德，即名他為某某。
《華嚴經》每有若干同名同號的菩薩，即由於此。
所以，誰有觀自在的功德，誰就可以稱為觀自在。
觀是對於宇宙人生真理的觀察，由此洞見人生的究竟。
下文照見，即是般若觀慧的作用。
自在即是自由，擺脫了有漏有取的蘊等繫縛，即得身心的自由自在。
用經文來解釋，照見五蘊皆空即是觀，度一切苦厄即得自在。
由此，觀自在菩薩可作兩說：一、特別指補怛落迦的觀自在菩薩。
二、凡是能觀察真理獲得痛苦解脫者，都名觀自在菩薩——本經指後者。
經上說八地以上的菩薩，得色自在、心自在、智自在，為菩薩的觀自在者。
然菩薩登地，通達真理，斷我法執，度生死苦，即可名觀自在。
就是勝解行者，能於畢竟空觀修習相應，也可以隨分得名觀自在了。

◆「菩薩」，梵語應云菩提薩埵。
菩提譯為覺悟，對事理能如實明白，了知人生的真意義，由此向人生的究竟努力以赴。
這不是世間知識所知，唯有般若慧纔能究竟洞見的。
佛是具有最高覺悟者，菩薩即以佛的大覺為理想的追求者。
薩埵譯為有情，情是堅強意欲向前衝進的力量。
人和一般動物，都有這種緊張衝動的力量，所以都是有情。
有的譬喻為金剛心，就是說明這種堅忍的毅力。
合起來，菩提薩埵譯為覺有情，有覺悟的有情，不但不是普通的動物，就是混過一世的人，也配不上這個名稱。
必須是了知人生的究竟所在，而且是為著這個而努力前進的，所以菩薩為一類具有智慧成分的有情。
又可以說：菩提薩埵是追求覺悟的有情。
有情雖同有緊張衝動的活力，可惜都把他們用在食、色、名位上。
菩薩是把這種強毅的力量，致力於人生究竟的獲得，起大勇猛，利濟人群以求完成自己，就是吃苦招難，也在所不計。
所以經裡常常稱讚菩薩不惜犧牲，難行能行。
以堅毅的力量求完成自己的理想——覺悟真理，利濟人群，淨化自己，這才不愧稱為菩薩。
又，覺是菩薩所要追求的，有情是菩薩所要救濟的。
上求佛道，下化有情，就是這覺有情的目的和理想。
由此看來，菩薩並不意味什麼神與鬼，是類似世間的聖賢而更高尚的。
凡有求證真理利濟有情的行者，都可名菩薩。
修到能照見五蘊皆空，度脫一切苦厄，即是觀自在菩薩。
此明能觀的人。
◆「行深般若波羅蜜多時」，此說觀自在菩薩所修的法門。
智慧，是甚深的。
深淺本是相對的，沒有一定的標準，但此處所說的深，專指體驗第一義空的智慧，不是一般凡夫所能得到的，故名為深。
《般若經》裡，弟子問佛：深奧是何義？
佛答以：「空是其義，無相、無願、不生不滅是其義」。
這空無相無願——即空性，不是一般人所能了達的，所以極為深奧。
《十二門論》也說：「大分深義，所謂空也」。
◆能「照見五蘊皆空」的，即是甚深般若慧。
般若的悟見真理，如火光的照顯暗室，眼睛的能見眾色一樣。
五蘊，是物質精神的一切，能於此五類法洞見其空，即是見到一切法空。
有的譯本，在五蘊下加個「等」字，即等於下文所要講的十二處、十八界、四諦、十二緣起等。
空性，是要在具體的事實上去悟解，依有明空，空依有顯，若離開了具體存在的事物，也不知什麼是空了。
所以佛經明空，總是帶著具體的事實的，如說五蘊空，十二處空等。
蘊，是積聚的意思，即是一類一類的總聚。
佛把世間法總分為五類：色、受、想、行、識。
一切物質的現象，總攝為色；
精神的現象，開為受、想、行、識四種，總名之曰五蘊。
色蘊的色，不是青黃等色，也不是男女之色。
此色有二義：一、質礙義，二、變壞義。
質礙義者：凡是有體積，佔有空間位置的，如扇子有扇子的體積和扇子所佔據的方位，鐘有鐘的體積和鐘所佔據的方位；
扇子與鐘都是有質礙的，兩者相遇即相障礙而不能並容。
變壞義是：有體積而存在的，受到另一質礙物的衝擊，可能而且是終久要歸於變壞的。
有此二義，即名為色，即等於近人所說的物質。
舊科學家所說物質最終的單元，依佛法也還是要變壞的。
常人見到現存事物的表面，不了解事物內在的矛盾，於是設想物體最後固定的實體。
其實，一切色法——物，自始至終即在不斷的衝擊、障礙，向著變壞的道路前進。

◆關於心理活動，佛把它分為受、想、行、識。
心理現象不如物質現象的容易了知，最親切的，要自己從反省的工夫中去理解。
佛觀察心理的主要活動為三類：一、受蘊：在我們與外境接觸時，內心上生起一種領納的作用。
如接觸到可意的境界時，內心起一種適悅的情緒，這名為樂受；
接觸到不適意的境界時，內心起一種不適悅的情緒，即是苦受。
另有一種中容的境界，使人起不苦不樂的感覺，此名捨受。
二、想蘊：想是在內心與外境接觸時，所起的認識作用，舉凡思想上的概念以及對於外境的了解、聯想、分析、綜合都是想的作用。
三、行蘊：此行是造作的意思，與外境接觸時，內心生起如何適應、改造等的心理活動，依之動身發語而成為行為。
行是意志的，以此執行對於境界的安排與處理。
其他的心理活動，凡是受、想所不攝的，都可以包括在這行蘊裡。
四、識蘊：此也是心理活動，是以一切內心的活動為對象的。
就是把上面主觀上的受、想、行等客觀化了，於此等客觀化了的受、想、行，生起了別認識的作用，即是識蘊。
識，一方面是一切精神活動的主觀力，一方面即受、想等綜合而成為統一性的。

◆這五蘊，是佛法對於物質、精神兩種現象的分類。
佛不是專門的心理學家或物理學家，佛所以要這樣說的，是使人由此了知五蘊無我。
一般人總直覺的有一個自我存在，佛為指出自我是沒有的，有的不過是物質與精神現象所起的協調作用而已。
若離此五蘊，想找一實體的自我，是找不到的。
身體是色，情緒上的苦樂感覺是受，認識事物的形相是想，意志上所起的欲求造作是行，了別統攝一切心理活動的是識，除此各種活動以外，還有什麼是實體的自我呢？
佛為破眾生實我計執，故說五蘊。
有些小乘學者以為佛說五蘊無我，我確是無的，而五蘊法是有情組織的原素，是實有的。
這是不知佛意，我執雖稍稍除去，法執又轉深，故說：五蘊皆空。
五蘊中的自我固不可得，五蘊法的自身也不可得。
因為五蘊法也是由因緣條件而存在的，由此所起的作用和形態，都不過是關係的假現。
如五蘊的某一點是真實的，那麼，這就是我了。
真實的自我不可得，故五蘊皆空。
但這種假現的作用與形態，雖空而還是有的。
如氫氧合成的水，有解渴、灌田、滌物等作用，有體積流動的形態，從此等形態作用上看，一般即認為是實有的。
然若以甚深智慧來觀察，則知任何作用與形態，都是依關係條件而假立的，關係條件起了變化，形態也就變化了，作用也就不存在了。
事物若是有實體性，則事物應永遠保持他固有的狀態，不應有變化，應不受關係條件的變動。
事實上，一切法都不是這樣，如剝芭蕉一樣，剝到最後，也得不到一點實在的。
諸法的存在，是如幻不實的，需要在諸法的當體了知其本性是空，這才不會執為是實有了。

◆一分學者，以為我無而法有，這是因於智慧淺薄的緣故。
在同一因緣法上，智慧深刻者，即能知其法空，所以說行深般若波羅蜜多時，照見五蘊皆空。
有的譯本，譯照見五蘊皆空為照見五蘊等自性皆空，自性就是含攝得不變、獨存的實體性，此實體性不可得，故曰皆空，而不是破壞因緣生法。
空從具體的有上顯出，有在無性的空上成立，空有相成，不相衝突。
這和常人的看法很不相同，常人以為有的不是沒有，空是沒有的不是有，把空和有的中間劃著一條不可溝通的界限。
依般若法門說：空和有是極相成的，二者似乎矛盾而是統一的。
佛法是要人在存在的現象上去把握本性空，同時在畢竟空的實相中去了解現象界的緣起法。
能這樣的觀察、體驗，即得度一切苦。
徹底的度苦，必須體驗空性，了知一切法空，生死間的苦痛繫縛，才能徹底解除。
所以在說了照見五蘊皆空後，接著說「度一切苦厄」。
苦是苦痛，厄是困難。
眾生的苦痛困難，不外內外兩種：屬於內自身心的，如生老病死等；
屬於外起的，如愛別離、怨憎會、求不得等。
這一切的苦難，根源都在眾生把自己看成有實體性而起，大至一國，小至一家，互相鬥爭，苦痛叢生，即都是由於不了我之本無，於是重自薄他，不惜犧牲他人以滿足自己。
我這樣想這樣做，你也這樣想這樣做，於是彼此衝突，相持不下，無邊苦痛就都跟著來了。
若知一切法都是關係的存在，由是了知人與人間是相助相成的，大家是在一切人的關係條件下而生活而存在，則彼此相需彼此相助，苦痛也自不生了。
物我、自他間如此，身心流轉的苦迫也如此。
總之，若處處以自我為前提，則苦痛因之而起；
若達法性空——無我，則苦痛自息。
菩薩的大悲心，也是從此而生，以能了知一切法都是關係的存在，救人即是自救，完成他人即是完成自己，由是犧牲自己，利濟他人。
個人能達法空，則個人的行動合理；
大家能達法空，則大家行動合理。
正見正行，自能得到苦痛的解放而自在。
