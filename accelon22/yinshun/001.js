忍不但忍辱，還忍苦耐勞，忍可（即認透確定）事理。
所以論說忍有三：忍受人事間的苦迫，叫生忍；
忍受身心的勞苦病苦，以及風雨寒熱等苦，叫法忍；
忍可諸法無生性，叫無生忍，無生忍即般若慧。
常人所不易忍的，即受人的欺虐等，所以經中多舉忍辱為例。
不論世間事或出世大事，在實行的過程中，身心的、自然的、人事的，都有種種的糾纏、困難。
尤其是菩薩發大心，行廣大難行，度無邊眾生，學無量佛法，艱苦是必然不免的。
