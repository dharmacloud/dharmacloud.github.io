無我等四相，並非實有我等四相，而加以取消或摧毀。
要知道：我等本不可得，由於眾生的顛倒，無中執有；
所以無我等四相，只是顯明他的本相無所有而已。
能離我等四相，即能離法相與非法相，所以說：離虛妄顛倒的諸相，即名為佛。
這與上文的「若見諸相非相，即見如來」，完全同一。
離四相，或者以為與佛相差還遠。
不知約覺悟實相——無分別法性說，與諸佛一覺一解脫，平等平等，也得名為佛。
所以古人說：「須陀洹名初得法身」。
論說：「佛陀，是覺悟真實之義，此名通於聲聞、獨覺及無上菩提三者」。

