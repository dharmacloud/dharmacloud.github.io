聽說莊嚴淨土，又在取著莊嚴，為尊貴的七寶，如意的衣食，美妙的香華音樂，不老不死的永生所迷惑了。
所以，佛告須菩提：如上所說的莊嚴，凡是修大乘行的菩薩，都應生清淨心，離去取相貪著穢惡根源，不要為淨土的莊嚴相——美麗的色相，宛轉的音聲，芬馥的香氣，可口的滋味，適意的樂觸，滿意的想像等而迷惑！要知色、聲、香、味、觸、法，都如幻如化，沒有真實的自性可得。
如取執色等有相可得，這即是三毒的根源，從此起貪、起瞋、起癡，即會幻起種種的苦痛和罪惡。
所以，應不住（著相）一切法，不住而住的住於空性，於無可住的法性而生淨心。
前說無我相、無法相、無非法相，能生一念清淨信心，即是這裡的生清淨心，無所住而生其心。
如此的離相而得淨心，這才能「心淨則國土淨」。
如取著淨土而不能淨心，即縱然進入莊嚴的淨土，那也還是苦痛的，還是穢惡的。
如上海，在物質方面，比較優美得多。
但他並不配稱為清淨，反而被人咒詛為罪惡的淵藪。
這可見莊嚴國土，要從清淨心中去開拓出來！
◆莊嚴佛土者，即非莊嚴，是名莊嚴。
像這一類型的三句論法，本經是頂多的。
或作三諦說；或約三性釋；
或約大乘揀別小乘說。
今依中觀者說：如莊嚴佛土，是討論觀察的對象。
這是緣起的，空無自性的，所以說即非莊嚴。
然而無自性空，並不破壞緣起施設，世出世法一切是宛然而有的，所以隨俗說是名莊嚴。
緣起，所以無性，無性所以待緣起，因此「即非」的必然「是名」，「是名」的必然「即非」，即二諦無礙的中道。
所以說：「諸佛依二諦，為眾生說法」。
