《般若經》說無莊嚴為莊嚴；
《華嚴經》說普莊嚴，都是由於性空慧的徹悟法性，淨願善行所成。
國土——世界是緣起假名，所以能廣大莊嚴。
沒有自性的世界，即沒有不變性，如遇穢惡的因緣，即成穢惡的世界；
如造集清淨的因緣，即自然會有清淨的世界出現。
假使，穢惡世界是實有定性而不可改易的，那就是塗抹一些清淨的上去，也不會清淨，反而更醜惡了！所以，世界無定，穢惡與清淨，全依眾生知見行為的邪正善惡而轉。
必須知道如此，才會發心轉穢惡的國土為清淨。
必須善悟國土莊嚴的非莊嚴，才能隨行願而集成國土的莊嚴。

