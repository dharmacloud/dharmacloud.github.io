如來說法，一切是如實的，所以一切可信。
法門的甚深，本生的修行，無不是可信的。
真語等五句，別譯少不誑語一句。
此五句，都是真實可信的意思。
如從差別的字義說：真是不妄的，實是不虛的，如是一樣的；
不誑即是實的，不異即是如的，如來的梵語，本有如法相而說的意思，所以佛說是一切可信的。

