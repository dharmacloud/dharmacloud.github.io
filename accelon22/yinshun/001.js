如能觀諸法從緣，都無真實的自性，悟入法性空，緣即空而緣起的假我，生大悲心，願度如幻眾生，這是無緣大悲。
無緣大悲，即與般若相應的大悲。
悟了眾生空寂無自性，所以雖度脫一切而實無眾生得滅度者。
如海中印現的明月，由於風吹波動而月相不見了。
從他的不見說，好似滅去了，其實那有自體可滅呢！發心菩提，重在發願度生，所以也就重於我空。

◆我、人、眾生、壽者，都是眾生的異名；
《般若經》中有十六種異名，都不外從眾生的某一特性而立名，眾生即執此為實有。
我，是主宰義，即每一眾生的行動，常人都有自己作主與支配其他（宰）的意欲，所以稱為我。
人，行人法，所以名為人。
眾生，約現在說：即五眾和合生的——有精神與物質和合的；
約三世說：即由前生來今生，今生去後生，不斷的生了又死，死了又受生，與補特伽羅的意義相合。
