此六度以般若為導，而實彼此相應相攝，一波羅蜜即具足一切波羅蜜。
本經發菩提心，以大悲度眾生為首，這與布施——以自己所有的給予眾生，使他離苦得樂，尤為吻合，故本經即以布施為主而統攝利他的六度行。
布施如何能攝六度？
布施有三：一、財施：以財物賙濟人是財施，以體力甚至犧牲生命去救助人，也是財施。
所不同的是：衣食等財物為外財施，體力、心力以及生命等為內財施。
這財施，即狹義的施波羅蜜。
二、無畏施：令眾生離諸怖畏，這就是持戒與忍辱二波羅蜜。
持戒，能處眾不礙大眾，不使人受到威脅不安。
如殺人者，使人有生存的威脅；
偷盜者，使人有外命（財物）喪失的恐怖等。
如能受持禁戒，潔身自守，即不會侵害他人，能使人與人間相安無事了。
但人類個性不一，你以戒自守，他卻以非禮待你；
如不能感化或設法避免，不能忍受而衝突起來，仍不免相殺相奪，造成人間的恐怖。
必須以戒自守——克己，又以忍寬容他人——恕人，才能做到無畏施。
三、法施：即精進、禪定、般若三波羅蜜。
般若是明達事理的，沒有智慧，即落於顛倒二邊，不知什麼是佛法？
是邪是正？那怎能救人？
禪定是鑑機的，如內心散亂，貪著世間，我見妄執，即不能洞見時機，不知眾生的根性，即不能知時知機而給予適宜的法藥。
精進是雄健無畏的，有了精進，才能克服障難，誨人不倦，利人不厭。
這樣，六波羅蜜統攝於布施，為菩提行的根本了。
要救眾生，不能不犧牲自己去利他——布施：這必須具足物質救濟，以達到眾生生活等的滿足；
必須以戒忍的精神，達到人與人間和樂安寧；
又必須以進、定、慧的教化，革新眾生的思想意志，而使之歸於中道。
從前，僧團中的「利和同均」「戒和共遵」「見和無諍」，也即是六度精神的實施！
◆佛對須菩提說：菩薩行布施——六度利他時，於法應無所住行於布施。
這即是說：不要住於色境而行布施，不要住於聲境乃至法境而行布施。
於法應無所住的法，指一切法說；
香味觸法的法，但指意識所對的別法塵。
住，是取著不捨的意思。
眾生在六塵境——認識的一切上起意識時，都有自性的執見，以色為實色，以聲為實聲，總以為是確實如此存在的。
因為取著六境，即為境所轉而不能自在解脫。
菩薩為度眾生而行布施，對於施者、受者、施、施時、施處、施因、施果這一切，當然要能遠離自性的妄取，能不著相而布施，才能真實利濟眾生。
否則，覺得有我是能施，他是受施，所施物如何如何，希望受者的報答，希望未來的福報，甚至因而貢高我慢，這都從住於法相而生起來，這那裡還像菩薩行？
所以，佛總結的對須菩提說：菩薩修行，應這樣的不住於相——相即六塵境相而行布施！
◆不住於相的布施，能降伏煩惱，能安住菩提心，而且所得的福德，廣大得不可思議！有相布施，不能通達三輪性空，所得功德即有限有量。
無論功德多麼大，總不過是人天有限的福報。
但如能以般若相應而布施，將此布施融歸於法性，迴向於一切眾生而同趨於大覺，功德即無限而不可思量。
如以一滴水投入大海中，即遍大海的水性而不可窮盡。
為了說明這個，佛便問須菩提說：你的意見怎樣？
東方的虛空可思量嗎？
南方、西方、北方的虛空，以及四維、上方、下方的虛空，可以思量嗎？
須菩提一概否定的說：不可。
佛即說：既知道十方虛空是不可思量的，也應該知道菩薩不住相布施的功德，也同虛空一般的不可思量。
這並不是以虛空的廣大，形容福德的廣大，而是以虛空的無所有，不可住、不可著、不可說邊際數量，如無相布施的自性不可得一樣。
菩薩是應該這樣的安住，這樣的行施，所以如來說：菩薩但應如所教住！無相布施，指空相應布施，通達能施所施畢竟無自性的布施。
即空如幻的布施，如此因，如此果，如此利他，如此自利，都法相宛然有而不失不壞。
佛怕人以無記心布施，或執理廢事，所以特舉不可思量的功德以顯示布施因果。

