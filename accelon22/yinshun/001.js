◆民國五十八年九月，我重回嘉義妙雲蘭若安住。
想到自己寫作的，別人記錄的，也不算少了。
文章未必自己的好，但到底費過自己的一番心力。
想到虛大師、慈航法師、大醒法師、續明法師等去世以後，由後人來搜集整編，實在並不容易。
倒不如自己來編集一下，應該省力得多。
同時，已經出版的，或是紙張差，或是字體小，參差不一。
不如統一重排，改用新四號字，也讓讀者省一點目力。
就這樣，定下了編集為《妙雲集》的決心。
◆我的作品，體裁不一，長短不一，所以將《妙雲集》分為三編。
