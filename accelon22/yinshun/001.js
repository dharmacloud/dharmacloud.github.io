◆全經大義，再扼要的提示二點：金剛般若即無上遍正覺：本經以金剛般若為名，而內容多明阿耨多羅三藐三菩提。
如佛為須菩提說如此發心，直至究竟菩提，徹始徹終的歸宗於離相無住。
說無上遍正覺為「是法平等無有高下」；
「於是法中無實無虛」：都是從般若無住以開示無上遍正覺。
般若無所住，無所住而生其心；
不取諸相，即生實相，即名為佛。
須知般若無住的現覺，即離相菩提的分證。
依此觀究竟，究竟也如是；
依此觀初心，初心也還如此。
所以，處處說無上遍正覺，實在即是處處說金剛般若。
不過，約修行趨果說，名之為般若無所住；
約望果行因說，名之為離相菩提心而已！
◆二道即五種菩提：本經初由須菩提問佛：「發阿耨多羅三藐三菩提心，應云何住？
云何降伏其心」？
經佛解說後，須菩提又照樣的再問一遍，佛答也大致相同。
所以，本經明顯的分為兩段。
《大般若經》有兩番囑累，《智論》說：「先囑累者，為說般若波羅蜜體竟；
今以說令眾生得是般若方便竟，囑累」。
智者即曾依此義，判本經的初問初答為般若道，後問後答為方便道。
此二道的分判，極好！
◆二道，為菩薩從初發心到成佛的過程中，所分的兩個階段。
從初發心，修空無我慧，到入見道，證聖位，這一階段重在通達性空離相，所以名般若道。
徹悟法性無相後，進入修道，一直到佛果，這一階段主要為菩薩的方便度生，所以名方便道。
依《智論》說：發心到七地是般若道——餘宗作八地，八地以上是方便道。
般若為道體，方便即般若所起的巧用。
◆般若即菩提，約菩提說：此二道即五種菩提。
一、發心菩提：凡夫於生死中，初發上求佛道、下化眾生的大心，名發阿耨多羅三藐三菩提心，所以名為發心菩提。
二、伏心菩提：發心以後，就依本願去修行，從六度的實行中，漸漸降伏煩惱，漸與性空相應，所以名為伏心菩提。
三、明心菩提：折伏粗煩惱後，進而切實修習止觀，斷一切煩惱，徹證離相菩提——實相，所以名為明心菩提。
