無想的，這是外道無想定的果報，名無想天。
這無想的眾生，有說：只是沒有粗顯的心識，微細的心識是有的。
有的說：什麼心識也不起。
非想非非想的，是無色界非想非非想處的眾生。
他實在是有想的，但印度某些宗教師，以為到達非想非非想處，就是涅槃解脫了。
所以，佛法中稱之為非想非非想，即雖沒有粗想——非有想，但還有細想——非無想；
還取著三界想，沒有能解脫呢！
◆發菩提心，本經以大悲大願去說明；
可見離了大悲大願，即沒有菩薩，也沒有佛道可成。
以度生為本的菩提心，第一，是廣大的：不但為一人，一些人，或一分眾生，而是以一切眾生為救拔的對象。
又是徹底的：眾生的苦痛無邊，冷了給他衣穿，餓了給他飯吃，病了給他醫藥，都可解除眾生的痛苦；
政治的修明，經濟的繁榮，學術的進步，也著實可以減輕眾生的痛苦。
但苦痛的根源沒有拔除，都是暫時的，局部的，終非徹底的救濟。
所以，菩薩的大菩提心，除了這些暫時的局部的而外，要以根本解脫的無餘涅槃去拯拔眾生。
涅槃為名詞，指解脫生死苦迫的當體；
滅度是動詞，即使眾生於涅槃中得到眾苦的解脫。
涅槃本不可說一說多，然依世俗施設來說，即有凡外與佛法的不同。
世俗有人說：冷了餓了，有饑寒的苦迫；
如生活富裕，豐衣足食，這就是涅槃。
涅槃的字義，有消散的意思，即苦痛的消除而得自在。
所以俗人拍著吃飽的肚子說：這就是涅槃。
有些外道，以四禪八定為涅槃；
不知這祇是定境的自我陶醉，暫時安寧，不是徹底的。
佛法說涅槃，有二：一、有餘（依）涅槃：通達一切法的寂滅性，離煩惱而得到內心的解脫，即是涅槃。
但由前生惑業所感的果報身還在，從身體而來的痛苦，還未能解除。
所以，即使是阿羅漢，饑寒老病的身苦，還是一樣的。
二、無餘（依）涅槃：無學捨身而入無量無數的法性，不再有物我、自他、身心的拘礙，名為無餘。
菩薩發願度生，願使每一眾生都得此究竟解脫，所以說：我皆令入無餘涅槃而滅度之。
無餘涅槃，為三乘聖者所共入，菩薩也會歸於此。
菩薩安住無住大涅槃，即此無餘涅槃的無方大用，能悲願無盡，不證實際罷了！本經以無餘涅槃度脫一切眾生，即本於三乘同入一法性，三乘同得一解脫的立場；
也就因此「通教三乘」而「但為菩薩」。
