為遣此虛妄執相，所以又稱不虛誑相現的空性為實相。
眾生執著實有，佛責斥為虛妄的。
雖本無虛妄相可得，勸眾生離此取著，所以說離妄相而見實相。
以真去妄，為不得已的方便。
如真的虛妄淨盡，真實也不可得，如以雹擊草，草死雹消。
所以說：如來所得法無實無虛。
◆菩薩修菩薩行，應契會此無實無虛。
若心住於色等法而行布施，這如走入無光的闇室，一切都不能見；
反之，菩薩心不住於色等法而行布施，那就如明目人，在日光朗照的地方，能見種種的形色。
