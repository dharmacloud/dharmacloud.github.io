聞此經而能生淨信，即能生實相。
實相，異譯作實想。
想即智慧的別名，如經說無常想、無我想等。
所以，實相即如實相而知的般若；
生實相，即「一切法不生則般若生」。
但實相如何可說，所以須菩提隨即說：實相，實即是非相，是離一切名言測度的畢竟空寂；
從不為虛誑妄取相所惑亂，名之為實相。
諸法實相，即諸法的實相不可得；
因為一切法的實相不可得，所以名為實相。
這是般若法門的實相說，顯示般若的特色！
