施畜生食者，獲福百倍；與犯戒人食者，
獲福千倍；施持戒人食者，獲福萬倍；施斷欲仙人食者，
獲福億倍；與向須陀洹食者，獲福不可計。
況復成須陀洹乎？
況向斯陀含、得斯陀含道！
況向阿那含、得阿那含道！
況向阿羅漢、得阿羅漢道！
況向辟支佛、得辟支佛！
況向如來、至真、等正覺！
況成佛及比丘僧！
