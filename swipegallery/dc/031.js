今日命終，彼無子姓，躬往收攝財寶，理使入官。
純金八萬斤，況復餘雜物乎！
然彼長者存在之日，所食如此之食，極為弊𭞲惡，
不食精細，所著衣服垢坌不淨，所乘車騎極為瘦弱。」
^cb612c11世尊告曰：「如是，大王！
如王來言：『夫慳貪之人得此財貨，不能食噉，
不與父母、妻子、僕從、奴婢，亦復不與朋友、
知識，亦復不與沙門、婆羅門、諸尊長者。
