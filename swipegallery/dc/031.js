是時，王波斯匿聞說法已，白世尊曰：「唯願世尊受我三月請，
及比丘僧，莫在餘處。」
是時，世尊默然受波斯匿請。
^cb609a26時，王波斯匿見世尊默然受請，即從坐起，
頭面禮足，便退而去。
