復經七萬歲，隨時供養，然燈、散華，懸繒幡蓋。
大王當知，燈光如來遺法滅盡，然後彼王方取滅度。
^cb610c28「爾時，地主大王者豈是異人乎？
莫作是觀。所以然者，爾時地主大王者，即我身是。
我於爾時，七萬歲中，以衣被、飯食、床臥具、
