懸繒幡蓋，作倡妓樂，不可稱計。
施諸浴池，辦諸油燈，辦種種飯食，味有百種。
是時，王波斯匿即白：「時到，唯願世尊臨顧此處。」
^cb609b5爾時，世尊以見時到，著衣持鉢，將諸比丘僧，
前後圍遶，入舍衛城，至彼講堂所。
到已，就座而坐，及比丘僧各隨次而坐。
^cb609b8是時，王波斯匿將諸宮人，手自行食，供給所須，
乃至三月，無所短乏，給與衣被、飯食、床臥具、病瘦醫藥。
