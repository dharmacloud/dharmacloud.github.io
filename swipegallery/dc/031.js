以得無上法，淫、怒、癡盡，永不得全，是世福田。
以此因緣本末，使漏盡阿羅漢世人所應供養。
^cb607a17「復以何因緣，轉輪聖王世人所應供養？
比丘當知，轉輪聖王以法治化，終不殺生，
復教化人使不殺生；自不盜竊，亦復教他人使不偷盜；
自不淫泆，復教他人不行淫泆；自不妄語，
亦復教人使不妄語；自不兩舌，鬪亂彼此，
亦復教他人使不兩舌；自不嫉妬、恚、癡，
亦復教他人不習此法；自行正見，復教他人使不邪見。
