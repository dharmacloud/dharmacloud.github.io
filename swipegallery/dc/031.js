一切無所愛惜，供給沙門、婆羅門、諸高德者。』」
^cb612c17時，王波斯匿說曰：「此婆提長者命終，為生何處？」
^cb612c18世尊告曰：「此婆提長者命終，生涕哭大地獄中。
所以然者，此斷善根之人，身壞命終，生涕哭地獄中。」
^cb612c20波斯匿王曰：「婆提長者斷善根耶？」
^cb612c21世尊告曰：「如是。大王！
如王所說，彼長者斷於善根，然彼長者故福已盡，更不造新。」
^cb612c23王波斯匿曰：「彼長者頗有遺餘福乎？」
^cb612c24世尊告曰：「無也，大王！
