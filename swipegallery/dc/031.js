一時，佛在舍衛國祇樹給孤獨園。
^cb635c19爾時，世尊告諸比丘：「諸有華之屬，瞻𭅇蔔之華、
須摩那華，天上、人中，婆師華最為第一。
此亦如是，諸善功德三十七道品之法，無放逸行為第一。
^cb635c22「若無放逸比丘修四意斷。
云何為四？於是，比丘！
若未生弊惡法，求方便令不生；已生弊惡法，
