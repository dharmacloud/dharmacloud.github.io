心不遠離，恒欲令滅；未生善法，求方便令生；
已生善法，重令增多，終不忘失，具足修行，心意不忘。
如是，諸比丘修四意斷。
如是，諸比丘！當作是學。」
^cb635c6爾時，諸比丘聞佛所說，歡喜奉行。
^n3【三】
^cb635c7聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb635c8爾時，世尊告諸比丘：「諸有星宿之光，月光最為第一。
此亦如是，諸善功德三十七品之法，無放逸行最為第一，
最尊、最貴。
^cb635c10「無放逸比丘修四意斷。
云何為四？於是，比丘！
若未生弊惡法，求方便令不生；若已生弊惡法，
求方便令滅；若未生善法，求方便令生；
若已生善法，求方便重令增多，終不忘失，
