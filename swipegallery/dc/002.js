「善哉！善哉！曼殊室利大慈悲者，起無量悲，勸請我說，
為欲義利種種業障所纏眾生，饒益安樂諸天人故。
曼殊室利！當善憶念，聽我所說。」
時曼殊室利童子樂聽佛說，白言：「唯然，世尊！」
^ck4【總標依正】
佛告曼殊室利：
「東方過此佛土
十恒河沙等佛土之外，
有世界名淨琉璃，
彼土有佛，名藥師琉璃光如來、應、正遍知、
明行足、善逝、世間解、無上士、調御丈夫、
天人師、佛、世尊。
^ck5【行願】
「曼殊室利！彼世尊藥師琉璃光如來本行菩薩行時，發十二大願。
何者十二？
^ck6【生佛平等願】
「第一大願：
願我來世於佛菩提得正覺時，自身光明熾然，照曜無量無數無邊世界，
三十二丈夫大相及八十小好以為莊嚴；
我身既爾，令一切眾生如我無異。
^ck7【開曉事業願】
「第二大願：
願我來世得菩提時，
身如琉璃，內外清淨，無復瑕垢，
光明曠大，威德熾然，身善安住，
焰網莊嚴，過於日月，
若有眾生，生世界之間，或復人中昏暗及夜，莫知方所，
以我光故，隨意所趣，作諸事業。
^ck8【無盡資生願】
「第三大願：
願我來世得菩提時，
以無邊無限智慧方便，令無量眾生界受用無盡，
莫令一人有所少乏。
^ck9【安立大道願】
「第四大願：
願我來世得菩提時，
諸有眾生行異道者，一切安立菩提道中，
行聲聞道、行辟支佛道者，皆以大乘而安立之。
^ck10【戒行清淨願】
「第五大願：
願我來世得菩提時，
若有眾生於我法中修行梵行，
此諸眾生無量無邊，一切皆得不缺減戒，具三聚戒，
無有破戒趣惡道者。
^ck11【諸根具足願】
「第六大願：
願我來世得菩提時，
若有眾生，其身下劣，諸根不具，醜陋頑愚，聾盲跛躄，身攣背傴，白癩癲狂，
若復有餘種種身病，聞我名已，一切皆得，諸根具足，身分成滿。
^ck12【身心康樂願】
「第七大願：
願我來世得菩提時，
若有眾生，諸患逼切，無護無依，無有住處，遠離一切資生醫藥，又無親屬，貧窮可愍，
此人若得聞我名號，
眾患悉除，無諸痛惱，
乃至究竟無上菩提。
^ck13【轉女成男願】
「第八大願：
願我來世得菩提時，
若有女人，為婦人百惡所逼惱故，厭離女身，願捨女形，
聞我名已，轉女人身成丈夫相，乃至究竟無上菩提。
^ck14【回邪歸正願】
「第九大願：
願我來世得菩提時，
令一切眾生解脫魔網，
若墮種種異見稠林，悉當安立置於正見，次第示以菩薩行門。
^ck15【從縛得脫願】
「第十大願：
願我來世得菩提時，
若有眾生，種種王法，繫縛、鞭撻、牢獄、應死，無量災難，悲憂煎迫，身心受苦，
此等眾生，以我福力，皆得解脫一切苦惱。
^ck16【得妙飲食願】
「十一大願：
願我來世得菩提時，
若有眾生，飢火燒身，為求食故，作諸惡業，

我於彼所，先以最妙色香味食，飽足其身，後以法味，畢竟安樂而建立之。
^ck17【得妙衣具願】
「十二大願：
願我來世得菩提時，
若有眾生，貧無衣服，寒熱蚊虻，日夜逼惱，
我當施彼，隨用衣服，種種雜色，如其所好，
亦以一切寶莊嚴具，花鬘、塗香、鼓樂、眾伎，隨諸眾生所須之，具皆令滿足。
^ck18【總結】
「此十二大願，是彼世尊藥師琉璃光如來、應、正遍知，行菩薩時，本昔所作。
^ck19【說略指廣】
「復次，曼殊室利！藥師琉璃光如來所有諸願，及彼佛土功德莊嚴，
乃至窮劫說不可盡。
^ck20【以西喻東】
彼佛國土一向清淨，無女人形，離諸欲惡，亦無一切惡道苦聲，
琉璃為地，城闕、垣墻、門窓、堂閣柱樑、斗拱、周匝羅網，皆七寶成，
如極樂國，淨琉璃界莊嚴如是。
^ck21【以伴讚主】
「於其國中有二菩薩摩訶薩，

一名日光，二名月光，
於彼無量無數諸菩薩眾，最為上首，持彼世尊藥師琉璃光如來正法之藏。
^ck22【結勸往生】
「是故，曼殊室利！信心善男子、善女人，
應當願生彼佛國土。」
^ck23【離慳吝】
爾時，世尊復告曼殊室利童子言：
「曼殊室利！或有眾生，不識善惡，
多貪無厭，不知布施及施果報，
愚癡無智，闕於信根，聚財護惜，不欲分施。
此等眾生，無施心故，見乞者來，其心不喜，
如割身肉。
復有無量慳貪眾生，自不受用，
亦不欲與父母、妻子，況奴婢、作使及餘乞人。
此等眾生，人間命終，生餓鬼道，或畜生道。
^ck24【離貪惜】
由昔人間，曾得聞彼藥師琉璃光如來名號故，
或在鬼道，或畜生道，如來名號暫得現前，
即於念時，彼處命終，還生人道，得宿命智，
怖畏惡趣，不樂欲樂，
好行慧施，讚歎施者，
一切所有，悉能捨施，漸以頭目、手足、血肉、身分
皆與求者，況餘財物。
^ck25【離毀犯】
「復次，曼殊室利！有諸眾生，雖奉如來，受持學句，
然破戒、破行、破於正見；
或受學句，護持禁戒，
然不求多聞，
不解如來所說修多羅中甚深之義；
或復多聞，而增上慢，自是非他，
嫌謗正法，為魔伴黨。
此等癡人，
及餘無量百千俱胝那由他眾生，行邪道者，當墮地獄。
此等眾生，應於地獄流轉無期，
^ck26【離見慢】
以得聞彼世尊藥師琉璃光如來名號故，於地獄處，
彼佛威力，如來名號暫得現前，
即時捨命，還生人道，正見精進，淳善淨心，
便能捨家，於如來教中出家學道，漸次修行菩薩諸行。
^ck27【離嫉妬】
「復次，曼殊室利！或有眾生，以妬忌故，但自稱讚，不讚他人。
此諸眾生，以自高輕他故，於三惡道，無量千歲，受諸苦毒；
過無量千歲已，於彼命終，
生畜生趣，作牛馬駝驢，
鞭杖捶擊，飢渴逼惱，身負重擔，隨路而行；
若生人道，常居下賤，
為人奴婢，受他驅役。
^ck28【斷魔羂】
若昔人中，聞彼世尊藥師琉璃光如來名號者，
以此善根，
眾苦解脫，諸根猛利，聰慧博識，恒求善本，
得與良友常相隨逐，能斷魔羂，
破無明㲉，竭煩惱河，
解脫一切生老病死，憂悲苦惱。
^ck29【離鬥訟咒詛】
「復次，曼殊室利！有諸眾生，
好喜乖離，更相鬪訟，此等互起惡心眾生，
身口及意，恒作諸惡，
為欲相損，各各常以無益相加，
或告林神、樹神、山神、塚神種種別神，

殺諸畜生，
取其血肉，祭祀一切夜叉、羅剎食血肉者，
書怨人字，并作其形，成就種種毒害、呪術、
厭魅、蠱道、起屍鬼呪，欲斷彼命及壞其身。
^ck30【轉起慈心】
由聞世尊藥師琉璃光如來名號故，
此諸惡事不能傷損，
皆得互起慈心、益心、無嫌恨心，
各各歡悅，更相攝受。
^ck31【往生極樂】
「復次，曼殊室利！此諸四眾——比丘、比丘尼、優婆塞、優婆私——
及餘信心善男子、善女人等，受八分齋，
或復一年，或復三月受持諸戒，
以此善根，隨所憙樂，隨所願求，若欲往生西方極樂世界阿彌陀如來所者，
^ck32【自然化生】
由得聞彼世尊藥師琉璃光如來名號故，
於命終時，有八菩薩乘空而來，示其道徑，
即於彼界，種種異色波頭摩華中自然化生；
^ck33【上生天國】
若復此人欲生天上，
即得往生，本昔善根無有窮盡，
不復更生諸餘惡趣，
^ck34【還生人間】
天上命盡，當生人間，
為轉輪王，四洲自在安立，
無量百千俱胝那由他眾生於十善業道，
^ck35【生剎帝利】
或復生於剎利大族、婆羅門大族、居士大家，金銀、粟帛、倉庫盈滿，
形色具足，自在具足，眷屬具足，勇健多力如大力士；
^ck36【轉生丈夫】
若有女人得聞說此如來名號至心受持，此人於後永離女身。」
^ck37【曼殊護持】
爾時，曼殊室利童子白佛言：
「世尊！我於後時，以彼世尊藥師琉璃光如來名號，於信心善男子、善女人所，
種種方便流布令聞，
乃至睡中亦以佛名覺寤其耳。
^ck38【諸天護持】
若受持此經讀誦宣說，或復為他分別開解，
若自書，若令人書，若取經卷，五色淨綵以盛裹之，灑掃淨處以安置之，
持種種華、種種香、塗香、華鬘、寶幢、幡蓋而用供養，

爾時，四大天王，與其眷屬，并餘百千俱胝那由他諸天，皆詣其所。
若此經卷流行之處，
若復有人誦持此經，以得聞彼世尊藥師琉璃光如來名號，及本昔所發殊勝大願故，當知是處無復橫死，
亦復不為諸鬼所持奪其魂魄，
設已奪者，還復如故。」
^ck39【受持儀軌】
佛言：「如是，如是！曼殊室利！如汝所說。
曼殊室利！信心善男子、善女人，若欲供養彼如來者，
此人應作如來形像，七日七夜受八分齋食清淨食，
於清淨處散種種華，燒種種香，以種種繒綵，種種幡幢，莊嚴其處，
澡浴清潔，著新淨衣，
應生無垢濁心，無怒害心，於一切眾生起利益心，慈悲喜捨平等之心，
鼓樂歌讚，右遶佛像，
應念彼如來本昔大願并解釋此經，
^ck40【獲福益】
如所思念，如所願求，一切所欲，皆得圓滿——
求長壽得長壽，
求福報得福報，
求自在得自在，
求男女得男女。
^ck41【百怪出現難】
「或復有人忽得惡夢，或見諸惡相，或怪鳥來集，
於其住所百怪出現，
此人若能以種種眾具，供養恭敬彼藥師琉璃光如來者，
一切惡夢、惡相、不吉祥事皆悉隱沒，
^ck42【一切怖畏難】
或有水怖、火怖、刀怖、毒怖、懸嶮之怖，惡象、師子、虎狼、熊羆、毒蛇、惡蝎、蜈蚣、蚰蜒如是等怖，
憶念供養彼如來者，
一切怖畏皆得解脫。
^ck43【內亂外患難】
若他國侵擾、賊盜反亂、
如是等怖，亦應念彼如來恭敬尊重。
