【疏鈔云】如來者。來而無來。去而不去。住而不住。非動非靜。上合諸佛下等群生。一性平等。故號如來。
【王日休曰】此分三言如來。皆謂真性佛也。若有人言如來若來若去。若坐若臥。是人不解我所說義者。真佛無相。故不可以若來若去若坐若臥形容之。若可以形容者。則是有相。故此人不曉解我所說義也。何以故者佛又自問何故不解我所說義乎。乃自答云。我所謂如來者。謂真佛也。真佛既無形相。又徧虛空世界。豈有去來哉。
