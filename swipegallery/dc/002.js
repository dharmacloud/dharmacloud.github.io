大德世尊！或有眾生信根不具，
聞說如來佛境界已，作是思惟：
『云何但念彼如來名獲爾許功德？』
心不信受，生於誹謗，
此等長夜無義饒益，當墮苦趣。」
佛言：
「阿難！若彼如來所有名號入其耳中，此人墮惡道者，
無有是處。
^ck50【信解希有】
阿難！諸佛境界誠為難信，
汝今信受，應知皆是如來威力，
非一切聲聞、辟支佛地所能信受，唯除一生補處菩薩摩訶薩。
阿難！人身難得，
於三寶中信敬尊重亦難可得，
聞彼如來名號倍難於此。






^ck51【功德無盡】
阿難！彼世尊藥師琉璃光如來無量菩薩行，
無量諸巧便，無量曠大願，
我欲一劫若過一劫說彼如來菩薩行願，乃至窮劫，
彼世尊藥師琉璃光如來本昔所行及殊勝大願，亦不究盡。」








^ck52【說延壽法】
爾時，眾中有菩薩摩訶薩名曰救脫，即從座起，
偏露一髆，右膝著地，向婆伽婆合掌曲躬白言：
^ck53【病患垂危】
「大德世尊！於未來世，當有眾生，身嬰重病，
長患羸瘦，不食飢渴，喉脣乾燥，
死相現前，目無所見，父母、親眷、朋友、知識啼泣圍遶，
^ck54【神識受報】
其人屍形臥在本處，閻摩使人引其神識，置於閻摩法王之前，
^ck55【神識受報】
此人背後有同生神，隨其所作，
若罪若福一切皆書，
盡持授與閻摩法王。
時閻摩法王推問其人，算計所作，隨善隨惡而處分之。
^ck56【作福續命】
若能為此病人歸依彼世尊藥師琉璃光如來，如法供養，
即得還復。
^ck57【勵力行善】
此人神識得迴還時，如從夢覺皆自憶知，
或經七日、或二十一日、或三十五日、或四十九日，神識還已，
具憶所有善惡業報，
由自證故，乃至失命不造惡業。
^ck58【結勸】
是故信心善男子、善女人，
應當供養藥師如來。」
^ck59【明延壽儀】
爾時，慧命阿難問救脫菩薩言：
「善男子！應云何供養彼世尊藥師琉璃光如來也？」
^ck60【答延壽儀】
救脫菩薩言：
「大德阿難！若有患人欲脫重病，當為此人七日七夜受八分齋，
當以飲食，及種種眾具，隨力所辦，供養比丘僧，
晝夜六時，禮拜供養彼世尊藥師琉璃光如來，
四十九遍讀誦此經，
然四十九燈，
應造七軀彼如來像，
一一像前各置七燈，
一一燈量大如車輪，
或復乃至四十九日光明不絕，
當造五色綵幡長四十九尺。
^ck61【救國難以延命】
「復次，大德阿難！灌頂剎利王等，若災難起時——
所謂人民疾疫難、他方侵逼難、自界反逆難、星宿變怪難、日月薄蝕難、非時風雨難、過時不雨難——
爾時，此灌頂剎利王，當於一切眾生，起慈愍心，赦諸繫閉，
依前所說供養法式，供養彼世尊藥師琉璃光如來。
