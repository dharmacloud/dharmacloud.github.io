安隱少病，無有非人奪其魂魄。」
^ck48【問答決定】
爾時，世尊告慧命阿難言：
「阿難！如我稱揚彼世尊藥師琉璃光如來所有功德，汝信受耶？汝於如是諸佛如來甚深境界多生疑惑。」
