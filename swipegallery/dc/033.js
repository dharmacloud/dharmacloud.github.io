諸世界狂愚之中，汝最為上！』
^cb657c21「是故，比丘！我觀此義已，故告汝等耳。
所以然者，此非善本功德，不得修梵行，亦復不得至涅槃處。
然思議此者，則令人狂，心意錯亂。
然比丘當知，彼人實見四種之兵。
所以然者，昔日諸天與阿須倫共鬪，當共鬪時，
諸天得勝，阿須倫不如。
是時，阿須倫便懷恐怖，化形極使小，從藕根孔中過。
佛眼之所見非餘者所及。
^cb657c29「是故，諸比丘！當思議四諦。
所以然者，此四諦者，有義、有理，得修梵行，
行沙門法，得至涅槃。
是故，諸比丘！捨離此世界之法，當求方便，思議四諦。
知是，諸比丘！當作是學。」
^cb658a4爾時，諸比丘聞佛所說，歡喜奉行。
