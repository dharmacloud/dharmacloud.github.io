又不中沒，復非在岸上，不為人、非人所捉，
亦不為水所迴轉，亦不腐敗，便當漸漸至涅槃處。
所以然者，涅槃者，正見、正治、正語、正業、
正命、正方便、正念、正定是涅槃之原本。」
