為無欲究竟？所謂彼究竟者，謂無欲究竟。
云何彼究竟者，有恚究竟？
為無恚究竟？所謂彼究竟者，無恚究竟，非有恚究竟。
云何有癡究竟？無癡究竟？
所謂彼究竟者，無癡究竟。
云何彼究竟者，為是有愛究竟？
為無愛究竟？所謂彼究竟者，無愛究竟。
