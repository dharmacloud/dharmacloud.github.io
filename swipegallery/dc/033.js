世間有此非法竊盜，汝今守田當雇其直，
諸人民來取他粳米者，即懲其罪。』
爾時，即安田主。比丘當知，爾時，其守田者，
號為剎利種，皆是舊法，非為非法。」
^cb737c1爾時，世尊便說此偈：
^gatha「始有剎利種，姓中之上者，
^gatha聰明高才人，天人所敬待。
^cb737c5「爾時，其有人民侵他物者，是時剎利取懲罰之。
然復彼人不改其𠎝，故復犯之。
是時剎利之主勅作刀杖，取彼人而梟其首。
爾時，世間初有此殺生。
是時，眾多人民聞此教令：『其有竊盜粳米者，剎利主即取殺之。』
皆懷恐懼，衣毛皆竪，各作草盧於中坐禪，
修其梵行，而一其心，捨離家業、妻子、兒婦獨靜其志，修於梵行。
因此已來，而有婆羅門之名姓。
是時，便有此二種姓出現世間。
^cb737c14「比丘當知，彼時由盜故便有殺生，由殺故便有刀杖。
是時，剎利主告人民曰：『其有端政高才者，當使統此人民。』
又告之曰：『其有人民竊盜者，使懲其罪。』
爾時，便有此毘舍種姓，出現於世。
^cb737c18「爾時，多有眾生便生此念：『今日眾生之類各共殺生，
皆由業之所致也，今可來往周旋以自生活。』
爾時，便有首陀羅種姓，出現世間。」
^cb737c21爾時，世尊便說此偈：
^gatha「初有剎利種，次有婆羅門，
^gatha第三名毘舍，次復首陀姓。
^gatha有此四種姓，漸漸而相生，
^gatha皆是天身來，而同為一色。
^cb737c27「比丘當知，爾時，有此殺、盜心，無復有此自然粳米。
^cb737c29「爾時，便有五種穀子：一者根子，二者莖子，
三者枝子，四者華子，五者果子，及餘所生之種子，是謂五種之子。
皆是他方剎土風吹使來，取用作種以此自濟。
如是，比丘！世間有此瑞應，便有生、老、病、
死，致使今日有五盛陰身，不得盡於苦際，此名為劫成敗時之變易也。
吾與汝說，諸佛世尊常所應行，今盡與汝說之。
當樂閑居靜處，當念坐禪，勿起懈怠，今不精誠，後悔無益。
此是我之教誨也。」
^cb738a9爾時，諸比丘聞佛所說，歡喜奉行。
^n2【二】
^cb738a11聞如是：
一時，佛在羅閱城迦蘭陀竹園所，與大比丘眾五百人俱。
^cb738a12是時，摩竭國王阿闍世，在群臣中而作是說：
「此拔祇國極為熾盛，人民眾多，吾當攻伐，攝彼邦土。」
^cb738a15是時，阿闍世王告婆利迦婆羅門曰：「汝今往至世尊所，
持吾姓名，往問訊世尊，禮敬承事云：『王阿闍世白世尊言：
「意欲攻伐拔祇國，為可爾不？」』
設如來有所說者，汝善思惟，來向吾說。
所以然者，如來語終不有二。」
^cb738a20是時，婆羅門受王教勅，往至世尊所，共相問訊，在一面坐。
是時，婆羅門白佛言：「王阿闍世禮敬世尊，
承事問訊，又復重白：『意欲往攻伐拔祇大國。』
先來問佛為可爾不？」
^cb738a24爾時，彼婆羅門以衣覆頭脚，著象牙屐，
腰帶利劍，不應說法。
^cb738a25是時，世尊告阿難曰：「若拔祇人民修七法者，終不為外寇所壞。
云何為七？若當拔祇國人民，盡集一處而不散者，
便不為他國所壞，是謂初法不為外寇所敗。
^cb738a29「復次，阿難！拔祇國人上下和順，拔祇人民不為外人所擒𤢌。
是謂，阿難，第二之法不為外寇所壞。
^cb738b2「復次，阿難！若拔祇國人亦不婬他，著他女人色，是謂第三之法不為外寇所壞。
^cb738b4「復次，阿難！若拔祇國不從此間而傳至彼，
亦復不從彼間傳來至此，是謂第四之法不為外寇所壞。
^cb738b7「復次，阿難！若拔祇國人供養沙門、婆羅門，
承事禮敬梵行人者，是謂第五之法，是時便不為外寇所獲。
^cb738b9「復次，阿難！若拔祇國人民不貪著他財寶者，是謂第六法不為外寇所壞。
^cb738b11「復次，阿難！若拔祇國人民皆同一心，不向神寺，
專精其意，便不為外寇所壞也，是謂第七之法不為外寇所壞。
是謂，阿難！彼拔祇人修此七法者，終不為外人所壞。」
^cb738b15是時，梵志白佛言：「設當彼人成就一法，
猶不可壞，何況七法而可壞乎？
止！止！世尊！國事猥多，欲還所止。」
爾時，梵志即從座起而去。
^cb738b18彼梵志不遠，爾時世尊告諸比丘：「我今當說七不退轉法，汝等諦聽！
善思念之。」
^cb738b20諸比丘白佛言：「唯然，世尊！」
爾時，諸比丘從佛受教。
世尊告曰：「云何為七不退轉之法？
比丘當知，若比丘共集一處，皆共和順，
上下相奉，轉進於上，修諸善法而不退轉，
亦不為魔所得便，是謂初法不退轉。
^cb738b25「復次，眾僧和合順從其教，轉進於上而不退轉，
不為魔王所壞，是謂第二之法不退轉也。
^cb738b27「復次，比丘！不著事務，不修世榮，轉進於上，
不為魔天所得其便，是謂第三不退轉之法也。
^cb738b29「復次，比丘！不諷誦雜書，終日策役其情意，
轉進於上，不為魔王得其便，是謂第四不退轉之法也。
^cb738c2「復次，比丘！勤修其法，除去睡眠，恒自警寤，
轉進於上，不為弊魔而得其便，是謂第五不退轉之法。
^cb738c5「復次，比丘！不學算術，亦不使人習之，樂閑靜之處，
修習其法，轉進於上，不為弊魔得其便也。
是謂第六不退轉之法。
^cb738c8「復次，比丘！起一切世間不可樂想，習於禪行，
忍諸法教，轉進於上，不為魔所得其便，是謂七不退轉法也。
若有比丘成就此七法，共和順者，便不為魔得其便也。」
^cb738c12爾時，世尊說此偈言：
^gatha「除去於事業，又非思惟亂，
^gatha設不行此者，亦不得三昧。
^gatha能樂於法者，分別其法義，
^gatha比丘樂此行，便致三昧定。
^cb738c17「是故，比丘！當求方便，成此七法。
如是，諸比丘！當作是學。」
^cb738c18爾時，諸比丘聞佛所說，歡喜奉行。
^n3【三】
^cb738c20聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb738c21爾時，世尊告諸比丘：「我今當說七使，汝等善思念之。」
^cb738c22諸比丘對曰：「如是。
世尊！」是時，諸比丘從佛受教。
^cb738c23世尊告曰：「云何為七？
一者貪欲使，二者瞋恚使，三者憍慢使，
四者癡使，五者疑使，六者見使，七者欲世間使。
是謂，比丘！有此七使，使眾生之類，永處幽闇，
纏結其身，流轉世間，無有休息，亦不能知生死根原。
猶如彼二牛，一黑一白，共同一軛，共相牽引，不得相遠。
此眾生類，亦復如是，為此貪欲使、無明使所纏結，
不得相離，其餘五使，亦復追從，五使適從，七使亦然。
若凡夫之人，為此七使所縛，流轉生死，
不得解脫，不能知苦之元本。
^cb739a4「比丘當知，由此七使，便有三惡趣：地獄、
畜生、餓鬼；由此七使，不能得度弊魔境界。
然此七使之法復有七藥。
云何為七？貪欲使者，念覺意治之；瞋恚使者，
法覺意治之；邪見使者，精進覺意治之；
欲世間使者，喜覺意治之；憍慢使者，猗覺意治之；
疑使者，定覺意治之；無明使者，護覺意治之。
是謂，比丘！此七使用七覺意治之。
^cb739a12「比丘當知，我本未成佛道為菩薩行，坐道樹下，
便生斯念：『欲界眾生為何等所繫？』
復作是念：『此眾生類為七使流轉生死，
永不得解；我今亦為此七使所繫，不得解脫。』
爾時，復作是念：『此七使為用何治之？』
復重思惟：『此七使者當用七覺意治之，我當思惟七覺意。』
思惟七覺意時，有漏心盡，便得解脫，後成無上正真之道，
七日之中結跏趺坐，重思惟此七覺意。
是故，諸比丘！若欲捨七使者，當念修行七覺意法。
如是，諸比丘！當作是學。」
^cb739a22爾時，諸比丘聞佛所說，歡喜奉行。
^n4【四】
^cb739a24聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb739a25爾時，世尊告諸比丘：「有七種之人可事、
可敬，是世間無上福田。
云何為七種人？所謂七人者：一者行慈，
二者行悲，三者行喜，四者行護，五者行空，
六者行無想，七者行無願。
是謂七種之人可事、可敬，是世間無上福田。
所以然者，其有眾生行此七法者，於現法中獲其果報。」
^cb739b2爾時，阿難白世尊言：「何以故不說須陀洹、
斯陀含、阿那含、阿羅漢、辟支佛、佛，乃說此七事乎？」
^cb739b4世尊告曰：「行慈七人其行，與須陀洹乃至佛其事不同；
雖供養須陀洹乃至佛，不現得報；然供養此七人者，於現世得報。
是故，阿難！當勤加勇猛成辦七法。
如是，阿難！當作是學。」
^cb739b8爾時，阿難聞佛所說，歡喜奉行。
^n5【五】
^cb739b10聞如是：
一時，佛在毘舍離獼猴池側，與大比丘眾五百人俱。
^cb739b11是時，世尊到時，著衣持鉢，及將阿難入毘舍離乞食。
爾時，毘舍離城內有大長者名毘羅先，
饒財多寶，不可稱計；然復慳貪無惠施之心，
唯食宿福，更不造新。
爾時，彼長者將諸婇女在後宮作倡伎樂，自相娛樂。
^cb739b16爾時，世尊往詣彼巷，知而問阿難曰：「今聞作倡伎樂為是何家？」
^cb739b18阿難白佛：「是毘羅先長者家。」
佛告阿難：「此長者却後七日命終，當生涕哭地獄中。
所以然者，此是常法。
若斷善根之人，命終之時，皆生涕哭地獄中。
今此長者宿福已盡，更不造新。」
^cb739b22阿難白佛言：「頗有因緣使此長者七日不命終乎？」
^cb739b23佛告阿難：「無此因緣得不命終乎！
昔所種行，今日已盡，此不可免。」
^cb739b25阿難白佛：「頗有方宜令此長者不生涕哭地獄乎？」
^cb739b26佛告阿難：「有此方宜可使長者不入地獄耳。」
^cb739b27阿難白佛言：「何等因緣使長者不入地獄？」
^cb739b28佛告阿難：「設此長者剃除鬚髮，著三法衣，
出家學道者，便得免此罪也。」
^cb739c1阿難白佛言：「今我能使此長者出家學道。」
^cb739c2爾時，阿難辭世尊已，往至彼長者家，在門外立。
是時，長者遙見阿難來，即出奉迎，便請使坐。
時，阿難語長者曰：「今我是一切智人邊聞，
然如來今記：『汝身却後七日當身壞命終，生涕哭地獄中。』」
^cb739c6長者聞已，即懷恐懼，衣毛皆竪，白阿難曰：「頗有此因緣使七日之中不命終乎？」
^cb739c8阿難告曰：「無此因緣令七日中得免命終。」
^cb739c9長者復白言：「頗有因緣我今命終，不生涕哭地獄中乎？」
^cb739c11阿難告曰：「世尊亦有此教：『若當長者剃除鬚髮，
著三法衣，出家學道者，便不入地獄中。』
汝今可宜出家學道，得到彼岸。」
^cb739c13長者白言：「阿難並在前去，我正爾當往。」
^cb739c14是時，阿難便捨而去。
長者便作是念：「言七日者猶常為遠，吾今宜可五欲自娛樂，
然後當出家學道。」
^cb739c17是時，阿難明日復至長者家，語長者曰：
「一日已過，餘有六日在，可時出家。」
^cb739c18長者白言：「阿難並在前，正爾當尋從。」
然彼長者猶故不去。
^cb739c20是時，阿難二日、三日，乃至六日至長者家，
語長者曰：「可時出家，後悔無及。
設不出家者，今日命終，當生涕哭地獄中。」
^cb739c22長者白阿難曰：「尊者並在前，正爾當隨後。」
^cb739c23阿難告曰：「長者！今日以何神足至彼間？
方言先遣吾耶？但今欲共一時俱往。」
^cb739c25是時，阿難將此長者往至世尊所，到已，
頭面禮足，白佛言：「今此長者欲得出家學道，
唯願如來當與剃除鬚髮，使得學道。」
^cb739c28佛告阿難：「汝今躬可度此長者。」
^cb739c29是時，阿難受佛教勅，即時與長者剃除鬚髮，
教令著三法衣，使學正法。
是時，阿難教彼比丘曰：「汝當念修行，念佛、
念法、念比丘僧、念戒、念施、念天、念休息、
念安般、念身、念死，當修行如是之法。
是謂，比丘！行此十念者，便獲大果報，得甘露法味。」
^cb740a6是時，毘羅先修行如是法已，即其日命終，生四天王中。
^cb740a7是時，阿難即闍維彼身，還至世尊所，頭面禮足，在一面立。
爾時，阿難白世尊言：「向者，比丘毘羅先者今已命終，為生何處？」
^cb740a10世尊告曰：「今此比丘命終生四天王。」
^cb740a11阿難白佛言：「於彼命終當生何處？」
^cb740a12世尊告曰：「於彼命終當生三十三天，展轉生豔天、
兜術天、化自在天、他化自在天，從彼命終復還來生，乃至四天王中。
是謂，阿難！毘羅先比丘七變周旋天人之中，
最後得人身，出家學道，當盡苦際。
所以然者，斯於如來有信心故。
^cb740a17「阿難當知，此閻浮提地南北二萬一千由旬，
東西七千由旬，設有人供養閻浮里地人，其福為多不？」
^cb740a19阿難白佛言：「甚多，甚多。
世尊！」
^cb740a20佛告阿難：「若有眾生如𤛓牛頃，信心不絕修行十念者；
其福不可量，無有能量者。
如是，阿難！當求方便，修行十念。
如是，阿難！當作是學。」
^cb740a23爾時，阿難聞佛所說，歡喜奉行。
^n6【六】
^cb740a25聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb740a26爾時，世尊告諸比丘：「我當說極妙之法，
初、中、竟善，義理深邃，具足得修梵行，此經名為淨諸漏法，汝等善思念之。」
^cb740a28比丘對曰：「如是。世尊！」
是時，諸比丘從佛受教。
^cb740a29世尊告曰：「彼云何名為淨諸漏法？
或有有漏，緣見得斷；或有有漏，親近得斷；
或有有漏，遠離得斷；或有有漏，娛樂得斷；
或有有漏，威儀得斷；或有有漏，思惟得斷。
^cb740b4「彼云何有漏由見得斷？
於是，凡夫之人不覩聖人，不順從如來之法，
不能擁護賢聖之法，不親近善知識，不與善知識從事。
其聞法所應思惟法者亦不分別，不應思惟者而思惟之。
未生欲漏而生，已生欲漏便增多；未生有漏而生，
已生有漏便增多；未生無明漏而生，已生無明漏便增多。
此法不應思惟而思惟之。
^cb740b12「彼云何法應思惟，然不思惟此法？
所言思惟法者，未生欲漏使不生，已生欲漏而滅之；
未生有漏令不生，已生有漏而滅之；未生無明漏令不生，已生無明漏而滅之。
是謂此法應可思惟而不思惟，所不應思惟者便思惟之。
應思惟者復不思惟之，未生欲漏而生，
已生欲漏而增多；未生有漏而生，已生有漏而增多；
未生無明漏而生，已生無明漏而增多。
彼人作如是思惟：『云何有過去久遠？
我今當有過去久遠？』
或復思惟：『無過去久遠？
云何當有過去久遠？
為誰有過去久遠？
云何復有當來久遠？
我今當有將來久遠？』
或復言：『無將來久遠。
云何當有將來久遠？
為誰有將來久遠？
云何有此眾生久遠？
此眾生久遠為從何來？
從此命終當生何處？』
彼人起此不祥之念，便興六見，展轉生耶想——有我見，
審有此見；無有我見，審興此見；有我見、
無我見，於中起審見；又復自觀身，復興此見：
於己而不見己；復興此見：於無我而不見無我，於中起此見。
爾時，彼人復生此邪見：我者即是今世，
亦是後世，常存於世而不朽敗，亦不變易，復不移動。
是謂名為邪見之聚。
邪見、災患、憂、悲、苦、惱，皆由此生而不可療治，
亦復不能捨，遂增苦本，由是不為沙門之行、涅槃之道。
^cb740c8「又復，比丘！賢聖弟子修其法，不失次敘，
善能擁護，與善知識共從事，彼能分別；
不可思惟法亦能知；所可思惟法亦能知之；
彼所不應思惟法亦不思惟之；所應思惟法而思惟之。
^cb740c12「彼云何不應思惟法而不思惟之？
於是，諸法未生欲漏而生，已生欲漏而增多；
未生有漏而生，已生有漏而增多；未生無明漏而生，已生無明漏而增多。
是謂此法不應思惟之。
^cb740c16「彼何等法應思惟者而思惟之？
於是，諸法未生欲漏而不生，已生欲漏而滅之；
未生有漏而不生，已生有漏而滅之；未生無明漏而不生，已生無明漏而滅之。
是謂此法應可思惟之。
^cb740c21「彼不應思惟者亦不思惟之，可思惟者便思惟之，
彼作如是思惟，便滅三法。
云何為三？身耶、戒盜、疑。
設不知、見，則增有漏之行；設見、聞、念、知者，
則不增有漏之行；已知、已見，有漏便不生。
是謂此漏見所斷也。
^cb740c26「彼云何漏恭敬所斷？
於是，比丘！堪忍飢寒，勤苦風雨、蚊虻、惡言、
罵辱，身生痛腦，極為煩疼，命垂欲斷，便能忍之。
若不爾者，便起苦惱。
設復能堪忍者，如是不生，是謂此漏恭敬所斷。
^cb741a1「彼云何漏親近所斷？
於是，比丘！持心受衣，不興榮飾，但欲支其形體，
欲除寒熱，欲令風雨不加其身，又覆形體不令外露。
又持心隨時乞食，不起染著之心，但欲趣支形體，
使故痛得差，新者不生；守護眾行，無所觸犯，
長夜安隱而修梵行，久存於世。
復持心意親近床座，亦不著榮華服飾，
但欲除飢寒、風雨、蚊虻之類，趣支其形，得行道法。
又復持心親近醫藥，不生染著之心；於彼醫藥，
但欲使疾病除愈故，身體得安隱。
設不親近者，則生有漏之患。
若親近，則無有漏之患，是謂此漏親近所斷。
^cb741a13「彼云何有漏遠離所斷？
於是，比丘！除去亂想，猶如惡象、駱駝、牛、
馬、虎、狼、狗、蛇蚖、深坑、危岸、柘棘、峻崖、濕泥，皆當遠離之。
