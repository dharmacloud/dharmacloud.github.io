復不相應者，彼盡分別諸受之名，先分別欲受之名，
而不分別見受、戒受、我受之名。
所以然者，以彼沙門、婆羅門不能分別此三受之名。
是故，或有沙門、婆羅門盡分別此諸受，
彼便分別欲受、見受，不分別戒受、我受。
所以然者，以彼沙門、婆羅門不能分別二受。
若使沙門、婆羅門盡能分別諸受，或復有不具者，
彼便能分別欲受、見受、戒受，不分別我受。
所以然者，以彼沙門、婆羅門不能分別我受故。
是故，復有沙門、婆羅門盡分別諸受，然復有不具者。
^cb644a28「此名四受，有何等義？
云何分別？所謂四受者，由愛而生。
如是，比丘！有是妙法所應分別。
若有不行此諸受，此不名為平等。
所以然者，諸法之義難了難解。
如此非法之義者，非三耶三佛之所說也。
比丘當知，如來盡能分別一切諸受，以能分別一切諸受，
則與相應，則能分別欲受、見受、我受、戒受。
是故，如來盡分別諸受，則與法共相應，無有相違。
