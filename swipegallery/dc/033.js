為非怒者所究竟？
所謂此究竟，彼當作是說，非怒者所究竟。
^cb644a5「比丘！有此二見，云何為二見？
所謂有見、無見。諸有沙門、婆羅門不知此二見之本末，
彼便有欲心、有瞋恚心、有愚癡心、有愛心、
