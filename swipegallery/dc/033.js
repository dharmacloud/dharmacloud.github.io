若復比丘知其時節，不失時宜，此名為隨其方宜。
如是，比丘知其時宜。
^cb728c18「云何比丘自能修己？
於是，比丘能自知己：『我今有此見聞念知，
有如是智慧，行步進止，恒隨正法。』
若有比丘不能自知智慧之宜，出入行來，此非比丘也。
以其比丘能自修己進止之宜，此名為自修己行，是謂比丘能自知己。
^cb728c23「云何比丘自知止足？
於是，比丘能自籌量睡眠、覺寤、坐臥、經行、
進止之宜，皆能知止足。
若有比丘不能知是者，則非比丘也。
以其比丘能解了此故，名為知足。
如是，比丘名為知足。
^cb728c27「云何比丘知入大眾？
於是，比丘分別大眾，此是剎利種，此是婆羅門眾，
此是長者眾，此是沙門眾，我當以此法宜則適彼眾中，
可語可默，皆悉知之。
若有比丘不知入眾，此非比丘。
以其比丘知入大眾故，名為知入眾也。
是謂比丘知入大眾也。
^cb729a4「云何比丘知眾人根元？
比丘當知，有二人。
云何為二？彼或有一人，欲往至園中親覲比丘；彼第二人不喜至彼觀見比丘。
彼人欲至園中親覲比丘者，此人最為上。
^cb729a8「比丘！復有二人。云何為二？
彼一人雖至比丘所，然不問其宜；彼第二人亦不往至寺中見比丘。
彼至寺人最為第一。
^cb729a11「比丘！復有二人。云何為二？
彼一人至比丘所問訊時宜；彼第二人不至比丘所問訊時宜。
彼人至寺者，最尊第一，出彼人上。
^cb729a14「比丘！復有二人。云何為二？
彼一人至比丘所，至心聽法；彼第二人不至比丘所，不至心聽法。
彼至心聽法者，於彼人最為第一。
^cb729a17「比丘！復有二人。云何為二？
彼有一人能觀察法，受持諷誦；彼第二人，不能受持諷誦。
彼人受持諷誦者，於此人上最為第一。
^cb729a20「比丘！復有二人。云何為二？
彼有一人，聞法解其義；彼第二人，聞法不解其義。
彼人聞法解義者，於此人最尊第一。
^cb729a23「比丘！復有二人。云何為二？
彼有一人聞法，法成就；彼第二人不聞法，法不成就。
彼人法法成就者，於此人第一。
^cb729a26「比丘！復有二人。云何為二？
彼一人聞法能堪忍修行，分別護持正法；第二人不能堪忍修行其法。
彼能修行法者，於此諸人最尊第一。
猶如牛有酪，由酪有酥，由酥有醍醐，最為第一，無能及者。
此亦如是，若有人能修行者，此人最為第一，
無能及者，是謂比丘觀察人根。
若有人不了此者，則非比丘也。
以其比丘聞法分別其義者，此為最上。
如是比丘觀察人根。
若有比丘成就七法者，於現法中快樂無為，意欲斷漏亦無有疑。
是故，比丘！當求方便，成此七法。
如是，比丘！當作是學。」
^cb729b9爾時，諸比丘聞佛所說，歡喜奉行。
^n2【二】
^cb729b11聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb729b12爾時，世尊告諸比丘：「三十三天晝度樹，
本縱廣五十由旬，高百由旬，東、西、南、北蔭覆五十由旬，三十三天在彼四月自相娛樂。
比丘當知，或有是時，彼晝度樹華葉凋落，萎黃在地。
爾時，諸天見此瑞應，普懷歡喜，欣情內發：『此樹不久當更生華實。』
^cb729b17「比丘當知，或有是時，彼樹華實皆悉凋落，捐棄在地。
是時，三十三天倍復歡喜，自相謂言：『此樹不久當作灰色。』
^cb729b20「比丘當知，復經歷數時，彼樹便作灰色。
是時，三十三天已見此樹而作灰色，甚懷喜悅，
自相謂言：『而今此樹已作灰色，不久當生羅網。』
是時，三十三天見此晝度樹已生羅網，不久當生雹節。
爾時，三十三天見已，復懷歡喜：『此樹今日已生雹節，不久當復開敷。』
^cb729b26「比丘當知，三十三天已見此樹漸漸開敷，
各懷歡喜：『此樹不久漸漸開敷，不久當盡著華。』
^cb729b28「比丘當知，或有是時，彼樹普悉開敷，皆懷歡喜：『此樹今日皆悉著華。』
爾時，此香逆風，百由旬內無不聞香者。
爾時，諸天四月之中於彼自相娛樂，樂不可計。
^cb729c3「此亦如是，若賢聖弟子意欲出家學道時，如似彼樹始欲凋落。
^cb729c4「復次，賢聖弟子捐棄妻財，以信堅固，出家學道，
剃除鬚髮，如似彼樹葉落在地。
^cb729c6「比丘當知，若賢聖弟子無貪欲想，除不善法，
念持歡喜，遊志一禪，似彼晝度樹而作灰色。
^cb729c8「復次，賢聖弟子有覺、有觀息，內有歡喜，
專其一心，無覺、無觀、遊心二禪，如似彼樹而生羅網。
^cb729c10「復次，賢聖弟子念而有護，自覺身有樂，
諸賢聖所救，護念具足，遊在三禪，如似彼樹而生雹節。
^cb729c13「復次，賢聖弟子苦樂已盡，先無愁憂，無苦無樂，
護念清淨，遊志四禪，如似彼樹漸漸開敷。
^cb729c15「復次，賢聖弟子盡有漏，成無漏心解脫、
智慧解脫，現法中而自娛樂：生死已盡，
梵行已立，所作已辦，更不復受胎，如實知之。
如似彼樹皆悉敷花。
^cb729c18「是時，賢聖弟子戒德之香，遍聞四遠，無不稱譽者，
四月之中而自娛樂，遊心四禪，具足行本。
是故，諸比丘！當求方便，成戒德之香。
如是，比丘！當作是學。」
^cb729c22爾時，諸比丘聞佛所說，歡喜奉行。
^n3【三】
^cb729c24聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb729c25爾時，世尊告諸比丘：「我今當說七事水喻，人亦如是。
諦聽！諦聽！善思念之。」
^cb729c26諸比丘對曰：「如是。
世尊！」
^cb729c27世尊告曰：「彼云何七事水喻而似人？
猶如有人沒在水底，如復有人暫出水還沒，
如復有人出水觀看，如復有人出頭而住，
如復有人於水中行，如復有人出水而欲到彼岸，如復有人已到彼岸。
是謂，比丘！七事水喻出現於世。
^cb730a3「彼云何人沒在水底而不得出？
於是，或有一人，不善之法遍滿其體，當經歷劫數，
不可療治，是謂此人沒在水底。
^cb730a6「彼何等人出水還沒？
或有一人信根漸薄，雖有善法而不牢固，
彼身、口、意行善，後復身、口、意行不善法，
身壞命終，生地獄中，是謂此人出水還沒。
^cb730a10「彼何等人出水觀看？
於是，或有人有信善根，身、口、意行，更不增益其法，
自守而住，彼身壞命終，生阿須倫中，是謂此人出水而觀。
^cb730a13「彼何等人出水住者？
於是，或有人有信精進，斷三結使，更不退轉，
必至究竟成無上道，是謂此人出水而住。
^cb730a15「彼何等人欲渡水者？
於是，或有人信根精進，恒懷慚愧，斷三結使，
婬、怒、癡薄，來至此世而斷苦際，是謂此人欲渡水者。
^cb730a18「彼何等人欲至彼岸？
或有人信根精進，斷下五結，成阿那含，
即彼般涅槃，更不來此世，是謂此人欲至彼岸者也。
^cb730a21「何等人已至彼岸者？
於是，或有一人，信根精進而懷慚愧，盡有漏成無漏，
於現法中而自娛樂；生死已盡，梵行已立，
所作已辦，更不復受胎，如實知之。
於此無餘涅槃界而般涅槃，是謂此人已渡彼岸者也。
^cb730a26「是謂，比丘！有此七人水喻向汝等說，諸佛世尊所應修行接度人民，
今已施行。當在閑居靜處，若在樹下，當念坐禪，勿起懈怠。
此是我之教誨。」
^cb730a29爾時，諸比丘聞佛所說，歡喜奉行。
^n4【四】
^cb730b2聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb730b3爾時，世尊告諸比丘：「聖王在遠國治化，
七法成就，不為怨家盜賊所擒獲。
云何為七？然彼城郭極為高峻，修治齊整，是謂彼王先成就第一之法。
復次，彼城門戶牢固，是謂彼城成就第二法。
復次，彼城外塹極深且廣，是謂此城成就第三之法。
復次，彼城內多諸穀米，倉庫盈滿，是謂彼城成就第四之法。
復次，彼城饒諸薪草，是謂彼城成就第五之法。
復次，彼城多諸器杖，備諸戰具，是謂彼城成就六法。
復次，彼城主極聰明高才，豫知人情，可鞭則鞭，
可治則治，是謂彼城成就七法，外境不能來侵。
是謂，比丘！彼城國主成就此七法，外人不得嬈近。
^cb730b16「此比丘亦復如是，若成就七法，弊魔波旬不得其便。
云何為七？於是，比丘！
戒律成就，威儀具足，犯小律尚畏，何況大者！
是謂比丘成就此第一之法，弊魔不得其便，
猶如彼城，高廣極峻，不可沮壞。
^cb730b20「復次，比丘！若眼見色，不起想著，亦不興念，
具足眼根，無所缺漏，而護眼根；耳聲、鼻香、
舌味、身觸、意法，亦復如是，亦不起想，具足意根而無亂想，
具足擁護意根，是謂比丘成就此第二法，
弊魔波旬不得其便，如彼城郭，門戶牢固。
^cb730b26「復次，比丘！多聞不忘，恒念思惟正法道教，
昔所經歷皆悉備知，是謂比丘成就此第三法，
弊魔波旬不得其便，如彼城郭，外塹極深且廣。
^cb730b29「復次，比丘！多諸方便，所有諸法，初善、中善、
竟善，具足清淨，得修梵行，是謂比丘成就此第四法，
如彼城郭，多諸穀米，外寇不敢來侵。
^cb730c3「復次，比丘！思惟四增上心之法，亦不脫漏，
是謂比丘成就此第五之法，弊魔波旬不得其便，
如彼城郭，多諸薪草，外人不能來觸嬈。
^cb730c6「復次，比丘！得四神足，所為無難，是謂比丘成就此第六之法，
弊魔波旬不得其便，如彼城內，器杖備具。
^cb730c9「復次，比丘！具能分別陰、入、界，亦復分別十二因緣所起之法，
是謂比丘成就此七法，弊魔波旬不得其便，
如彼城郭之主，聰明高才，可收則收，可捨則捨。
^cb730c12「今此比丘亦復如是，具知分別陰、持、入諸病。
若有比丘成就此七法者，弊魔波旬終不得其便。
是故，諸比丘！當求方宜，分別陰、持、入及十二因緣，
不失次第，便度魔界，不處其中。
如是，比丘！當作是學。」
^cb730c17爾時，諸比丘聞佛所說，歡喜奉行。
