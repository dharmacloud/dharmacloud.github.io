能熟思惟不去心首，設不將護者，則生有漏；
設擁護者，則不生有漏，是謂有漏遠離所斷。
^cb741a19「彼云何有漏娛樂所斷？
於是，比丘！生欲想而不捨離，設起瞋恚想，
亦不捨離，設復起疾想，亦不捨離，設不捨離者，
則生有漏；能捨離者，便能不起有漏。
是謂此漏娛樂所斷。
^cb741a23「云何有漏威儀所斷？
於此，比丘！若眼見色不起色想，亦不起染污之心，
具足眼根，亦無缺漏而護眼根。
若耳聞聲、鼻嗅香、舌知味、身知細滑、意知法，
都不起染污之心，亦不起想著而護意根。
若不攝其威儀，則生有漏；若攝其威儀者，則無有漏之患。
是謂此漏威儀所斷。
^cb741a29「彼何等有漏思惟所斷？
於是，比丘！修念覺意，依無欲、依無污、依滅盡而求出要，
修法覺意、精進覺意、喜覺意、猗覺意、定覺意、
護覺意，依無欲、依無污、依滅盡而求出要。
若不修此者，則生有漏之患；設能修者，則不生有漏之患。
是謂此漏思惟所斷。
^cb741b6「若復，比丘！於比丘中諸所有漏，見斷者便見斷之，
恭敬斷者便恭敬斷之，親近斷者親近斷之，
遠離斷者遠離斷之，威儀斷者威儀斷之，思惟斷者思惟斷之。
是謂，比丘！具足一切威儀，能斷結、去愛，
度於四流，漸漸越苦。
是謂，比丘！除有漏之法。
諸佛世尊常所施行慈念一切有形之類，今已施行。
汝等常樂閑居樹下，勤加精進，勿有懈怠。
今不勤加者，後悔無益。
此是我之訓教。」
