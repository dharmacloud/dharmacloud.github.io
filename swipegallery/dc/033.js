於現法中自身作證而自遊戲：生死已盡，
梵行已立，所作已辦，更不復受胎，如實知之。
是謂柔軟中柔軟沙門。
^cb654a4「是謂，比丘！有此四人出現於世。
是故，諸比丘！當求方便，於柔軟中作柔軟沙門。
如是，諸比丘！當作是學。」
^cb654a6爾時，諸比丘聞佛所說，歡喜奉行。
