此義我一向說，苦、苦習、苦滅、苦滅道跡，我一向說。
以何等故，我一向說此？
此是義相應，是法相應，是梵行本，趣智、
趣覺、趣於涅槃，是故我一向說此。
是為不可說者則不說，可說者則說，當如是持，當如是學。」
