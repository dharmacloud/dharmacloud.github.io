一時，佛遊舍衛國，在勝林給孤獨園。
^cb603a6爾時，世尊告諸比丘：「優陀羅羅摩子，彼在眾中，
數如是說：『於此生中，觀此覺此，不知癰本，然後具知癰本。』
優陀羅羅摩子無一切知，自稱一切知；
實無所覺，自稱有覺。
優陀羅羅摩子如是見、如是說，有者，是病、
是癰、是刺；設無想者，是愚癡也；若有所覺，
是止息、是最妙，謂乃至非有想非無想處。
彼自樂身，自受於身，自著身已，修習乃至非有想非無想處，
身壞命終，生非有想非無想天中，彼壽盡已，
復來此間，生於狸中。
此比丘正說者，於此生中，觀此覺此，不知癰本，然後具知癰本。
^cb603a17「云何比丘正觀耶？
比丘者，知六更觸，知習、知滅、知味、知患、
知出要，以慧知如真，是謂比丘正觀也。
云何比丘覺，比丘者，知三覺，知習、知滅、
知味、知患、知出要，以慧知如真，是謂比丘覺。
云何比丘不知癰本，然後具知癰本？
比丘者，知有愛滅，拔其根本，至竟不復生，
是謂比丘不知癰本，然後具知癰本。
癰者，謂此身也。色麤四大，從父母生，飲食長養，
衣被按摩，澡浴強忍，是無常法、壞法、散法，是謂癰也。
癰本者，謂三愛也。
欲愛、色愛、無色愛，是謂癰本。
癰一切漏者，謂六更觸處也。
眼漏視色，耳漏聞聲，鼻漏嗅香，舌漏嘗味，
身漏覺觸，意漏知諸法，是謂癰一切漏。
