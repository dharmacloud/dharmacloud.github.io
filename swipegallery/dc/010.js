汝等當知欲，知欲所因生，知欲有報，知欲勝如，
知欲滅盡，知欲滅道。
汝等當知業，知業所因生，知業有報，知業勝如，
知業滅盡，知業滅道。
汝等當知苦，知苦所因生，知苦有報，知苦勝如，
知苦滅盡，知苦滅道。
^cb599b25「云何知漏？謂有三漏：欲漏、有漏、無明漏，是謂知漏。
云何知漏所因生？
謂無明也。因無明則便有漏，是謂知漏所因生。
云何知漏有報？謂無明纏者，為諸漏所漬，
彼因此受報，或得善處，或得惡處，是謂知漏有報。
云何知漏勝如？謂或有漏生地獄中，或有漏生畜生中，
或有漏生餓鬼中，或有漏生天上，或有漏生人間，是謂知漏勝如。
云何知漏滅盡？謂無明滅，漏便滅，是謂知漏滅盡。
云何知漏滅道？謂八支聖道，正見乃至正定為八，是謂知漏滅道。
若比丘如是知漏，知漏所因生，知漏受報，
知漏勝如，知漏滅盡，知漏滅道者，是謂達梵行，能盡一切漏。
