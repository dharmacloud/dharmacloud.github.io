憍為心結、慢為心結，妬為心結、增上慢為心結、
貪為心結，諸比丘！
若有人有此二十一結染著心者，當觀其人必墮惡趣，不生善處。
猶如白㲲新衣，久久朽故，多諸塵垢，意欲染成其色，
青、黃、赤、黑終不得成。
何以故？以有塵垢故。
如是，比丘！若有人以此二十一結染著心者，
當觀其人必墮惡趣，不生善處。
^cb573c23「設復有人無此二十一結染著心者，當知斯人必生天上，不墮地獄中。
猶如新淨白㲲，隨意欲作何色，青、黃、赤、
黑，必成其色，終不敗壞。
所以然者，以其淨故。
此亦如是，其有無此二十一結染著心者，
當觀其人必生天上，不墮惡趣。
^cb573c28「若彼賢聖弟子起瞋恚心結，觀已，便能息之。
起恚害心結、起睡眠心結、起調戲心結、
起疑心結、起怒心結、起忌心結、起惱心結、
起疾心結、起憎心結、起無慚心結、起無愧心結、
起幻心結、起姦心結、起偽心結、起諍心結、
起憍心結、起慢心結、起妬心結、起增上慢心結、起貪心結。
^cb574a6「若彼賢聖弟子無瞋、無恚、無有愚惑，心意和悅，
以慈心普滿一方而自娛樂，二方、三方、四方亦爾。
四維上下，於一切中，一切亦一切，一切世間以無限無量，
不可稱計，心無恚怒而自遊戲。
以此慈心，遍滿其中，得歡喜已，心意便正。
^cb574a11「復以悲心普滿一方而自娛樂，二方、三方、
四方亦爾，四維上下，於一切中，一切亦一切，
一切世間以無量無限，不可稱計，心無恚怒而自遊戲。
以此悲心，遍滿其中，得歡喜已，心意便正。
^cb574a15「復以喜心普滿一方而自娛樂，二方、三方、
四方亦爾，四維上下，於一切中，一切亦一切，
一切世間，以無量無限不可稱計，心無恚怒而自遊戲。
以此喜心，遍滿其中，得歡喜已，心意便正。
^cb574a20「復以護心普滿一方而自娛樂，二方、三方、
四方亦爾，四維上下，於一切中，一切亦一切，
一切世間以無量無限不可稱計，心無恚怒而自遊戲。
以此護心，遍滿其中，得歡喜已，心意便正。
^cb574a24「便於如來所成於信根，根本不移，豎䜿高顯幢，不可移動。
諸天、龍、神、阿須倫、沙門、婆羅門，或世人民，
