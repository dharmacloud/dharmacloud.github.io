云何身有患心無患。
云何身有病心無病？」
^cb573b3長者白舍利弗言：「實無此辯重問世尊，
身有患心有患，身有患心無患。
尊者舍利弗必有此辯，願具分別。」
^cb573b5舍利弗言：「諦聽！諦聽！
