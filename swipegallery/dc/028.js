終不從其法，如實而不知，此則非沙門、婆羅門。
於沙門，則犯沙門法；於婆羅門，則犯婆羅門法，
此沙門、婆羅門終不以身作證而自遊戲。
諸有沙門、婆羅門於此二見誦讀、諷念，
知捨，如實而知，此則沙門持沙門行，婆羅門知婆羅門行，
自身取證而自遊戲：生死已盡，梵行已立，
更不復受有，如真知之。
是故，諸比丘！於此二見不應習行，不應諷誦，盡當捨離。
如是，諸比丘！當作是學。」
