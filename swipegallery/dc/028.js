此是如來、至真、等正覺、明行成為、善逝、
世間解、無上士、道法御、天人師，號佛、眾祐，
於中得歡喜，心意便正，亦復成就於法。
如來法者，甚為清淨，不可移動，人所愛敬。
如是智者當作是觀，便於中而得歡喜，亦復成就於眾。
如來聖眾甚為清淨，性行純和，法法成就，
戒成就、三昧成就、智慧成就、解脫成就、解脫見慧成就。
聖眾者，四雙八輩，此是如來聖眾，可愛可貴，實可承事。
於中得歡喜，心意便正。
^cb574b7「彼復以此三昧，心清淨無瑕穢，諸結便盡，
亦無沾污，性行柔軟，逮於神通，便得自識無量宿命事，
所從來處，靡不知之。
