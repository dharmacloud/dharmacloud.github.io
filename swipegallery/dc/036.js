斷三結使，成須陀洹。
^cb788c20「爾時，諸剎利婦聞彌勒如來出現世間，
成等正覺，數千萬眾往至佛所，頭面禮足，在一面坐。
各各生心，求作沙門，出家學道；或有越次取證，或有不取證者。
爾時，阿難！其不越次取證者，盡是奉法之人，患厭一切世間不可樂想。
爾時，彌勒當說三乘之教，如我今日弟子之中，
大迦葉者行十二頭陀，過去諸佛所善修梵行，
此人常佐彌勒，勸化人民。」
^cb788c28爾時，迦葉去如來不遠，結加趺坐，正身正意，繫念在前。
爾時，世尊告迦葉曰：「吾今年已衰耗，年向八十餘。
然今如來有四大聲聞，堪任遊化，智慧無盡，眾德具足。
云何為四？所謂大迦葉比丘、君屠鉢漢比丘、
賓頭盧比丘、羅云比丘。
汝等四大聲聞要不般涅槃，須吾法沒盡，然後乃當般涅槃。
大迦葉亦不應般涅槃，要須彌勒出現世間。
所以然者，彌勒所化弟子，盡是釋迦文佛弟子，由我遺化得盡有漏。
摩竭國界毘提村中，大迦葉於彼山中住。
又彌勒如來將無數千人眾，前後圍遶，
往至此山中，遂蒙佛恩，諸鬼神當與開門，使得見迦葉禪窟。
^cb789a12「是時，彌勒伸右手指示迦葉，告諸人民：
『過去久遠釋迦文佛弟子，名曰迦葉！
今日現在，頭陀苦行最為第一。』
是時，諸人民見已，歎未曾有，無數百千眾生，
諸塵垢盡，得法眼淨。
或復有眾生，見迦葉身已，此名為最初之會，
九十六億人，皆得阿羅漢。
斯等之人皆是我弟子。
所以然者，悉由受我教訓之所致也。
亦由四事因緣：惠施、仁愛、利人、等利。
爾時，阿難！彌勒如來當取迦葉僧伽梨著之。
是時，迦葉身體奄然星散。
是時，彌勒復取種種香華，供養迦葉。
所以然者，諸佛世尊有恭敬心於正法故。
彌勒亦由我所受正法化，得成無上正真之道。
^cb789a25「阿難當知，彌勒佛第二會時，有九十四億人，
皆是阿羅漢，亦復是我遺教弟子，行四事供養之所致也。
又彌勒第三之會九十二億人，皆是阿羅漢，亦復是我遺教弟子。
^cb789a28「爾時，比丘姓號，皆名慈氏弟子，如我今日諸聲聞皆稱釋迦弟子。
爾時，彌勒與諸弟子說法：『汝等比丘，當思惟無常之想、
樂有苦想、計我無我想、實有空想、色變之想、
青瘀之想、腹脹之想、食不消想、血想、一切世間不可樂想。
所以然者，比丘當知，此十想者，皆是過去釋迦文佛與汝等說，
令得盡有漏，心得解脫。
^cb789b7「『若復此眾中釋迦文佛弟子，過去時修於梵行，
來至我所；或於釋迦文佛所，奉持其法，
來至我所；或復於釋迦文佛所，供養三寶，
來至我所；或於釋迦文佛所，彈指之頃，
修於善本，來至此間；或於釋迦文佛所，
行四等心，來至此者；或於釋迦文佛所，
受持五戒、三自歸，來至我所；或於釋迦文佛所，
起神寺廟，來至我所；或於釋迦文佛所，
補治故寺，來至我所；或於釋迦文佛所，
受八關齋法，來至我所；或於釋迦文佛所，
香花供養，來至此者；或復於彼聞佛法，
悲泣墮淚，來至我所；或復於釋迦文佛，
專意聽法，來至我所；復盡形壽善修梵行，
來至我所；或復書讀諷誦，來至我所者；
承事供養，來至我所者。』
^cb789b21「是時，彌勒便說此偈：
^gatha「『增益戒聞德，禪及思惟業，
^gatha善修於梵行，而來至我所。
^gatha勸施發歡心，修行心原本，
^gatha意無若干想，皆來至我所。
^gatha或發平等心，承事於諸佛，
^gatha飯食於聖眾，皆來至我所。
^gatha或誦戒契經，善習與人說，
^gatha熾然於法本，今來至我所。
^gatha釋種善能化，供養諸舍利，
^gatha承事法供養，今來至我所。
^gatha若有書寫經，頒宣於素上，
^gatha其有供養經，皆來至我所。
^gatha繒綵及眾物，供養於神寺，
^gatha自稱南無佛，皆來至我所。
^gatha供養於現在，諸佛過去者，
^gatha禪定正平等，亦無有增減。
^gatha是故於佛法，承事於聖眾，
^gatha專心事三寶，必至無為處。』
^cb789c11「阿難當知，彌勒如來在彼眾中當說此偈。
爾時，眾中諸天、人民思惟此十想，十一姟人諸塵垢盡，得法眼淨。
^cb789c13「彌勒如來千歲之中，眾僧無有瑕穢，爾時恒以一偈，
以為禁戒：
^gatha「『口意不行惡，身亦無所犯，
^gatha當除此三行，速脫生死淵。』
^cb789c17「過千歲之後，當有犯戒之人，遂復立戒。
「彌勒如來當壽八萬四千歲，般涅槃後，遺法當存八萬四千歲。
所以然者，爾時眾生皆是利根。
其有善男子、善女人，欲得見彌勒佛，及三會聲聞眾，
及鷄頭城，及見蠰佉王，并四大藏珍寶者，
欲食自然粳米，并著自然衣裳，身壞命終生天上者，
彼善男子、善女人，當勤加精進，無生懈怠，
亦當供養諸法師承事，名華、㨶香種種供養無令有失。
如是，阿難！當作是學。」
^cb789c26爾時，阿難及諸大會聞佛所說，歡喜奉行。
