隨我教勅，自歸命佛、法、聖眾。」
^cb818b12諸子白言：「我不堪任自歸佛、法、聖眾。」
^cb818b13父復告曰：「加賜汝二千、三千、四千、五千兩純金，
宜當自歸佛、法、聖眾，長夜之中獲福無量。」
^cb818b15爾時，諸子聞斯語已，默然受之。
