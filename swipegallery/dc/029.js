金澡罐一枚，牛千頭，好女一人，持用與上坐使呪願。
爾時，上坐告主人曰：『我今受此五百兩金及金杖、
金澡罐，當用供養師。
此女人，及牛千頭還施主人。
所以然者，吾不習欲，亦不積財。』
^cb598b15「是時，超術梵志受此金杖、澡罐已，便往詣鉢摩大國。
其王名曰光明。時，彼國王請定光如來及比丘眾衣食供養。
時，彼國王告令城內：『其有人民有香華，
盡不得賣，若有賣者當重罰之；吾自出買，不須轉賣。』
復勅人民，掃灑令淨，勿使有土沙穢惡；
懸繒幡蓋，香汁塗地，作倡妓樂，不可稱計。
^cb598b22「爾時，彼梵志見已，便問行道人曰：『今是何日？
掃灑道路，除治不淨，懸繒幡蓋，不可稱計。
將非國主太子有所娉娶？』
彼行道人報曰：『梵志不知耶？
鉢摩大國王今請定光如來、至真、等正覺，
衣食供養，故平治道路，懸繒幡蓋耳。』
然梵志祕記亦有此語：『如來出世，甚難得遇，
時時乃出，實不可見，猶如優曇鉢華，時時乃出。』
此亦如是，如來出現於世，甚不可值。
又梵志書亦有語：『有二人出世甚難得值。
云何二人？如來及轉輪聖王。
此二人出現甚難得值。』
爾時，彼復作是念：『我今可急速報佛恩，今且以此五百兩金奉上定光如來。』
復作是念：『書記所載，如來不受金銀珍寶；
我可持此五百兩金，用買華香，散如來上。』
^cb598c8「是時，梵志即入城內求買華香。
爾時，城中行人報曰：『梵志不知耶？
國王有教令，其有香華賣者當重罰之。』
時，彼超術梵志便作是念：『是我薄祐，求華不獲，將知如何？』
便還出城，在門外立。
^cb598c12「爾時，有婆羅門女名曰善味，持水瓶行取水，手執五枚華。
梵志見已，語彼女人曰：『大妹！
我今須華，願妹見賣與我。』
梵志女曰：『我何時是汝妹？
為識我父母不？』時，超術梵志復生此念：
『此女人性行寬博，意在戲笑。』
即復語言：『賢女！我當與價，是非見惠此華。』
梵志女曰：『豈不聞大王有嚴教，不得賣華乎？』
^cb598c19「梵志曰：『賢女！此事無苦，王不奈汝何！
我今急須此五枚華，我得此華，汝得貴價。』
梵志女曰：『汝急須華，欲作何等？』
梵志報曰：『我今見有良地，欲種此華。』
梵志女曰：『此華以離其根，終不可生。
云何方言我欲種之？』
梵志報曰：『如我今日所見良田，種死灰尚生，何況此華？』
^cb598c25「梵志女曰：『何者是良田，種死灰乃生乎？』
梵志報曰：『賢女！有定光佛、如來、至真、等正覺，出現於世。』
梵志女曰：『定光如來為何等類？』
梵志即報彼女曰：『定光如來者，有如是之德，
有如是之戒，成諸功德。』
梵志女曰：『設有功德者，欲求何等福？』
梵志報曰：『願我後生當如定光如來、至真、
等正覺，禁戒、功德亦當如是。』
^cb599a3「梵志女曰：『設汝許我世世作夫婦，我便與汝華。』
梵志曰：『我今所行，意不著欲。』
梵志女曰：『如我今身不求為汝作妻，使我將來世與汝作妻。』
超術梵志曰：『菩薩所行，無有愛惜，設與我作妻者，必壞我心。』
梵志女曰：『我終不壞汝施意，正使持我身施與人者，終不壞施心。』
是時，便持五百金錢，用買五枚華，與彼女人，
共作誓願，各自別去。
^cb599a11「是時，定光如來、至真、等正覺時到著衣持鉢，
與比丘僧前後圍繞，入鉢摩大國。
時，超術梵志遙見定光如來顏貌端政，
見莫不歡，諸根寂靜，行不錯亂，有三十二相、八十種好。
猶如澄水無有穢濁，光明徹照，無所罣礙。
亦如寶山，出諸山上。
見已，便發歡喜心，於如來所持此五莖華，至定光如來所。
到已，在一面住。
^cb599a19「時，超術梵志白定光佛言：『願見採受，設世尊今不授決者，
便當於此處斷其命根，不願此生。』
爾時，世尊告曰：『梵志！
不可以此五莖華授無上等正覺。』
梵志白言：『願世尊與我說菩薩所行法！』
定光佛告曰：『菩薩所行，無所愛惜。』
^cb599a24「爾時，梵志便說偈言：
^gatha「『不敢以父母，持施與外人；
^gatha諸佛真人長，亦復不敢施。
^gatha日月周行世，此二不可施；
^gatha餘者盡可施，意決無有難。』
^cb599a29「爾時，定光佛復以此偈報梵志曰：
^gatha「『如汝所說施，亦不如來言；
^gatha當忍億劫苦，施頭身耳目。
^gatha妻子國財寶，車馬僕從人；
^gatha設能堪與者，今當授汝決。』
^cb599b5「爾時，摩納復說此偈：
^gatha「『大山熾如火，億劫堪頂戴；
^gatha不能壞道意，唯願時授決。』
^cb599b8「爾時，定光如來默然不語。
時，彼梵志手執五莖華，右膝著地，散定光如來，
並作是說：『持是福祐，使將來世當如定光如來、
至真、等正覺，而無有異！』
