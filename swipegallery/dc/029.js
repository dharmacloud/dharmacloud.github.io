來至舍衛祇洹精舍，往至世尊所。
爾時，世尊見曇摩留支來，作是告曰：「善哉！
曇摩留支！久來此間。」
^cb597b9曇摩留支白世尊曰：「如是。
世尊！久來此間。」
^cb597b10爾時，上坐及諸比丘各生斯念：「此曇摩留支恒在世尊左右，
然今世尊告曰：『善哉！
曇摩留支！久來此間。』」
^cb597b12爾時，世尊知諸比丘心中所念，欲斷狐疑故，
便告諸比丘：「非為曇摩留支久來此間，故我言此義。
所以然者，昔者過去無數劫時，有定光如來、
至真、等正覺、明行成為、善逝、世間解、無上士、
道法御、天人師，為佛、眾祐，出現於世，治在鉢摩大國，與大比丘眾十四萬八千人俱。
