猶如新淨白褻，無有塵垢，易染為色。
時梵志婦亦復如是，即於座上得法眼淨，
彼已得法、見法，分別其法，無有狐疑，已逮無畏，
自歸三尊：佛、法、聖眾，受持五戒。
是時，尊者大迦葉重為梵志婦說微妙法已，即從坐起而去。
^cb589b24迦葉去未久時，婦夫婿來至家。
婆羅門見婦顏色甚悅，非復常人。
時，婆羅門即問其婦，婦即以此因緣具向夫婿說之。
時，婆羅門聞是語已，便將其婦共詣精舍。
往至世尊所，時，婆羅門與世尊共相問訊，在一面坐。
婆羅門婦頭面禮世尊足，在一面坐。
