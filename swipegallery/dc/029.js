聞已，各懷愁憂，便往至世尊所，頭面禮足，
在一面住，白世尊曰：「明日清旦，願世尊勿復入城！
所以然者，王阿闍世今有教令，勅語城內人民之類：
『明日勿復在里巷行來，吾欲放醉象，害沙門瞿曇。
設沙門有一切智，明日清旦不入城乞食。』
