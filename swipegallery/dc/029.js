超術梵志前禮師足，便退而去。
^cb598a2「爾時，鉢摩大國去城不遠，有眾梵志普集一處，
欲共大祠，亦欲講論。
時，有八萬四千梵志共集。
第一上坐亦復諷誦外道書䟽，莫不練知，
天文、地理、星宿變怪，皆悉了知。
