即給與所須，又傳此言：「提婆達兜名德不可具記。」
^cb802c27爾時，眾多比丘聞已，往至世尊所，頭面禮足，
白佛言：「提婆達兜者極大神足，能得衣裳、
飲食、床臥具、病瘦醫藥。」
