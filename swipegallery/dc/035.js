照明一切諸法，亦如良藥療治一切眾病，猶如甘露食無厭足。
若不得此七覺意者，眾生之類流轉生死。
諸比丘！當求方便，修七覺意。
如是，諸比丘！當作是學。」
^cb731b12爾時，諸比丘聞佛所說，歡喜奉行。
