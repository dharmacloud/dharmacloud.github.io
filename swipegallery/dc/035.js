是謂第十非法親國之難。
^cb777b20「是謂，比丘！十非法入國之難。
是故，比丘！莫復生心親近國家。
如是，比丘！當作是學。」
^cb777b22爾時，諸比丘聞佛所說，歡喜奉行。
^n7【七】
^cb777b24聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb777b25爾時，世尊告諸比丘：「若國王成就十法者，
不得久存，多諸盜賊。
云何為十？於時國王慳貪，以小輕事，便興瞋恚，不觀義理。
若王成就初法，則不得久存，國饒盜賊。
^cb777b29「復次，彼王貪著財物，不肯庶幾，是謂國王成就此二法，則不得久存。
^cb777c1「復次，彼王不受人諫，為人暴虐，無有慈心，
是謂第三法，不得久存。
^cb777c3「復次，彼王枉諸人民，橫取繫閉，在牢獄中，
無有出期，是謂第四法，不得久存。
^cb777c5「復次，國王非法相，佐不案正行，是謂五法，不得久存。
^cb777c6「復次，彼王貪著他色，遠離己妻，是謂彼王成就六法，不得久存。
^cb777c7「復次，國王好喜嗜酒，不理官事，是謂成就七法，不得久存。
^cb777c9「復次，國王好喜歌舞戲樂，不理官事，是謂第八法，不得久存。
^cb777c10「復次，國王恒抱長患，無有強健之日，是謂第九之法，不得久存。
^cb777c12「復次國王不信忠孝之臣，翅羽尠少，無有強佐，
是謂國王成就此十法，不得久存。
^cb777c14「今比丘眾亦復如是，若成就十法，不增善本功德，
身壞命終，入地獄中。
何謂十法？於是，比丘不持禁戒，亦無恭恪之心，
是謂比丘成就初法，不得究竟有所至到。
^cb777c18「復次，比丘不承事佛，不信真言，是謂比丘成就第二之法，不得久住。
^cb777c19「復次，比丘不承事法，漏諸戒律，是謂比丘成就第三之法，不得久住。
^cb777c21「復次，比丘承事聖眾，恒自卑意，不信彼受，
是謂比丘成就第四之法，不得久住。
^cb777c23「復次，比丘貪著利養，心不放捨，是謂比丘成就第五之法，不得久住。
^cb777c24「復次，比丘不多學問，不勤加誦讀翫習，
是謂比丘成就六法，不得久存。
^cb777c26「復次，比丘不與善知識從事，恒與惡知識從事，
是謂比丘第七之法，不得久存。
^cb777c28「復次，比丘恒喜事役，不念坐禪，是謂第八之法，不得久存。
^cb777c29「復次，比丘復著算數，返道就俗，不習正法，
是謂比丘第九之法，不得久存。
^cb778a2「復次，比丘不樂修梵行，貪著不淨，是謂比丘第十之法，不得久存。
是謂，比丘！成就此十法者，必墮三惡趣，不生善處。
^cb778a5「若國王成就十法，便得久住於世。
云何為十？於是，國王不著財物，不興瞋恚，
亦復不以小事起怒害心，是謂第一之法，便得久存。
^cb778a8「復次，國王受群臣諫，不逆其辭，是謂成就第二之法，便得久存。
^cb778a9「復次，國王常好惠施，與民同歡，是謂第三。
^cb778a10「以法取物，不以非法，是謂第四之法，便得久存。
^cb778a12「復次，彼王不著他色，恒自守護其妻，是謂成就第五之法，便得久存。
^cb778a13「復次，國王亦不飲酒，心不荒亂，是謂成就第六之法，便得久存。
^cb778a15「復次，國王亦不戲笑，降伏外敵，是謂成就第七之法，便得久存。
^cb778a16「復次，國王案法治化，終無阿曲，是謂成就第八之法，便得久存。
^cb778a18「復次，國王與群臣和睦，無有竟爭，是謂成就第九之法，便得久存。
^cb778a19「復次，國王無有病患，氣力強盛，是謂第十之法，便得久存。
若國王成就此十法者，便得久存，無奈之何。
^cb778a22「比丘眾亦復如是，若成就十法者，如屈伸臂頃，便生天上。
云何為十？於是，比丘奉持禁戒，戒德具足，
不犯正法，是謂比丘成就此初法，身壞命終，生善處天上。
^cb778a26「復次，比丘於如來所，有恭敬之心，是謂比丘成就此第二法，
得生善處。
^cb778a28「復次，比丘順從法教，一無所犯，是謂比丘成就第三之法，得生善處。
^cb778a29「復次，比丘恭奉聖眾，無有懈惰之心，是謂成就第四之法，得生天上。
^cb778b2「復次，比丘少欲知足，不著利養，是謂比丘第五之法，得生天上。
^cb778b3「復次，比丘不自用意，恒隨戒法，是謂成就第六之法，生於善處。
^cb778b5「復次，比丘不著事務，常喜坐禪，是謂成就第七之法，得生天上。
^cb778b6「復次，比丘樂閑靜之處，不在人間，是謂成就第八之法，生於善處。
^cb778b8「復次，比丘不與惡知識從事，常與善知識從事，
是謂成就第九之法，得生善處。
^cb778b10「復次，比丘常修梵行，離於惡法，多聞學義，不失次敘。
如是比丘成就十法者，如屈伸臂頃，生善處天上。
^cb778b13「是謂，比丘！非法之行入地獄者，當念捨離；
十正法之行，當共奉修。
如是，比丘！當作是學。」
^cb778b15爾時，諸比丘聞佛所說，歡喜奉行。
^n8【八】
^cb778b17聞如是：
一時，佛在羅閱城迦蘭陀竹園所，與大比丘眾五百人俱。
^cb778b18是時，眾多比丘時到，著衣持鉢，入羅閱城乞食。
是時，眾多比丘便作是念：「我等入城乞食，
日猶故早，我等可至外道異學，與共論議。」
是時，眾多比丘便至外道異學所。
時，諸外道遙見諸沙門來，各各自謂言：
「各各寂寞，勿有高聲語言，沙門瞿曇弟子今來此間；
然沙門之法，稱譽寂寞之人，令知我等正法，不亂有亂。」
^cb778b26爾時，眾多比丘便至外道異學所，共相問訊，在一面坐。
^cb778b27爾時，外道問諸比丘：「汝等，沙門瞿曇與諸弟子說此妙法，是諸比丘盡解一切諸法而自遊戲不干？
我等亦復與諸弟子說此妙法而自遊戲。
我之所說，與汝有何等異？
有何差別？說法戒教一類無異。」
是時，眾多比丘聞外道異學所說，亦不稱善，
復非言惡，即從坐起，各退而去。
^cb778c4是時，眾多比丘自相謂言：「我等當持此義，往白世尊。
若如來有所說者，我當念奉行。」
^cb778c6爾時，眾多比丘入羅閱城乞食已，還至房中，
收攝衣鉢，往至世尊所，頭面禮足，住在一面。
爾時，眾多比丘以此緣本，盡向如來說之。
爾時，世尊告諸比丘：「彼外道異學問此義已，
汝等應持此語報之：『一論、一義、一演，乃至十論、
十義、十演，說此語時有何等義？』
設汝持此語往問者，彼人則不能報之，彼外道異學遂增愚惑。
所以然者，非彼所有境界。
^cb778c15「是故，比丘！我不見天及人民、魔、若魔天、
釋、梵天王能報此語者，除如來及如來弟子從吾聞者，此則不論。
一論、一義、一演，我雖說此義，由何故而說乎？
一切眾生由食而存，無食則死，彼比丘平等厭患，
平等解脫，平等觀察，平等分別其義，平等盡苦際，同一義而不二。
我所說者，正謂此耳。
^cb778c22「一義、一論、一演，乃至十論、十義、十演，我雖說此義，由何說乎？
名與色，彼何等謂名？
所謂痛、想、念、更、思惟，是謂名也。
彼云何名為色耶？
四大及四大所造色，是謂名為色。
以此緣本，故名為色也。
二論、二義、二演者，由此因緣故，我今說之。
若比丘平等厭患，平等解脫，平等觀察，
平等分別其義，平等盡其苦際。
^cb778c29「三論、三義、三演，由何等故而說此義乎？
所謂三痛。云何為三？
所謂苦痛、樂痛、不苦不樂痛。
彼云何名為樂痛？
所謂心中樂想，亦不分散，是謂名為樂痛。
彼云何名為苦痛？
所謂心中憒亂而不定一，思惟若干想，是謂名苦痛。
彼云何名為不苦不樂痛？
所謂心中無苦無樂想，復非一定，復非亂想，
亦不思惟法與非法，恒自寂默，心無有記，是故名為不苦不樂痛。
是謂三痛。若比丘平等厭患，平等解脫，
平等觀察，平等分別其義，平等盡其苦際。
我所說三論、三義、三演者，正謂此耳。
^cb779a11「四義、四論、四演，由何等故復說此義乎？
所謂四諦。云何為四？
所謂苦、習、盡、道聖諦。
彼云何為苦諦？所謂生苦、老苦、病苦、死苦、
憂悲惱苦、怨憎會苦、恩愛別苦、所欲不得苦。
彼云何名為習諦？
所謂愛本與欲相應者，是謂名為習諦。
彼云何名為苦盡諦？
所謂彼愛永盡無餘，更不復生，是謂名苦盡諦。
彼云何名為苦要諦？
所謂賢聖八品道：正見、正治、正語、正命、
正業、正方便、正念、正三昧，是名為八品之道也。
若比丘平等厭患，平等解脫，平等分別其義，
平等觀察，平等盡其苦際，是謂四論、四義、四演。
我所說者，正謂此耳。
^cb779a23「五論、五義、五演，我今所說，由何等故說？
所謂五根。云何為五？
信根、精進根、念根、定根、慧根。
云何名為信根？所謂賢聖弟子，信如來道法，
彼如來、至真、等正覺、明行成為、善逝、世間解、
無上士、道法御、天人師，號佛、眾祐，出現於世，是謂名為信根。
彼云何名為精進根？
所謂身心意并勤勞不倦，滅不善法，使善增益，
順心執持，是謂名為精進根。
彼云何名為念根？
所謂念根者，所誦不忘，恒在心懷，總持不失，
有為、無漏之法，終不忘失，是謂名為念根。
彼云何名為定根。
所謂定根者，心中無錯亂，無若干想，恒專精一意，是謂名為三昧根。
彼云何名智慧根？
所謂知苦、知習、知盡、知道，是謂名智慧之根。
此名五根也。比丘於中平等解脫，平等分別其義，平等盡其苦際。
五論、五義、五演，我所說者，正謂此耳。
^cb779b10「六論、六義、六演，我所說者，由何等故乎？
所謂六重之法也。
云何為六？於是，比丘恒身行慈心，若在閑淨室中，
常若一心，可尊可貴，恒與和合，是謂比丘第一重法。
復次，口行慈心，終無虛妄，可敬可貴，是謂第二重法。
復次，意行慈，不起憎嫉，可敬可貴，是謂第三重法。
復次。若得法利之養，鉢中遺餘，與諸梵行之人等心施與，是謂第四重法可敬可貴。
復次，奉持禁戒，無所脫失，賢人之所貴，是謂第五重法可敬可貴。
復次，正見賢聖得出要，得盡苦際，意不錯亂，
與諸梵行之人等修其行，是謂第六之法可敬可貴。
爾時，比丘平等厭患，平等解脫，平等分別其義，平等盡於苦際。
六論、六義、六演，我所說者，正謂此耳。
^cb779b25「七論、七義、七演，由何等故而說此乎？
所謂七神識止處。
云何為七？或有眾生，若干想，若干種身，所謂天及人也。
或有眾生，若干種身一想，所謂梵迦夷天最初出時。
或有眾生，一想一身，所謂光音天是也。
或有眾生，一身若干想，所謂遍淨天是也。
或有眾生，空處無量，所謂空處天是也。
或有眾生，識處無量，所謂識處天是也。
或有眾生，無所有處無量，所謂不用處天是也。
或有眾生，有想無想處無量，所謂有想無想天是也。
是謂，比丘！七神止處。
於是，比丘平等解脫，乃至平等盡於苦際。
七論、七義、七演，我所說者，正謂此耳。
^cb779c8「八論、八義、八演，我所說者，由何等故而說此乎？
所謂世間八法是隨世迴轉。
云何為八？利、衰、毀、譽、稱、譏、苦、樂，是謂世間八法隨世迴轉。
若比丘於中平等解脫，乃至盡於苦際。
八論、八義、八演，我所說者，正謂此耳。
^cb779c13「九論、九義、九演，我所說者，由何故而說此乎？
所謂九眾生居處。
云何為九？若有眾生。
若干種身，所謂天及人。
或有眾生，若干種身一想，謂梵迦夷天最初出時是也。
或有眾生，一想一身，所謂光音天是也。
或有眾生，一身若干想，所謂遍淨天也。
或有眾生，空處無量，所謂空處天是也。
或有眾生，識處無量，所謂識天也。
或有眾生，無有處無量，所謂不用處天是也。
或有眾生，有想無想處無量，所謂有想無想天是也。
無想眾生及諸所生之類，為九神止處。
於是，比丘平等解脫，乃至盡於苦際。
九論、九義、九演，我所說者，正謂此耳。
^cb779c25「十論、十義、十演，由何等說乎？
所謂十念：念佛、念法、念比丘僧、念戒、念施、
念天、念休息、念安般、念身、念死，是謂十念。
若比丘平等解脫，乃至盡於苦際。
十論、十義、十演。如是，比丘！
從一至十。
^cb780a1「比丘當知，若外道異學聞此語者，猶不能熟視顏色，況欲報之！
其有比丘解此義者，於現法中最尊第一之人。
若復比丘、比丘尼思惟此義，乃至十歲，
必成二果、若阿羅漢、若阿那含。
比丘！且捨十歲。若一年之中思惟此義者，
必成二果，終無中退。
比丘！且捨一年，其四部之眾十月，若至一月，
思惟此義者，必成二果，亦不中退。
且捨一月。若四部之眾七日之中思惟此義，
必成二果，終不有疑。」
^cb780a10爾時，阿難在世尊後，執扇扇佛。
爾時，阿難白佛言：「世尊！
此法極為甚深。若所在方面有此法者，當知便遇如來。
唯然，世尊！此法名何等，當云何奉行？」
^cb780a13佛告阿難：「此經名為十法之義，當念奉行。」
^cb780a14爾時，阿難及諸比丘聞佛所說，歡喜奉行。
^n9【九】
^cb780a16聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb780a17爾時，世尊告諸比丘：「其有修行十想者，
便盡有漏，獲通作證，漸至涅槃。
云何為十？所謂白骨想、青瘀想、𮌨脹想、
食不消想、血想、噉想、有常無常想、貪食想、
死想、一切世間不可樂想。
是謂，比丘！修此十想者，得盡有漏，得至涅槃界。
又是，比丘！十想之中，一切世間不可樂想最為第一。
所以然者，其有修行不可樂想，持信奉法，此二人必越次取證。
是故，比丘！若在樹下靜處露坐，當思惟此十想。
是故，比丘！當作是學。」
^cb780a26爾時，諸比丘聞佛所說，歡喜奉行。
^n10【一〇】
^cb780a28聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb780a29爾時，有一比丘至世尊所，頭面禮足，在一面坐。
爾時，彼比丘白世尊言：「如來今日與諸比丘說：
『十想之法，其能修者，斷諸有漏，成無漏行。』
如我，世尊！不堪任行此想。
所以然者，欲心多故，身意熾盛，不得寧息。」
^cb780b5爾時，世尊告彼比丘：「汝今當捨淨想，思惟不淨想；
捨有常想，思惟無常想；捨有我想，思惟無我想；
捨可樂想，思惟不可樂想。
所以然者，若比丘思惟淨想，欲心便熾盛；
若思惟不淨想，便無欲心。
比丘當知，欲為不淨，如彼屎聚；欲如鸜鵒，
饒諸音響；欲無返復，如彼毒蛇；欲如幻化，
如日消雪；當念捨欲，如棄塜間。
欲還自害，如蛇懷毒；欲無厭患，如飲鹹水；
欲難可滿，如海吞流；欲多可畏，如羅剎村；
欲猶怨家，恒當遠離。
欲猶少味，如蜜塗刀；欲不可愛，如路白骨；
欲現外形，如廁生華；欲為不真，如彼畫瓶，
內盛醜物，外見殊特；欲無牢固，亦如聚沫。
是故，比丘！當念遠離貪欲之想，思惟不淨之想。
