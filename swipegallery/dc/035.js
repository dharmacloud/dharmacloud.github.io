爾時，世尊告均頭曰：「汝今抱患極為篤重，
不須下床，吾自有坐。」
^cb731a16爾時，世尊告均頭曰：「汝所患為增為損，不增損乎？
有能堪任受吾教也？」
^cb731a18是時，均頭比丘白佛言：「弟子今日所患極篤，但有增無損也。
所服藥草，靡不周遍。」
