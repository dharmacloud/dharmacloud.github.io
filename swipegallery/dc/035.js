可誦知誦，可授前人便授前人，可語知語。
若有比丘不知此者，不知止觀進止之宜，此非比丘。
若復比丘知其時節，不失時宜，此名為隨其方宜。
如是，比丘知其時宜。
^cb728c18「云何比丘自能修己？
於是，比丘能自知己：『我今有此見聞念知，
有如是智慧，行步進止，恒隨正法。』
若有比丘不能自知智慧之宜，出入行來，此非比丘也。
以其比丘能自修己進止之宜，此名為自修己行，是謂比丘能自知己。
^cb728c23「云何比丘自知止足？
於是，比丘能自籌量睡眠、覺寤、坐臥、經行、
進止之宜，皆能知止足。
若有比丘不能知是者，則非比丘也。
以其比丘能解了此故，名為知足。
如是，比丘名為知足。
^cb728c27「云何比丘知入大眾？
於是，比丘分別大眾，此是剎利種，此是婆羅門眾，
此是長者眾，此是沙門眾，我當以此法宜則適彼眾中，
可語可默，皆悉知之。
若有比丘不知入眾，此非比丘。
以其比丘知入大眾故，名為知入眾也。
是謂比丘知入大眾也。
^cb729a4「云何比丘知眾人根元？
比丘當知，有二人。
云何為二？彼或有一人，欲往至園中親覲比丘；彼第二人不喜至彼觀見比丘。
彼人欲至園中親覲比丘者，此人最為上。
^cb729a8「比丘！復有二人。云何為二？
彼一人雖至比丘所，然不問其宜；彼第二人亦不往至寺中見比丘。
彼至寺人最為第一。
^cb729a11「比丘！復有二人。云何為二？
彼一人至比丘所問訊時宜；彼第二人不至比丘所問訊時宜。
彼人至寺者，最尊第一，出彼人上。
^cb729a14「比丘！復有二人。云何為二？
彼一人至比丘所，至心聽法；彼第二人不至比丘所，不至心聽法。
彼至心聽法者，於彼人最為第一。
^cb729a17「比丘！復有二人。云何為二？
彼有一人能觀察法，受持諷誦；彼第二人，不能受持諷誦。
彼人受持諷誦者，於此人上最為第一。
^cb729a20「比丘！復有二人。云何為二？
彼有一人，聞法解其義；彼第二人，聞法不解其義。
彼人聞法解義者，於此人最尊第一。
^cb729a23「比丘！復有二人。云何為二？
彼有一人聞法，法成就；彼第二人不聞法，法不成就。
彼人法法成就者，於此人第一。
^cb729a26「比丘！復有二人。云何為二？
彼一人聞法能堪忍修行，分別護持正法；第二人不能堪忍修行其法。
彼能修行法者，於此諸人最尊第一。
猶如牛有酪，由酪有酥，由酥有醍醐，最為第一，無能及者。
此亦如是，若有人能修行者，此人最為第一，
無能及者，是謂比丘觀察人根。
若有人不了此者，則非比丘也。
以其比丘聞法分別其義者，此為最上。
如是比丘觀察人根。
若有比丘成就七法者，於現法中快樂無為，意欲斷漏亦無有疑。
是故，比丘！當求方便，成此七法。
如是，比丘！當作是學。」
^cb729b9爾時，諸比丘聞佛所說，歡喜奉行。
^n2【二】
^cb729b11聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb729b12爾時，世尊告諸比丘：「三十三天晝度樹，
本縱廣五十由旬，高百由旬，東、西、南、北蔭覆五十由旬，三十三天在彼四月自相娛樂。
比丘當知，或有是時，彼晝度樹華葉凋落，萎黃在地。
爾時，諸天見此瑞應，普懷歡喜，欣情內發：『此樹不久當更生華實。』
^cb729b17「比丘當知，或有是時，彼樹華實皆悉凋落，捐棄在地。
是時，三十三天倍復歡喜，自相謂言：『此樹不久當作灰色。』
^cb729b20「比丘當知，復經歷數時，彼樹便作灰色。
是時，三十三天已見此樹而作灰色，甚懷喜悅，
自相謂言：『而今此樹已作灰色，不久當生羅網。』
是時，三十三天見此晝度樹已生羅網，不久當生雹節。
爾時，三十三天見已，復懷歡喜：『此樹今日已生雹節，不久當復開敷。』
^cb729b26「比丘當知，三十三天已見此樹漸漸開敷，
各懷歡喜：『此樹不久漸漸開敷，不久當盡著華。』
^cb729b28「比丘當知，或有是時，彼樹普悉開敷，皆懷歡喜：『此樹今日皆悉著華。』
爾時，此香逆風，百由旬內無不聞香者。
爾時，諸天四月之中於彼自相娛樂，樂不可計。
^cb729c3「此亦如是，若賢聖弟子意欲出家學道時，如似彼樹始欲凋落。
^cb729c4「復次，賢聖弟子捐棄妻財，以信堅固，出家學道，
剃除鬚髮，如似彼樹葉落在地。
^cb729c6「比丘當知，若賢聖弟子無貪欲想，除不善法，
念持歡喜，遊志一禪，似彼晝度樹而作灰色。
^cb729c8「復次，賢聖弟子有覺、有觀息，內有歡喜，
專其一心，無覺、無觀、遊心二禪，如似彼樹而生羅網。
^cb729c10「復次，賢聖弟子念而有護，自覺身有樂，
諸賢聖所救，護念具足，遊在三禪，如似彼樹而生雹節。
^cb729c13「復次，賢聖弟子苦樂已盡，先無愁憂，無苦無樂，
護念清淨，遊志四禪，如似彼樹漸漸開敷。
^cb729c15「復次，賢聖弟子盡有漏，成無漏心解脫、
智慧解脫，現法中而自娛樂：生死已盡，
梵行已立，所作已辦，更不復受胎，如實知之。
如似彼樹皆悉敷花。
^cb729c18「是時，賢聖弟子戒德之香，遍聞四遠，無不稱譽者，
四月之中而自娛樂，遊心四禪，具足行本。
是故，諸比丘！當求方便，成戒德之香。
如是，比丘！當作是學。」
