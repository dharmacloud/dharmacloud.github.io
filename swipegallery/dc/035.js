若有比丘成就九法者，於現法中不得長大。
云何為九？與惡知識從事親近，非事恒喜遊行，
恒抱長患，好畜財貨，貪著衣鉢，多虛乾妄亂意非定，
無有慧明，不解義趣，不隨時受誨。
是謂，目連！若比丘成就此九者，於現法中不得長大有所潤及。
^cb771b23「設有比丘能成就九者便有所成辦。
云何為九？與善知識從事，修行正法不著邪業，
恒遊獨處不樂人間，少病無患，亦復不多畜諸財寶，
不貪著衣鉢，勤行精進無有亂心，聞義便解更不中受，隨時聽法無有厭足。
是謂，目連！若有比丘成就此九法者，於現法中多所饒益。
是故，目連，當念勤加往誨諸比丘，使長夜之中致無為之處。」
^cb771c2爾時，世尊便說此偈：
^gatha「常念自覺悟，勿著於非法，
^gatha所修應正行，得度生死難。
^gatha作是而獲是，作此獲此福，
^gatha眾生流浪久，斷於老病死。
^gatha以辦更不習，復更造非行，
^gatha如此放逸人，成於有漏行。
^gatha設有勤加心，恒在心首者，
^gatha展轉相教誡，便成無漏人。
^cb771c12「是故，目乾連！當與諸比丘而作是誨，當念作是學。」
^cb771c13是時，世尊與諸比丘說極妙之法，令發歡喜之心。
是時，諸比丘聞法已，於彼眾中六十餘比丘漏盡意解。
^cb771c15爾時，諸比丘聞佛所說，歡喜奉行。
^n3【三】
^cb771c17聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb771c18爾時，世尊告諸比丘：「若有比丘依村落住，
善法消滅，惡法遂增。
彼比丘當作是學：『我今在村落居止，惡法遂增，
善法漸減，念不專一，不得盡有漏，不至無為安隱之處。
我所得衣被、飲食、床臥具、病瘦醫藥，勞苦乃獲。』
彼比丘當作是學：『吾今住此村落之中，
惡法遂增，善法消滅，我亦不以衣被、飲食、
床臥具、醫藥，故來作沙門！
吾所求願者，今不獲果。』
又彼比丘當遠離村落去。
^cb771c26「若復有比丘依村落住，善法增益，惡法消滅，
所得衣裳、飲食、床臥具，勤勞乃獲，彼比丘當作是學：
『我今依此村落住，善法增益，惡法消滅，
所得供養之具，勤勞乃得。
有我不以衣被故出家學道，修於梵行。
我所學道求願者必成其法，應當盡形壽承事供養。』」
^cb772a4爾時，世尊便說偈曰：
^gatha「衣被及飲食，床臥及所安，
^gatha不應貪著想，亦莫來此世。
^gatha不以衣被故，出家而學道，
^gatha所以學道者，必果其所願。
^gatha比丘尋應時，盡形住彼村，
^gatha於彼般涅槃，盡其命根本。
^cb772a11「是時，彼比丘若在人間靜處所遊之村，
善法增益，惡法自滅，彼比丘盡形壽住彼村中，不應遠遊。」
^cb772a13是時，阿難白世尊言：「如來常不說四大依食得存，亦依於心所念法？
諸善之法依心而生？
又彼比丘依村落住，勞苦精神乃求衣食。
彼云何生善法，住彼村落而不遠遊？」
^cb772a17佛告阿難：「衣被、飯食、床臥具、病瘦醫藥有三種。
若復比丘專念四事供養，所欲不果，此依是苦。
若復興知足之心，不起想著，諸天、人民代其歡喜。
又比丘！當作是學。
我由此故而說此義。
是故，阿難！比丘當念少欲知足。」
^cb772a22如是，阿難聞佛所說，歡喜奉行。
^n4【四】
^cb772a24聞如是：
一時，佛在婆羅園中。
爾時，世尊時到，著衣持鉢，入婆羅村乞食。
是時，弊魔波旬便作是念：「今此沙門欲入村乞食，我今當以方宜教諸男女不令與食。」
是時，弊魔波旬尋告國界人民之類：「無令施彼沙門瞿曇之食。」
^cb772a29爾時，世尊入村乞食，人民之類皆不與如來共言談者，
亦無有來承事供養者，如來乞食竟不得，便還出村。
^cb772b2是時，弊魔波旬至如來所問佛言：「沙門！
乞食竟不得乎？」
^cb772b4世尊告曰：「由魔所為，使吾不得食，汝亦不久當受其報。
魔！今聽吾說，賢劫之中有佛名拘樓孫如來、
至真、等正覺、明行成為、善逝、世間解、無上士、
道法御、天人師，號佛、眾祐，出現於世。
是時，彼亦依此村居止，將四十萬眾。
爾時，弊魔波旬便作是念：『吾今求此沙門方便，終不果獲。』
時，魔復作是念：『吾今當約勅婆羅村中人民之類，使不施沙門之食。』
是時，諸聖眾著衣持鉢，入村乞食。
爾時諸比丘竟不得食，即還出村。
^cb772b13「爾時，彼佛告諸比丘，說如此妙法：『夫觀食有九事：
四種人間食，五種出人間食。
云何四種是人間食？
一者揣食，二者更樂食，三者念食，四者識食，是謂世間有四種之食。
彼云何名為五種之食，出世間之表？
一者禪食，二者願食，三者念食，四者八解脫食，
五者喜食，是謂名為五種之食。
如是，比丘！九種之食，出世間之表，當共專念，
捨除四種之食，求於方便辦五種之食。
如是，比丘！當作是學。』
爾時，諸比丘受彼佛教已，即自剋己，成辦五種之食。
是時，彼魔波旬不能得其便。
^cb772b24「是時，波旬便作是念：『吾今不能得此沙門方便，
今當求眼、耳、鼻、口、身、意之便。
吾今當住村中，教諸人民，使沙門眾等求得利養，
使令得之，以辦利養倍增多也；使彼比丘貪著利養，
不能暫捨，復欲從眼、耳、鼻、口、身、意得方便乎。』
^cb772c1「是時，彼佛、聲聞到時，著衣持鉢，入村乞食。
是時，婆羅門村人民供給比丘衣被、飯食、
床臥具、病瘦醫藥，不令有乏，皆前捉僧伽梨，以物強施。
是時，彼佛與眾聲聞說如此之法：『夫利養者，
墮人惡趣，不令至無為之處。
汝等，比丘！莫趣想著之心，向於利養，當念捨離；
其有比丘著利養者，不成五分法身，不具戒德。』
^cb772c8「是故，比丘！未生利養之心，當使不生；已生利養之心，時速滅之。
如是。比丘！當作是學。」
時，魔波旬即隱形去。
^cb772c11爾時，諸比丘聞佛所說，歡喜奉行。
^n5【五】
^cb772c13聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb772c14爾時，世尊告諸比丘：「當行慈心，廣布慈心；
以行慈心，所有瞋恚之心，自當消除。
所以然者，比丘當知；昔日有鬼極為弊暴，來在釋提桓因座上而坐。
是時，三十三天極為瞋恚：『云何此鬼在我主床上坐乎？』
是時，諸天適興恚心，彼鬼遂轉端正，顏貌殊常。
爾時，釋提桓因在普集講堂上坐，與玉女共相娛樂。
是時，有天子往至釋提桓因所，白帝釋言：
『瞿翼當知，今有惡鬼在尊座上坐，今三十三天極懷恚怒，
諸天適興恚怒，彼鬼遂轉端正，顏貌勝常。』
是時，釋提桓因便作是念：『此鬼必是神妙之鬼。』
^cb772c25「是時，釋提桓因往至彼鬼所，相去不遠，
自稱姓名：『吾是釋提桓因，諸天之主。』
時，釋提桓因自稱姓名時，彼惡鬼轉成醜形，
顏貌可惡，是彼惡鬼即時消滅。
比丘！當以此方便，知其行慈心而不捨離，其德如是。
^cb773a1「又且，比丘！吾昔日時，七歲之中恒修慈心，
經歷七成劫、敗劫，不往來生死，劫欲壞時，
便生光音天，劫欲成時，便生無想天上，
或作梵天，統領諸天，領十千世界，又復三十七變為釋提桓因，
又無數變為轉輪聖王。
比丘！以此方便，知其行慈心，其德如是。
^cb773a7「復次，行慈心者，身壞命終，生梵天上，離三惡道，去離八難。
復次，其行慈者，生中正之國。
復次，行慈者，顏貌端正，諸根不缺，形體完具。
復次，其行慈心者，躬自見如來，承事諸佛，
不樂在家，欲得出家學道者，著三法衣，
剃除鬚髮，修沙門之法，修無上梵行。
^cb773a13「比丘當知，猶如金剛，人取食之，終不消化，要當下過。
其行慈心之人，亦復如是，若如來出世，
要當作道，修無上梵行；生死已盡，梵行已立，
所作已辦，更不復受有，如實知之。」
^cb773a17是時，尊者阿難白佛言：「世尊！
設如來不出世時，彼善男子不樂在家，當何所趣向？」
^cb773a19佛告阿難曰：「若如來不出時，然善男子不樂在家，
自剃鬚髮，在閑靜之處，剋己自修，即於彼處，
盡諸有漏，成無漏行。」
^cb773a22是時，阿難白佛言：「云何，世尊！
彼人自修梵行、三乘之行，彼人何所趣向？」
^cb773a24佛告阿難：「如汝所言，吾恒說三乘之行。
過去、將來三世諸佛，盡當說三乘之法。
阿難當知，或有是時，眾生之類顏貌壽命，
轉轉減少，形器瘦弱，無復威神，多諸瞋怒、
嫉妬、恚癡、姦偽、幻惑，所行不真。
或復有利根捷疾，展轉諍競，共相鬪訟；
或以手拳、瓦石、刀杖，共相傷害。
是時，眾生之類執草便成刀劍，斷斯命根。
其中眾生，行慈心者無有瞋怒，見此變怪，
皆懷恐懼，悉共馳走，離此惡處，在山野之中，
自然剃除鬚髮，著三法衣，修無上梵行，
剋己自修，盡有漏心而得解脫，便入無漏境，
各各自相謂言：『我等已勝怨家。』
阿難當知，彼名為最勝。」
^cb773b8是時，阿難復白佛言：「彼人為在何部？
聲聞部，辟支部，為佛部耶？」
^cb773b9佛告阿難：「彼人當名正在辟支部。
所以然者，此人皆由造諸功德，行眾善本，
修清淨四諦，分別諸法。
夫行善法者，即慈心是也。
所以然者，履仁行慈，此德廣大。
吾昔著此慈仁之鎧，降伏魔官屬，坐樹王下，
成無上道，以此方便，知慈最第一，慈者最勝之法也。
阿難當知，故名為最勝。
行慈心者，其德如是，不可稱計。
當求方便，修行慈心。
如是，阿難！當作是學。」
^cb773b18爾時，阿難聞佛所說，歡喜奉行。
^n6【六】
^cb773b20聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb773b21爾時，尊者舍利弗清旦從靜室起至世尊所，
頭面禮足，在一面坐。
爾時，佛告舍利弗曰：「汝今諸根清淨，顏貌與人有異，汝今遊何三昧？」
^cb773b24舍利弗白佛言：「唯然，世尊！
我恒遊空三昧。」
^cb773b25佛告舍利弗言：「善哉！
善哉！舍利弗！乃能遊於空三昧。
所以然者，諸虛空三昧者最為第一，其有比丘遊虛空三昧，
計無吾我、人、壽命，亦不見有眾生；亦復不見諸行本末，
已不見，亦不造行本；已無行，更不受有；
已無受有，不復受苦樂之報。
^cb773c2「舍利弗當知，我昔未成佛道，坐樹王下，
便作是念：『此眾生類為不剋獲何法，流轉生死不得解脫？』
時，我復作是念：『無有空三昧者，便流浪生死，不得至竟解脫。
有此空三昧，但眾生未剋，使眾生起想著之念，
以起世間之想，便受生死之分。
若得是空三昧，亦無所願，便得無願三昧；
以得無願三昧，不求死此生彼，都無想念時，彼行者復有無想三昧可得娛樂。
此眾生類皆由不得三昧故，流浪生死。』
觀察諸法已，便得空三昧，已得空三昧，便成阿耨多羅三藐三菩提。
當我爾時，以得空三昧，七日七夜觀視道樹，目未曾眴。
舍利弗，以此方便，知空三昧者，於諸三昧最為第一三昧，
王三昧者，空三昧是也。
是故，舍利弗！當求方便，辦空三昧。
如是，舍利弗！當作是學。」
^cb773c18爾時，舍利弗聞佛所說，歡喜奉行。
^n7【七】
^cb773c20聞如是：
一時，佛在羅閱城迦蘭陀竹園所，與大比丘眾千二百五十人俱。
^cb773c21爾時，羅閱城中有長者名曰尸利掘，饒財多寶，
金銀、珍寶、車𤦲、馬腦，不可稱計；又且踈薄佛法，但事外道尼乾子。
國王、大臣皆悉識知。
是時，外道梵志及尼乾子，在家、出家者自誹謗，
言有我，言有我身，并六師輩皆悉運集，
共作此論：「今沙門瞿曇靡事不知，有一切智，
然我等不得利養，今此沙門多得利養，
要當作方宜，使不得利養。
我等當往至尸利掘舍，教彼長者而作權宜。」
^cb774a1是時，外道梵志尼乾子及彼六師往至尸利掘長者家，
語長者曰：「大姓當知，汝是梵天所生，是梵天子，多所饒益。
汝今可往至沙門瞿曇所，愍我等故，請沙門及比丘眾來在家祠之。
又勅屋中作大火坑，極燃熾火，食皆著毒，請使來食。
若沙門瞿曇有一切智，知三世事者，則不受請；
設無一切智，便當受請，將諸弟子，盡為火所燒，
天、人得安，無有火害。」
^cb774a10是時，尸利掘默然，隨六師語，即出城至世尊所，
頭面禮足，持雜毒之心，白如來言：「唯願世尊及比丘僧當受我請。」
^cb774a12爾時，世尊知彼心中所念，默然受請。
是時，尸利掘以見如來默然受請，便從坐起，
頭面禮足，便退而去。
中道便作是念：「今我六師所說審諦，然沙門不知我心中所念，
必當為大火所燒。」
是時，尸利掘即還家勅作大坑，燃大燒火，
復約勅辦種種飯食，皆悉著毒，復於門外作大火坑，
燃大火，又於火上施設敷床，皆以惡毒著食中，
而白：「時至。」
^cb774a20爾時，世尊以知時至，著衣持鉢，將諸比丘眾，
前後圍繞，往至彼家，又勅諸比丘僧：「諸人皆不得先吾前行；
亦不得先吾前坐；亦復不得先吾前食。」
^cb774a24是時，羅閱城中人民之類，聞尸利掘作大火坑，
又作毒食，請佛及比丘僧。
四部之眾悉皆涕泣：「將非害如來及比丘僧乎？」
或復有至世尊所，頭面禮足，白佛言：「願世尊莫至彼長者家，
又彼人作大火坑，兼作毒食。」
^cb774a29佛告之曰：「諸人勿懷恐怖，如來終不為他所害。
正使閻浮里內火至梵天，猶不能燒吾，
何況此小火欲害如來，終無此理。
優婆塞知，吾無復害心。」
^cb774b3爾時世尊，比丘僧前後圍遶，入羅閱城，至長者家。
爾時，世尊告諸比丘：「汝等勿先入長者家，
亦莫先食；要須如來食，然後乃食。」
^cb774b6爾時，世尊適舉足門閾上，爾時火坑自然化作浴池，
極為清涼；眾華滿其中，亦生蓮花，大如車輪，
七寶為莖，亦生餘蓮華，蜜蜂王遊戲其中。
爾時，釋提桓因、梵天王及四天王，及乾沓和、
阿須輪，及諸閱叉、鬼神等，見火坑中生此蓮華，各各稱慶！
異音同聲，各各說曰：「便為如來勝中第一。」
^cb774b13爾時，彼長者家有種種外道異學，集在其家。
爾時，優婆塞、優婆夷見如來變化已，歡喜踊躍，不能自勝。
外道異學見如來變化已，甚懷愁憂。
上虛空中諸尊神天，散種種名華於如來身上。
^cb774b17爾時，世尊履虛去地四寸，至長者家。
如來舉足之處，便生蓮華，大如車輪。
爾時，世尊右迴告諸比丘：「汝等，悉皆蹈此蓮花上。」
時，諸聲聞皆從蓮華上至長者家。
^cb774b21爾時，世尊便說古昔之喻說：「我過去來，
供養恒沙諸佛，承事、禮敬，未失聖意，持是至誠之誓，
使此諸坐，皆悉牢固。」
^cb774b24爾時，世尊告諸比丘：「我今聽諸比丘，先以手𠗦座，
然後乃坐，此是我之教也。」
爾時，世尊及諸比丘僧皆悉就座，是座下皆生蓮華，極為芬香。
^cb774b27是時，尸利掘見如來如斯變化，便生斯念：
「吾為外道異學所誤，失我人中之行，永失天路；
心意憒然，如飲雜毒，必當趣此三惡道中。
實是如來出世難遇。」
覺知此已，即時涕零，頭面禮足，白佛言：
「唯願如來聽我悔過，改往修來。
自知有罪，觸嬈如來。
唯願世尊受我悔過，更不犯之。」
^cb774c5佛告言：「長者！改過捐捨本意，乃能自知觸犯如來。
賢聖法中甚為曠大，聽汝改過，隨法而捨；
我今受汝改悔，後更莫犯。」
如是再三。
^cb774c8爾時，阿闍世王聞尸利掘長者施大火坑，
及雜毒食，欲害如來。
聞已，瞋恚熾盛，告群臣曰：「要當消滅閻浮里地與此人同尸利掘名字者。」
又復阿闍世憶如來功德已，悲泣涕零，
脫天冠已，告群臣曰：「吾今復用活為？
乃使如來為火所燒，及比丘僧皆當被燒。
汝等速來至長者家，觀視如來。」
