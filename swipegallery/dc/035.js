小劫，此人為應何劫？」
^cb804b28佛告阿難：「斯人當經歷大劫。
所謂大劫者，即賢劫，是盡劫數，行盡命終，還復人身。」
^cb804b29阿難白佛：「提婆達兜盡喪人根，遂復成就。
所以然者，劫數長遠，夫大劫者不過賢劫。」
