但長者食已辦者，隨時供設。」
^cb775a3爾時，長者手自斟酌，行種種飯食。
^cb775a4爾時，世尊便說斯偈：
^gatha「至誠佛法眾，害毒無遺餘，
^gatha諸佛無有毒，至誠佛害毒。
^gatha至誠佛法眾，害毒無遺餘，
^gatha諸佛無有毒，至誠法害毒。
^gatha至誠佛法眾，害毒無遺餘，
^gatha諸佛無有毒，至誠僧害毒。
^gatha貪欲瞋恚毒，世間有三毒，
^gatha如來永無毒，至誠佛害毒。
^gatha欲怒瞋恚毒，此三世間毒，
^gatha如來法無毒，至誠法害毒。
^gatha欲怒瞋恚毒，世間有三毒，
^gatha如來僧無毒，至誠僧害毒。」
