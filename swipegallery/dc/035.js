爾時，阿闍世王見如來口出光明，亦復遍見如來顏色殊特，
極懷歡喜，不能自勝。
^cb774c28爾時，尸利掘長者白世尊言：「我所設食皆悉有毒，
唯願世尊小停，今當更施食。
所以然者，無令如來體有增損。」
