尋時至尸利掘長者家，下象即至尸利掘舍內。
爾時，眾人普集門外，有八萬四千人。
爾時，阿闍世王見蓮華大如車輪，歡喜踊躍，
不能自勝，並作是說：「使如來恒勝眾魔。」
告耆婆伽王子曰：「善哉！
耆婆伽！乃信如來如斯之要。」
