如是比丘正向無漏。
^cb776a7「云何比丘依倚身行？
於是，比丘無明已除。
如是比丘依倚身行。
^cb776a8「云何比丘心善得解脫？
於是，比丘愛已除盡。
如是比丘心善得解脫。
^cb776a10「云何比丘智慧解脫？
於是，比丘觀苦諦，習、盡、道諦，如實知之。
如是比丘智慧解脫。
^cb776a12「是謂，比丘！聖賢十事所居之處。
