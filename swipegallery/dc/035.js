所以然者，皆由不隨時將護之所致，後復斷壞不成其子。
此亦如是，若比丘、比丘尼、五心結不斷、
五心弊不除，晝夜之中於善法減，無有增益。
^cb817b23「若復比丘、比丘尼，五心結斷、五心弊除，
晝夜之中善法增益，無有損減。
猶如雞子若八、若十二，隨時將護，隨時育養，
隨時蔭覆，彼雞雖生斯念：『使我雞子全不成就。』
