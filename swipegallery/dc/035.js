所以然者，隨時長養，令得無為，時諸雞子尋得出外。
此亦如是，若比丘、比丘尼，五心弊斷、五心結除，
彼比丘、比丘尼於長夜之中善法增益，無有損減。
^cb817c2「是故，比丘、若比丘尼、當施設心無有猶豫狐疑於佛、猶豫狐疑於眾。
