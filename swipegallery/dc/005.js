「世饒王佛說經竟，法寶藏菩薩便壹其心，則得天眼徹視，悉自見二百一十億諸佛國中諸天人民之善惡國土之好醜，則選心所欲願，便結得是二十四願經，則奉行之，精進勇猛，勤苦求索。如是無央數劫所師事供養諸佛已，過去佛亦無央數，其法寶藏菩薩至其然後自致得作佛，名無量清淨覺最尊，智慧勇猛，光明無比。今現在所居國甚快善，在他方異佛國教授八方上下無央數諸天、人民及蜎飛蠕動之類，莫不得過度解脫憂苦者。
「無量清淨佛為菩薩時，常奉行是二十四願，珍寶愛重，保持恭順，精進禪行之。與眾超絕，卓然有異，皆無有能及者。」
佛言：「何等為二十四願者？
「一、我作佛時，令我國中無有地獄、禽獸、餓鬼、蜎飛蠕動之類，得是願乃作佛；不得從是願終不作佛。
「二、我作佛時，令我國中人民有來生我國者，從我國去，不復更地獄、餓鬼、禽獸蠕動；有生其中者，我不作佛。
「三、我作佛時，人民有來生我國者，不一色類金色者，我不作佛。
「四、我作佛時，人民有來生我國者，天人、世間人有異者，我不作佛。
「五、我作佛時，人民有來生我國者，皆自推所從來生本末、所從來十億劫宿命；不悉知念所從來生，我不作佛。
「六、我作佛時，人民有來生我國者，不悉徹視，我不作佛。
「七、我作佛時，人民有來生我國者，不悉知他人心中所念者，我不作佛。
「八、我作佛時，我國中人民不悉飛者，我不作佛。
「九、我作佛時，我國中人民不悉徹聽者，我不作佛。
「十、我作佛時，我國中人民有愛欲者，我不作佛。
「十一、我作佛時，我國中人民住止盡般泥洹；不爾者，我不作佛。
「十二、我作佛時，我國諸弟子，令八方上下各千億佛國中諸天、人民、蠕動之類作緣一覺大弟子，皆禪一心，共數我國中諸弟子，住至百億劫無能數者；不爾者，我不作佛。
「十三、我作佛時，令我光明勝於日月、諸佛之明百億萬倍，炤無數天下窈冥之處皆常大明，諸天、人民、蠕動之類見我光明，莫不慈心作善來生我國；不爾者，我不作佛。
「十四、我作佛時，令八方上下無數佛國諸天、人民、蠕動之類令得緣一覺果證弟子，坐禪一心，欲共計知我年壽幾千萬億劫，令無能知壽涯底者；不爾者，我不作佛。
「十五、我作佛時，人民有來生我國者，除我國中人民所願，餘人民壽命無有能計者；不爾者，我不作佛。
「十六、我作佛時，國中人民皆使莫有惡心；不爾者，我不作佛。
「十七、我作佛時，令我名聞八方上下無數佛國，諸佛各於弟子眾中歎我功德國土之善，諸天、人民、蠕動之類聞我名字，皆悉踊躍來生我國；不爾者，我不作佛。
「十八、我作佛時，諸佛國人民有作菩薩道者，常念我淨潔心，壽終時，我與不可計比丘眾飛行迎之，共在前立，即還生我國作阿惟越致；不爾者，我不作佛。
「十九、我作佛時，他方佛國人民前世為惡，聞我名字及正為道欲來生我國，壽終皆令不復更三惡道，則生我國在心所願；不爾者，我不作佛。
「二十、我作佛時，我國諸菩薩不一生等，置是餘願功德；不爾者，我不作佛。
「二十一、我作佛時，我國諸菩薩不悉三十二相者，我不作佛。
「二十二、我作佛時，我國諸菩薩欲共供養八方上下無數諸佛，皆令飛行，欲得萬種自然之物，則皆在前，持用供養諸佛悉遍已，後日未中則還我國；不爾者，我不作佛。
「二十三、我作佛時，我國諸菩薩欲飯時，則七寶鉢中生自然百味飲食在前，食已，鉢皆自然去；不爾者，我不作佛。
「二十四、我作佛時，我國諸菩薩說經行道不如佛者，我不作佛。」
佛告阿難：「無量清淨佛為菩薩時，常奉行是二十四願，分檀布施，不犯道禁，忍辱精進，一心智慧。志願常勇猛，不毀經法，求索不懈。每獨棄國捐王，絕去財色，精明求願，無所適莫。積功累德無央數劫，自致作佛悉皆得之，不忘其功也。」
佛言：「無量清淨佛光明最尊第一無比，諸佛光明皆所不及也。八方上下無央數諸佛中，有佛項中光明照七丈、中有佛項中光照一里、中有佛項中光明照五里、中有佛項中光明照二十里、中有佛項中光明照四十里、中有佛項中光明照八十里、中有佛項中光明照百六十里、中有佛項中光明照三百二十里、中有佛項中光明照六百四十里、中有佛項中光明照千三百里、中有佛項中光明照二千六百里、中有佛項中光明照五千二百里、中有佛項中光明照萬四百里、中有佛項中光明照二萬一千里、中有佛項中光明照四萬二千里、中有佛項中光明照八萬四千里、中有佛項中光明照十七萬里、中有佛項中光明照三十五萬里、中有佛項中光明照七十萬里、中有佛項中光明照百五十萬里、中有佛項中光明照三百萬里、中有佛項中光明照六百萬里、中有佛項中光明照千二百萬里。
「中有佛項中光明照一佛國、中有佛項中光明照兩佛國、中有佛項中光明照四佛國、中有佛項中光明照八佛國、中有佛項中光明照十五佛國、中有佛項中光明照三十佛國、中有佛項中光明照六十佛國、中有佛項中光明照百二十佛國、中有佛項中光明照五百佛國、中有佛項中光明照千佛國、中有佛項中光明照二千佛國、中有佛項中光明照四千佛國、中有佛項中光明照八千佛國、中有佛項中光明照萬六千佛國、中有佛項中光明照三萬二千佛國、中有佛項中光明照六萬四千佛國、中有佛項中光明照十三萬佛國、中有佛項中光明照二十六萬佛國、中有佛項中光明照五十萬佛國、中有佛項中光明照百萬佛國、中有佛項中光明照二百萬佛國。」
佛言：「八方上下無央數諸佛，其項中光明所照皆如是也；無量清淨佛項中光明焰照千萬佛國。所以諸佛光明所照有遠近者何？本前世宿命求道為菩薩時，所願功德各自有大小，至其然後作佛時，悉各自得之，是故令光明轉不同等。諸佛威神同等耳，自在意所欲作為不豫計。無量清淨佛光明所照最大，諸佛光明皆所不能及也。」
佛稱譽無量清淨佛光明：「無量清淨佛光明極善，善中明好甚快無比，絕殊無極也。無量清淨佛光明殊好，勝於日月之明百億萬倍也。無量清淨佛光明，諸佛光明中之極明也；無量清淨佛光明，諸佛光明中之極好也；無量清淨佛光明，諸佛光明中之極雄傑也；無量清淨佛光明，諸佛光明中之快善也；無量清淨佛光明，諸佛光明中之王也；無量清淨佛光明，諸佛光明中之最極尊也；無量清淨佛光明，諸佛光明中之壽明無極。
「無量清淨佛光明焰照諸無央數天下幽冥之處皆常明，諸有人民、蜎飛蠕動之類，莫不見無量清淨佛光明，見無量清淨佛光明莫不慈心歡喜者。世間諸有婬妷、瞋怒、愚癡，見無量清淨佛光明莫不作善者。諸泥犁、禽獸、薜荔考掠勤苦之處，見無量清淨佛光明至，皆休止不得復治，死後莫不得解脫憂苦者也。
「無量清淨佛光明，名聞八方上下無窮無極無央數佛國，諸天人民莫不聞知，聞知者莫不得過度者。」
佛言：「我不獨稱譽無量清淨佛光明也，八方上下無央數諸佛、辟支佛、菩薩、阿羅漢，所稱譽皆如是。」
佛言：「其有人民——善男子、善女人——聞無量清淨佛聲，稱譽光明。如是朝暮常稱譽其光明明好，至心不斷絕，在心所欲願往生無量清淨佛國，可得為諸菩薩、阿羅漢所尊敬，智慧勇猛。若其然後作佛者，亦當復為八方上下無央數辟支佛、菩薩、阿羅漢所稱譽光明，亦當復如是。則眾比丘僧、諸菩薩、阿羅漢、諸天、帝王、人民聞之，皆歡喜踊躍，莫不讚歎者。」
佛言：「我道無量清淨佛光明姝好，巍巍稱譽快善，晝夜一劫尚未竟也，我但為若曹小說之耳。」
佛說無量清淨為菩薩，求索得是二十四願。時阿闍世王太子與五百大長者迦羅越子，各持一金華蓋，前上佛已，悉却坐一面聽經。
阿闍世王太子及五百長者子，聞無量清淨佛二十四願，皆大歡喜踊躍，心中俱願言：「令我等後作佛時，皆如無量清淨佛。」
佛則知之，告諸比丘僧：「是阿闍世王太子及五百長者子，却後無央數劫皆當作佛，如無量清淨佛。」
佛言：「是阿闍世王太子、五百長者子，作菩薩道已來無央數劫，皆各供養四百億佛已，今復來供養我。是阿闍世王太子及五百人等，皆前世迦葉佛時為我作弟子，今皆復會是共相值也。」則諸比丘僧聞佛言，皆心踊躍，莫不歡喜者。
佛告阿難：「無量清淨佛作佛已來凡十八劫，所居國名須摩提，正在西方，去是閻浮利地界千億萬須彌山佛國。
「其國地皆自然七寶：其一寶者名白銀，二寶者名黃金，三寶者水精，四寶者琉璃，五寶者珊瑚，六寶者虎珀，七寶者車𤦲。是七寶皆以自共為地，曠蕩甚大無極；皆自相參轉相入中，各自焜煌參光極明，自然軟甚姝好無比。
「如其七寶地，諸八方上下眾寶中精，都自然之，合會共化生耳，其寶比如第六天上之七寶也。
「其國中無有須彌山，其日、月、星辰、第一四天王、第二忉利天，皆在虛空中。
「其國土無有大海水、亦無小海水，無江河恒水也，亦無山林溪谷，無有幽冥之處。
「其國七寶地皆平正，無有泥犁、禽獸、餓鬼、蜎飛蠕動之類也，無阿須倫、諸龍、鬼神也。
「終無有大雨時，亦無春、夏、秋、冬也；亦無有大寒亦不大熱，常和調中，適甚快善無比。
「皆有萬種自然之物、百味飲食，意欲有所得則自然在前，意不用者則自然化去。比如第六天上自然之物，恣若自然則皆隨意。
「其國中悉諸菩薩、阿羅漢，無有婦女，壽命極壽，壽亦無央數劫。女人往生者則化生，皆作男子。
「但有菩薩、阿羅漢無央數，悉皆洞視徹聽——悉遙相見、遙相瞻望、遙相聞語聲——悉皆求道善者，同一種類無有異人也。其諸菩薩、阿羅漢，面目皆端正，清潔絕好，悉同一色，無有偏醜惡者。諸菩薩、阿羅漢皆才猛黠慧。其所衣服，皆衣自然之衣。都心中所念常念道德，其所欲語言便皆豫相知意，其所念言道，常說正事。
「其國中諸菩薩、阿羅漢自共相與語言輒說經道，終不說他餘之惡。
「其語言音響如三百鍾聲，皆相敬愛，無有相憎者。皆自以長幼、上下先後言之，都共往會，以義而禮，轉相敬事，如兄如弟。以仁履義，不妄動作；言語而誠，轉相教令；不相違戾，轉相承受。皆心潔淨，無所貪慕，終無有婬妷、瞋怒之心、愚癡之態也。無有邪心念、婦女意也。悉智慧勇猛，和心歡樂，好喜經道。皆自知其前世所從來生、億萬劫世時宿命善惡存亡，現在却知無極。
「無量清淨佛所可教授講堂、精舍，皆復自然七寶——金、銀、水精、琉璃、白玉、虎珀、車𤦲——自共轉相成也，甚姝明好，絕姝無比。亦無有作者，亦不知所從來；亦無有持來者，亦無所從去。
「無量清淨佛所願德重，其人作善故，論經語義、說經行道，講會其中，自然化生耳。其講堂、精舍皆復有七寶樓觀欄楯，復以金、銀、水精、琉璃、白玉、虎珀、車𤦲為瓔珞，復以白珠、明月珠、摩尼珠為交絡，覆蓋其上；皆自作五音聲，音聲甚姝無比。
「無量清淨佛國諸菩薩、阿羅漢所居舍宅，皆復以七寶——金、銀、水精、琉璃、珊瑚、虎珀、車𤦲、馬瑙——化生轉共相成也。其舍宅皆悉各有七寶樓觀欄楯，復以金、銀、水精、琉璃、白玉、虎珀、車𤦲為瓔珞，復以白珠、明月珠、摩尼珠為交絡，覆蓋其上，皆各復自作五音聲。
「無量清淨佛講堂、精舍，及諸菩薩、阿羅漢所居七寶舍宅中，外內處處皆復自然流泉水浴池。其浴池者，皆復以自然七寶——七寶俱生，金、銀、水精、琉璃、珊瑚、虎珀、車𤦲——轉共相成也。水底沙皆復以七寶——金、銀、水精、琉璃、珊瑚、虎珀、車𤦲也。有純白銀池者，其底沙皆黃金也；中有純黃金池者，其水底沙皆白銀也；中有純水精池者，其水底沙皆琉璃也；中有純琉璃池者，其水底沙皆水精也；中有純珊瑚池者，其水底沙皆虎珀也；中有純虎珀池者，其水底沙皆珊瑚也；中有純車𤦲池者，其水底沙皆馬瑙也；中有純馬瑙池者，其水底沙者皆車𤦲也；中有純白玉池者，其水底沙者皆紫磨金也；中有純紫磨金池者，其水底沙者皆白玉也。
「中復有二寶共作一池者，其水底沙者皆金、銀也；中復有三寶共作一池者，其水底沙者皆金、銀、水精也；中復有四寶共作一池者，其水底沙金、銀、水精、琉璃也；中復有五寶共作一池者，其水底沙皆金、銀、水精、琉璃、珊瑚、虎珀也；中復有六寶共作一池者，其水底沙皆金、銀、水精、琉璃、珊瑚、虎珀、車𤦲也；中復有七寶共作一池者，其水底沙皆金、銀、水精、琉璃、珊瑚、虎珀、車𤦲、馬瑙也。
「其浴池中有長四十里者、中有池長八十里者、中有池長百六十里者、中有池長三百二十里者、中有池長六百四十里者、中有池長千二百八十里者、中有浴池長二千五百六十里者、中有浴池長五千一百二十里者、中有浴池長萬二百四十里者、中有浴池長二萬四百八十里者，其縱廣各適等。是浴池者，皆諸菩薩、阿羅漢常所可浴池。」
佛言：「無量清淨佛浴池長四萬八千里，廣亦四萬八千里。其浴池皆七寶轉自共相成，其池水底沙皆復以七寶、白珠、明月珠、摩尼珠也。
「無量清淨佛及諸菩薩、阿羅漢浴池中水皆清淨香潔，中皆有香華——悉自然生百種華，種種異色、異香——華皆千葉。諸華甚香無比，香不可言也。其華香者亦復非世間之華，復勝天上之華；是華香者，八方上下眾華香中精自然生耳。池中水流行，轉相灌注；池中水流亦不遲亦不駃，皆復自作五音聲。」
佛言：「八方上下無央數佛國諸天、人民、及蜎飛蠕動之類，諸生無量清淨佛國者，都皆於是七寶水池蓮華中化生，便則自然長大，亦無乳養之者，皆食自然之飲食。其身體者，亦非世間人之身體也，亦非天上人之身體也，皆積眾善之德，悉受自然虛無之身體，甚姝好無比。」
佛語阿難：「如世間貧窮乞丐人，令在帝王邊住者，其人面目形貌何等類乎？寧類帝王面目形貌顏色不？」
阿難言：「假令使子在帝王邊住者，其面目形狀甚醜惡不好，不如帝王面目形類姝好百千億萬倍也。所以者何？見乞人貧窮困極，飲食未曾有美食時也。既惡食不能得飽食，食栽支命，骨節相撐拄。無所用自給，常乏無有儲，飢餓寒凍，怔忪愁苦。但坐其前世宿命為人時，愚癡無智富益慳，有財不肯慈哀、仁賢為善、博愛施與，但欲唐得，貪惜飲食，獨食嗜美，不信施貸後得償報也。復不信作善後世得其福，蒙籠頑佷益作眾惡。如是，壽終財物盡索，素無恩德無所恃怙，入惡道中坐之適苦，然後得出解脫。今生為人，作於下賤貧家作子，強像人形，狀貌甚醜。衣被弊壞，單空獨立，不蔽形體，乞匃生活耳。飢寒困苦，面目羸劣，不類人色。坐其前世身之所作，受其殃罰，示眾見之莫誰哀者；棄捐市道，暴露痟瘦，黑醜惡極不及人耳。
「所以帝王人中獨尊最好者何？皆其前世宿命為人時作善，信愛經道，布施恩德，博愛順義，慈仁喜與。不貪飲食，與眾共之，無所遺惜，都無違諍。得其福德，壽終德隨，不更惡道。今生為人，得生王家，自然尊貴，獨王典主，攬制人民，為人雄傑。面目潔白，和顏好色，身體端正，眾共敬事。美食、好衣隨心恣意，在樂所欲自然在前，都無違諍。於人中姝好，無憂快樂，面色光澤，故乃爾耳。」◎
