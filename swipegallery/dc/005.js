佛告阿難：「夜摩、兜率乃至色、無色界一切諸天，皆依空界而住。」
阿難白言：「空界無礙，云何依住？業因果報不可思議。」
佛告阿難：「汝身果報亦不可思議，眾生業報亦不可思議，諸佛聖力不可思議。彼佛國土雖無大海，而有泉河處處交流；其水或闊十由旬、二十由旬、三十由旬，乃至百千由旬；深十二由旬。其水清淨具八功德；出微妙聲，譬如百千萬種音樂之聲，遍諸佛剎，一切眾生聞者適悅，得大快樂。又水兩岸，復有無數栴檀香樹，吉祥菓樹，花卉恒芳，光明照耀。若彼眾生過此水時，要至足者、要至膝者，乃至要至項者；或要冷者、溫者、急流者、慢流者；其水一一隨眾生意令受快樂。又於水中出種種聲，佛聲、法聲、僧聲、止息聲、無性聲、波羅蜜聲、力聲、無畏聲、通達聲、無行聲、無生聲、無滅聲、寂靜聲、大慈聲、大悲聲、喜捨聲、灌頂聲，出如是種種微妙音聲。眾生聞已，發清淨心，無諸分別，正直平等，成熟善根，永不退於阿耨多羅三藐三菩提心。又彼佛剎其中生者，不聞地獄聲、餓鬼聲、畜生聲、夜叉聲、鬪諍聲、惡口聲、兩舌聲、殺生聲、偷盜聲、一切惡聲。而彼眾生，色相端嚴，福德無量，智慧明了，神通自在。宮殿、樓閣、園林、池沼、衣服、臥具，如他化自在天，最上快樂之具，一切豐足。
「復次，阿難！彼土眾生，思香花等欲供諸佛，作是念時，花香、瓔珞、塗香、末香、幢幡、傘蓋，及諸伎樂，隨意即至滿佛剎中。若思飲食、湯藥、衣服、臥具、頭冠、耳環、真珠、羅網等，隨念即至亦遍佛剎。又復思念摩尼寶等，莊嚴宮殿、樓閣、堂宇、房閤，或大、或小、或高、或下；如是念時，隨意現前無不具足。
「復次，阿難！譬如有人少有財寶，對受灌頂位剎帝利王，所有威勢悉皆不現。又剎帝利對天帝釋前，所有威勢悉皆不現。又天帝釋對他化自在天，所有威勢悉皆不現。又他化自在天等，及色、無色界一切威勢，對無量壽如來極樂國土，悉皆不現。如是彼土功德莊嚴不可思議。
「復次，阿難！彼佛國土每於食時，香風自起吹動寶樹，樹相掁觸出微妙音，演說苦、空、無常、無我、諸波羅蜜。復吹樹花落於地上，周遍佛剎高七人量，平正莊嚴，柔軟光潔。行人往來足躡其地深四指量，如迦隣那觸身安樂。過食時後，是諸寶花隱地不現。經須臾間復有風生，吹樹落花布地面上，如前無異。初夜、後夜亦復如是。
「復次，阿難！彼佛國土無其黑闇，無其星曜，無其日、月，無其晝、夜，無其取、捨，無其分別，純一無雜；唯受清淨最上快樂。若有善男子、善女人，若已生、若當生，是人決定證於阿耨多羅三藐三菩提。於意云何？彼佛剎中無三種失：一、心無虛妄，二、位無退轉，三、善無唐捐。
「復次，阿難！東方有恒河沙數世界，諸佛如來出廣長舌相，放無量光說誠實言，稱讚無量壽佛不可思議功德。南方亦有恒河沙數世界，諸佛如來出廣長舌相，放無量光說誠實言，稱讚無量壽佛不可思議功德。西方亦有恒河沙數世界，諸佛如來出廣長舌相，放無量光說誠實言，稱讚無量壽佛不可思議功德。北方亦有恒河沙數世界，諸佛如來出廣長舌相，放無量光說誠實言，稱讚無量壽佛不可思議功德。如是四維、上、下恒河沙數世界，諸佛如來出廣長舌相，放無量光說誠實言，稱讚無量壽佛不可思議功德。阿難！於意云何？欲令眾生聞彼佛名，發清淨心憶念受持，歸依供養求生彼土。是人命終，皆得往生極樂世界，不退轉於阿耨多羅三藐三菩提。
「復次，阿難！若有善男子、善女人，聞此經典，受持、讀誦、書寫、供養，晝、夜相續，求生彼剎。是人臨終，無量壽如來與諸聖眾現在其前，經須臾間，即得往生極樂世界，不退轉於阿耨多羅三藐三菩提。
「復次，阿難！若有善男子、善女人，發菩提心已，持諸禁戒堅守不犯，饒益有情，所作善根悉施與之，令得安樂，憶念西方無量壽如來及彼國土。是人命終，如佛色相種種莊嚴，生寶剎中賢聖圍繞，速得聞法，永不退轉於阿耨多羅三藐三菩提。
「復次，阿難！若有善男子、善女人，發十種心，所謂：一、不偷盜，二、不殺生，三、不婬欲，四、不妄言，五、不綺語，六、不惡口，七、不兩舌，八、不貪，九、不瞋，十、不癡。如是晝、夜思惟極樂世界無量壽佛，種種功德種種莊嚴，志心歸依頂禮供養。是人臨終，不驚、不怖、心不顛倒，即得往生彼佛國土。有無量無數諸佛世尊，稱讚無量壽佛功德名號，聞是法已，永不退於阿耨多羅三藐三菩提。
