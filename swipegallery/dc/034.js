欲猶怨家，恒當遠離。
欲猶少味，如蜜塗刀；欲不可愛，如路白骨；
欲現外形，如廁生華；欲為不真，如彼畫瓶，
內盛醜物，外見殊特；欲無牢固，亦如聚沫。
是故，比丘！當念遠離貪欲之想，思惟不淨之想。
汝今，比丘！當憶昔迦葉佛所奉行十想，
今當重思惟十想，有漏心便解脫。」
