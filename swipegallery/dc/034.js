如是比丘為知起煙。
^cb794c23「云何比丘知良田茂草處？
於是，比丘賢聖八品道如實知之。
是謂比丘知良田茂草處。
^cb794c25「云何比丘知所愛？
於是，比丘若聞如來所說法寶，心便愛樂。
如是比丘為知所愛。
^cb794c27「云何比丘擇道行？
