補治故寺，來至我所；或於釋迦文佛所，
受八關齋法，來至我所；或於釋迦文佛所，
香花供養，來至此者；或復於彼聞佛法，
悲泣墮淚，來至我所；或復於釋迦文佛，
專意聽法，來至我所；復盡形壽善修梵行，
