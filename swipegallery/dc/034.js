是故，比丘！若自觀察時，思惟微妙之法，
又當察二義，無放逸行，使成果實，至甘露滅盡之處。
若當受他供養衣被、飲食、床臥具、病瘦醫藥，
不唐其勞，亦使父母得其果報，承事諸佛，禮敬供養。
如是，比丘！當如是學。」
^cb776b12爾時，諸比丘聞佛所說，歡喜奉行。
^n4【四】
^cb776b14聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb776b15爾時，世尊告諸比丘：「如來成十種力，得四無所畏，在大眾中能師子吼。
云何為十力？於是，如來是處如實知之，非處如實知之。
^cb776b18「復次，如來處所，知他眾生因緣處所受其果報。
^cb776b19「復次，如來知若干種界、若干種持、若干種入，如實知之。
^cb776b20「復次，如來若干種解脫、無量解脫，如實知之。
^cb776b21「復次，如來知他眾生智慧多少，如實知之。
^cb776b22「復次，如來知他眾生心中所念，如實知之：
有欲心知有欲心，無欲心知無欲心；有瞋恚心知有瞋恚心，
無瞋恚心知無瞋恚心；有愚癡心知有愚癡心，
無愚癡心知無愚癡心；有愛心知有愛心，
無愛心知無愛心；有受心知有受心，無受心知無受心；
亂心知有亂心，無亂心知無亂心；散心知有散心，
無散心知無散心；少心知有少心，無少心知無少心；
廣心知有廣心，無廣心知無廣心；無量心知無量心，
有量心知有量心，如實知之。
定心知有定心，無定心知無定心；解脫心知解脫心，無解脫心知無解脫心。
^cb776c6「復次，如來盡知一切所趣心之道，或一二生、
三生、四生、五生、十生、五十生、百生、千生、
億百千生、無量生、成劫、敗劫、無數成敗劫中，
我昔生彼處，名是，字是，食如此之食，受其苦樂，
壽命長短，死此生彼，彼死生此。
自憶如是無數宿命之事。
^cb776c11「復次，如來知眾生生死之趣，以天眼觀眾生之類，
善色、惡色、善趣、惡趣，隨行所種，皆悉知之。
或復眾生身、口、意行惡，誹謗賢聖，造邪見業，
身壞命終，生地獄中；或復眾生身、口、意行善，
不誹謗賢聖，恒行正見，身壞命終，生善處天上，
是謂名為天眼清淨，觀眾生類所趣之行。
^cb776c18「復次，如來有漏盡，成無漏心解脫、智慧解脫；
生死已盡，梵行已立，所作已辦，更不復受有，如實知之。
是謂如來有此十力，名為無著，得四無所畏，
在大眾中作師子吼，轉於梵輪。
^cb776c22「云何如來得四無所畏？
欲言如來成等正覺，若有眾生，欲言知者，
則無此處；若復有沙門、婆羅門欲來誹謗佛，
不成等正覺者，則無此處；以無此處，則獲安隱。
^cb776c26「然我今日欲言已盡有漏，設復有沙門、
婆羅門、天、若魔天來，欲言未盡有漏者，
則無此處；以無此處，則獲安隱。
^cb776c28「復次，我所說法，賢聖得出要者，如實盡於苦際。
設有沙門、婆羅門、天、若魔天來，欲言未盡苦際者，
無此處；以無此處，則獲安隱。
^cb777a2「復次，我所說內法者、墮惡趣者，設復有沙門、
婆羅門來，欲言非者，則無此處。
是謂，比丘！如來有四無所畏。
^cb777a5「設有外道異學言：『彼沙門瞿曇！
有何等之力，有何無畏，自稱無著最尊？』
汝等當持此十力往報之。
設復外道異學重作是說：『我等亦成就十力。』
汝等比丘復當問曰：『汝有何十力？』
是時，外道異學則不能報也，遂增其惑。
所以然者，我終不見沙門、婆羅門自稱言得四無所畏，除如來者。
是故，比丘！當求方便，成十力、四無所畏。
如是，比丘！當作是學。」
^cb777a13爾時，諸比丘聞佛所說，歡喜奉行！
^n6【六】
^cb777a15聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb777a16爾時，世尊告諸比丘：「親近國家有十非法。
云何為十？於是國家起謀害心欲殺國王，
緣此陰謀，王致命終。
彼人民類便作是念：『此沙門、道士數來往返，此必是沙門所為。』
