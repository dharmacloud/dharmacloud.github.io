吾當與說之；若使國王、大臣、人民之類問如來義者，
亦當與說之；若剎利四姓來問義者，亦當與說之。
所以然者，今日如來得四無所畏，說法無有怯弱；
亦得四禪，於中自在，兼得四神足，不可稱計，
行四等心，是故如來說法無有怯弱，非羅漢、
辟支佛所能及也；是故如來說法亦無有難。
汝今，諸比丘！當求方便，行四等心，慈、悲、
喜、護。如是，諸比丘！
當作是學。
^cb667c24「所以然者，若比丘所為眾生善知識遇，
及一切父母知親，盡當以四事教令知法。
云何為四？一者當恭敬於佛，是時如來者，
至真、等正覺、明行成為、善逝、世間解、無上士、
道法御、天人師，號佛、眾祐，度人無量。
當求於法，修行正真之法，除穢惡之行，此是智者之所修行。
復當方便供養眾僧，如來眾者，恒共和合，
無有諍訟，法成就、戒成就、三昧成就、智慧成就、
解脫成就、解脫知見成就。
所謂四雙八輩、十二賢士，此是如來聖眾，
可尊、可貴，世間無上福田。
復當勸助使行賢聖法、律，無染無污，寂靜無為。
若有比丘欲行道者，普共行此四事之法。
所以然者，法之供養三尊，最尊、最上，無能及者。
如是，諸比丘！當作是學。」
^cb668a10爾時，諸比丘聞佛所說，歡喜奉行。
^n3【三】
^cb668a12聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb668a13爾時，世尊告諸比丘：「有四事行跡。
云何為四？有樂行跡所行愚惑，此名初行跡；
復有樂行跡所行速疾；復有苦行跡所行愚惑；復有苦行跡所行速疾。
^cb668a16「彼云何名為樂行跡所行愚惑？
或有一人貪欲熾盛，瞋恚、愚癡熾盛，所行甚苦，
不與行本相應，彼人五根愚闇亦不捷疾。
云何為五？所謂信根、精進根、念根、慧根、定根。
若以愚意求三昧盡有漏者，是謂名為樂行跡鈍根得道者也。
^cb668a22「彼云何名為樂根行跡速疾？
或有一人無欲、無婬；然於貪欲，恒自偏少不慇懃，
為瞋恚、愚癡極為減少，五根捷疾無有放逸。
云何為五？所謂信根、精進根、念根、定根、
慧根，是謂五根。然得五根成於三昧，盡有漏成無漏，是謂名為利根行於道跡也。
^cb668a27「彼云何名為苦行跡行於愚惑？
或有一人婬意偏多，瞋恚、愚癡熾盛。
彼以此法而自娛樂，盡有漏成無漏，是謂名為苦行跡鈍根者也。
^cb668b1「云何苦行跡行於速疾？
於是，或有一人少欲少婬，無有瞋恚，亦不起想，行此三法。
爾時，有此五根，無有缺漏。
云何為五？所謂信根、精進根、念根、定根、
慧根，是謂為五。彼以此法得三昧，盡有漏成無漏，是謂苦行跡利根者也。
^cb668b7「是謂比丘有此四行跡，當求方便，捨前三行跡，後一行者當共奉行。
所以然者，苦行跡三昧者難得，以得便成道，久存於世。
所以然者，不可以樂求樂，由苦然後成道。
是故，諸比丘！恒以方便，成此行跡。
如是，諸比丘！當作是學。」
^cb668b12爾時，諸比丘聞佛所說，歡喜奉行。
^n4【四】
^cb668b14聞如是：
一時，佛在羅閱城迦蘭陀竹園所，與大比丘眾五百人俱。
^cb668b15爾時，四梵志皆得五通，修行善法，普集一處，
作是論議：「此伺命來時不避豪強，各共隱藏，使伺命不知來處。」
^cb668b18爾時，一梵志飛在空中，欲得免死，然不免其死，即在空中而命終。
第二梵志復入大海水底，欲得免死，即於彼命終。
彼第三梵志欲得免死，入須彌山腹中，復於中死。
彼第四梵志入地至金剛際，欲得免死，復即彼而命終。
^cb668b23爾時，世尊以天眼觀見四梵志，各各避死，普共命終。
爾時，世尊便說此偈：
^gatha「非空非海中，非入山石間，
^gatha無有地方所，脫止不受死。」
^cb668b28爾時，世尊告諸比丘：「於是，比丘！
有梵志四人集在一處，欲得免死，各歸所奔，故不免死。
一人在空，一人入海水，一人入山腹中，
一人入地，皆共同死。
是故，諸比丘！欲得免死者，當思惟四法本。
云何為四？一切行無常，是謂初法本，當念修行。
一切行苦，是謂第二法本，當共思惟。
一切法無我，此第三法本，當共思惟。
滅盡為涅槃，是謂第四法本，當共思惟。
如是，諸比丘！當共思惟此四法本。
所以然者，便脫生、老、病、死、愁、憂、苦、惱，此是苦之元本。
是故，諸比丘！當求方便，成此四法。
如是，諸比丘！當作是學。」
^cb668c10爾時，諸比丘聞佛所說，歡喜奉行。
^n5【五】
^cb668c12聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb668c13爾時，世尊告諸比丘：「三十三天有四園觀，
諸天於中而自娛樂，五樂自娛。
云何為四？難檀槃那園觀、麁澁園觀、晝夜園觀、雜種園觀。
然四園之內有四浴池；極冷浴池、香味浴池、
輕便浴池、清徹浴池。
云何為四？一者難陀浴池，二名難陀頂浴池，
三名蘇摩浴池，四名歡悅浴池。
^cb668c19「比丘當知，四園之內有此四浴池，令人身體香潔無有塵垢。
何以故名為難檀槃那園？
若三十三天入難檀槃那園已，心性喜悅，
不能自勝，於中而自娛樂，故名為難檀槃那園。
^cb668c23「復以何故名為麁澁園觀？
若三十三天入此園中已，身體極麁，猶如冬時以香塗身，
身體極麁，此亦如是。
若三十三天入此園中已，身體極麁不與常同，
以是之故，名為麁澁園觀。
^cb668c27「復以何故名為晝夜之園？
若使三十三天入此園中已，爾時諸天顏色各異，
作若干種形體，猶如婦女著種種衣裳，不與本形同。
此亦如是，若三十三天入此園中已，作若干種色不與本同，
以是故名為晝夜之園。
^cb669a4「復以何故名為雜種之園？
爾時，最尊之天及中天、下天，入此園已，
皆同一類，設復最下之天大不得入餘三園中，
猶如轉輪聖王所入之園，餘王不復得入園中浴洗，人民之類正可得遙見耳。
此亦如是，若最尊神天所入園中浴洗，
餘小天不復得入，是故名為雜種浴池。
^cb669a10「復以何故名為難陀浴池？
若三十三天入此池中已，極懷歡悅，是故名為難陀浴池。
^cb669a12「復以何故名為難陀頂浴池？
若三十三天入此池中已，兩兩捉手摩其頂而浴洗，
正使天女亦復如是，以是之故，名為難陀頂浴池。
^cb669a15「復以何故名為蘇摩浴池？
三十三天入此池中已，爾時諸天顏貌，
盡同人色，無有若干，是故名為蘇摩浴池。
^cb669a18「復以何故名為歡悅浴池？
若三十三天入此池中已，盡無憍慢上下之想，
望意偏少，爾時盡同一心而浴洗，故名為歡悅浴池。
是謂，比丘！有此因緣，便有此之名。
^cb669a21「今如來正法之中亦復如是，有四園之名。
云何為四？一者慈園，二者悲園，三者喜園，四者護園。
是謂，比丘！如來正法之中有此四園。
^cb669a24「復以何故名為慈園？
比丘當知，由此慈園生梵天上，從梵天終，
當生豪尊之家，饒財多寶，恒有五樂自娛，
未曾離目，以是之故，名為慈園。
^cb669a28「復以何故名為悲園？
比丘當知，若能親近悲解脫心，生梵光音天。
若來生人中，生豪族家，無有瞋恚，亦饒財多寶，故名為悲園。
^cb669b2「復以何故名為喜園？
若能親近喜園者，生光音天。
若來生人間，國王家生，恒懷歡喜，故名為喜園。
^cb669b4「復以何故名為護園？
若有人親近護者，生無想天，壽八萬四千劫。
若復來生人中，當生中國家，亦無瞋恚，
恒護一切非法之行，以是故名為護園。
^cb669b8「比丘當知，如來正法之中有此四園，使諸聲聞得遊戲其中，
然如來此四園之中有四浴池，使我聲聞於中洗浴而自遊戲，
盡有漏成無漏，無復塵垢。
云何為四？一名有覺有觀浴池，二名無覺無觀浴池，
三名護念浴池，四名無苦無樂浴池。
^cb669b13「以何等故名為有覺有觀浴池？
若有比丘得初禪已，於諸法中恒有覺、
觀，思惟諸法，除去結纏，永無有餘，以是之故，名為有覺有觀。
^cb669b16「復以何故名為無覺無觀浴池？
若有比丘得二禪已，滅有覺、有觀，以禪為食，以是故名之為無覺無觀。
^cb669b19「復以何故名為護念浴池？
若比丘得三禪已，滅有覺、有觀，無覺、無觀，
恒護念三禪，以是之故，名為護念浴池。
^cb669b22「復以何故名為不苦不樂浴池？
若有比丘得四禪已，亦不念樂，復非念苦，
亦不念過去當來之法，但用心於現在法中，
以是之故，名為不苦不樂浴池。
^cb669b25「是故，諸比丘！如來正法之中有此四浴池，
使我聲聞於中洗浴，滅二十一結，度生死海，入涅槃城。
是諸比丘！若欲度此生死海者，當求方便，
滅二十一結，入涅槃城。
如是，諸比丘！當作是學。」
^cb669c1爾時，諸比丘聞佛所說，歡喜奉行。
^n6【六】
^cb669c2聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb669c3爾時，世尊告諸比丘：「猶如四大毒蛇極為凶暴，舉著一函中。
若有人從四方來，欲令活、不求死，欲求樂、
不求苦，不愚不闇，心意不亂，無所繫屬。
^cb669c6「是時，若王、若王大臣喚此人而告之曰：
『今有四大毒蛇極為兇暴，汝今當隨時將養沐浴令淨，
隨時飲食無令使乏，今正是時，可往施行。』
是時，彼人心懷恐懼，不敢直前，便捨，馳走莫知所湊深。
復重告彼人作是語：『今使五人皆持刀劍而隨汝後，
其有獲汝者，當斷其命，不足稽遲。』
^cb669c13「是時，彼人畏四大毒蛇，復畏五人捉持刀劍者，
馳走東西，不知如何？
復告彼人曰：『今復使六怨家使隨汝後，
其有得者當斷其命；欲所為者可時辦之。』
是時，彼人畏四大毒蛇，復畏五人持刀杖者，
復畏六怨家，便馳走東西，彼人若見空墟之中，欲入中藏。
若值空舍，若破牆間無堅牢處，若見空器，
盡無所有，若復有人與此人親友，欲令免濟，
便告之曰：『此間空閑之處多諸賊寇，欲所為者今可隨意。』
^cb669c23「是時，彼人復畏四大毒蛇，復畏五人持刀杖者，
復畏六怨家，復畏空墟村中，便馳走東西。
彼人前行，若見大水極深且廣，亦無人民及橋梁可度得至彼岸，
然復彼人所立之處多諸惡賊。
是時，彼人作是思惟：『此水極為深廣，饒諸賊寇，當云何得度彼岸？
我今可集聚材木草蘘作栰，依此栰從此岸得至彼岸。』
是時，彼人便集薪草作栰已，即得至彼岸，志不移動。
^cb670a2「諸比丘當知，我今作喻，當念解之。
說此義時，為有何義？
言四毒蛇者，即四大是也。
云何為四大？所謂地種、水種、火種、風種，是謂四大。
五人持刀劍者，此是五盛陰也。
云何為五？所謂色陰、痛陰、想陰、行陰、識陰是也。
六怨家者，欲愛是也。
空村者，內六入是也。
云何為六？所謂六入者，眼入、耳入、鼻入、
口入、身入、意入。
^cb670a9「若有智慧者而觀眼時，盡空無所有，亦不牢固；
若復觀耳、鼻、口、身、意時，盡空無所有，皆虛、
皆寂，亦不牢固。云水者，四流是也。
云何為四？所謂欲流、有流、無明流、見流。
大栰者，賢聖八品道是也。
云何為八？正見、正治、正語、正方便、正業、
正命、正念、正定，是謂賢聖八品道也。
水中求度者，善權方便精進之力也。
此岸者，身邪也；彼岸者，滅身邪也。
此岸者，阿闍世國界也：彼岸者，毘沙王國界也。
此岸者，波旬國界也；彼岸者，如來之境界也。」
^cb670a19是時，諸比丘聞佛所說，歡喜奉行。
^n7【七】
^cb670a21聞如是：
一時，佛在舍衛國祇樹給孤獨園。
爾時世尊與大比丘眾五百人俱。
^cb670a22爾時，舍衛城中有一優婆塞，而命終還生舍衛城中大長者家，
最大夫人妊身。爾時，世尊以天眼觀，清淨無瑕穢，見此優婆塞生舍衛城中最富長者家。
即於其日，復有梵志身壞命終，生地獄中，爾時世尊亦以天眼觀。
復即以其日，阿那邠邸長者命終，生善處天上，是時世尊亦以天眼觀。
即其日，有一比丘而取滅度，世尊亦以天眼觀見。
^cb670b1爾時，世尊見此四事已，便說斯偈：
^gatha「若人受胞胎，惡行入地獄，
^gatha善者生天上，無漏入涅槃。
^gatha賢者今受胎，梵志入地獄，
^gatha須達生天上，比丘取滅度。」
^cb670b7是時，世尊從靜室起，詣普集講堂而就坐。
爾時，世尊告諸比丘：「今有四事。
若人能修行者，身壞命終，得生人中。
云何為四？所謂身、口、意、命清淨無瑕穢者。
若命終時得生人中。
^cb670b11「若復，比丘！更有四法，有人習行者，入地獄中。
云何為四？所謂身、口、意、命不清淨。
是謂，比丘！有此四法。
若有人親近者，身壞命終，生地獄中。
^cb670b14「復次，比丘！復有四法，習修行者，生善處天上。
云何為四？惠施、仁愛、利人、等利。
是謂，比丘！有人行此法者，身壞命終，生善處天上。
^cb670b17「復次，比丘！更有四法，若有人修行者，身壞命終，
盡有漏，成無漏心解脫、智慧解脫：生死已盡，
梵行已立，所作已辦，更不復受胎，如實知之。
云何為四？有覺有觀禪、無覺無觀禪、護念禪、
苦樂滅禪，是謂比丘有四事法。
若有人習行者，盡有漏，成無漏心解脫、
智慧解脫：生死已盡，梵行已立，所作已辦，
更不復受胎，如實知之。
^cb670b24「是故，諸比丘！若有族姓子、四部之眾，欲生人中者，
當求方便，行身、口、意、命清淨；若得生天上者，
亦當求方便，行四恩；若得盡有漏，成無漏心解脫、
智慧解脫，亦當求方便，行四禪。
如是，諸比丘！當作是學。」
^cb670b29爾時，諸比丘聞佛所說，歡喜奉行。
^n8【八】
^cb670c2聞如是：
一時，佛在毘舍離城外林中。
^cb670c3爾時，世尊告諸比丘：「我昔未成佛道時，爾時依彼大畏山而住。
是時彼山，其有欲心、無欲心入中者衣毛皆竪。
若復極盛熱時，野馬縱橫，露其形體而坐，夜便入深林中。
若復極寒之日，風雨交流，晝便入林中，夜便露坐。
^cb670c8「我爾時，正能誦一偈，昔所未聞，昔所未見也。
^gatha「澹淡夜安，大畏山中，
^gatha露其形體，是我誓願。
^cb670c12「若我至塚間，取彼死人之衣，而覆形體。
爾時，若案吒村人來取木支，著我耳中，
或著鼻中，或有唾者，或有溺者，或以土坌其身上；
