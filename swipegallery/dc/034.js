復以雜寶與諸梵志，將八萬四千眾生，
往至佛所，求作沙門；盡成道果，得阿羅漢。
^cb788c14「是時，修梵摩大長者聞彌勒已成佛道，
將八萬四千梵志之眾，往至佛所，求作沙門；得阿羅漢。
唯修梵摩一人，斷三結使，必盡苦際。
^cb788c17「是時，佛母梵摩越復將八萬四千婇女之眾，
往至佛所，求作沙門。
