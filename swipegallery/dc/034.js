捨可樂想，思惟不可樂想。
所以然者，若比丘思惟淨想，欲心便熾盛；
若思惟不淨想，便無欲心。
比丘當知，欲為不淨，如彼屎聚；欲如鸜鵒，
饒諸音響；欲無返復，如彼毒蛇；欲如幻化，
如日消雪；當念捨欲，如棄塜間。
