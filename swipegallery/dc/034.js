今復以非法取民財寶，此必沙門所為。』
是謂第九非法親國之難。
^cb777b16「復次，國土人民普得疫病，皆由宿緣。
是時，人民各生斯念：『我等昔日無復疾病，
今各得患，死者盈路，必是沙門呪術所致。』
是謂第十非法親國之難。
^cb777b20「是謂，比丘！十非法入國之難。
是故，比丘！莫復生心親近國家。
如是，比丘！當作是學。」
