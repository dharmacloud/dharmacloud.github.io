以為禁戒：
^gatha「『口意不行惡，身亦無所犯，
^gatha當除此三行，速脫生死淵。』
^cb789c17「過千歲之後，當有犯戒之人，遂復立戒。
「彌勒如來當壽八萬四千歲，般涅槃後，遺法當存八萬四千歲。
所以然者，爾時眾生皆是利根。
