盡生死原本，平等解脫，彼能分別地種，都不起想著。
地種、人、天、梵王，乃至有想無想處，亦復如是。
至於涅槃，不著涅槃，不起涅槃之想。
所以然者，皆由壞婬、怒、癡之所致也。
比丘當知，如來、至真、等正覺善能分別於地，
亦不著於地種，不起地種之想。
所以然者，皆由破愛網之所致，因有有生，
因生有老死，皆悉除盡，是故如來成最正覺。」
佛說此語時，是時諸比丘不受其教。
所以然者，由魔波旬閉塞心意故。
^cb766b16「此經名曰一切諸法之本，我今具足說之。
諸佛世尊所應修行，我今已具足施行。
汝等當念閑居樹下，端意坐禪，思惟妙義。
今不為者，後悔無益。
此是我之教誡也。」
