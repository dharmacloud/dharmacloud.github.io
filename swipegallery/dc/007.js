於是，世尊則於晡時燕坐而起，從神室出，露地經行。
時，彼大眾中，有一人見世尊則於晡時燕坐而起，
從神室出，露地經行，即往詣佛，稽首作禮，隨佛經行。
佛見在後，問彼人曰：『以何等故，大眾喧鬧，
其聲高大，音響震動耶？』
彼人白曰：『世尊！今日天大雷雨雹，殺四牛、
耕者二人，彼送葬時，大眾喧閙，其聲高大，音響震動。
世尊！向者不聞聲耶？』
世尊答曰：『我不聞聲。』
復問：『世尊！向為眠耶？』
答曰：『不也。』復問：『世尊！
時寤不聞此大聲耶？』
答曰：『如是。』爾時，彼人便作是念：『甚奇！
甚特！極息至寂，如來、無所著、等正覺之所行。
所以者何？寤而不聞此大音聲。』
若世尊寤而不聞此大音聲者，我受持是世尊未曾有法。
^cb471c9「我聞世尊一時在鬱鞞羅尼連然河邊，阿闍惒羅尼拘類樹下初得佛道。
