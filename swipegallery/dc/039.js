若欲建立世界。一任微塵熾然。若欲除滅世界。覺悟人法俱空。了無一法可得。湛然清淨。不被諸境所轉。皆由於自己也。
【僧了性曰】此分。佛恐末劫人。重重執著因果。不相離捨。故重囑須菩提。人人身中。有微細善惡雜念。猶如大千世界微塵之多。此念無非影響虛妄建立。故云非微塵眾。亦因轉却無明煩惱之心。變作慈悲無礙之智。方入空寂智解。得大安樂。是名微塵眾。
