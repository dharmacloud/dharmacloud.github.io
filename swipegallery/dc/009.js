謂比丘行知足，衣取覆形，食取充軀，是謂道從知足，非無厭得。
^cb541c20「云何道從遠離，非樂聚會，非住聚會，非合聚會得？
謂比丘行遠離，成就二遠離，身及心俱遠離，
是謂道從遠離，非樂聚會，非住聚會，非合聚會得。
^cb541c23「云何道從精勤，非懈怠得？
謂比丘常行精進，斷惡不善，修諸善法，
恒自起意，專一堅固，為諸善本不捨方便，
是謂道從精勤，非懈怠得。
