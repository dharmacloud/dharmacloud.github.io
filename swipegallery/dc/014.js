如是瞋恚、睡眠、調悔，斷疑度惑，於諸善法無有猶豫，我於疑惑淨除其心。
諸賢！我已斷此五蓋——心穢、慧羸，離欲，
離惡不善之法，至得第四禪成就遊。
諸賢！我已得如是定心清淨，無穢無煩，
柔軟𣽈善住，得不動心，趣向漏盡通智作證。
