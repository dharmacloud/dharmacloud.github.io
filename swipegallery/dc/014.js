正知出入，善觀分別，屈伸低仰，儀容庠序，
善著僧伽梨及諸衣鉢，行住坐臥、眠寤語默，皆正知之。
^cb733c27「『諸賢！我已成就此聖戒身及極知足，亦成就聖護諸根，
得正知出入，獨住遠離在無事處，或至樹下空安靖處，
山巖石室、露地穰𧂐，或至林中，或在塜間。
