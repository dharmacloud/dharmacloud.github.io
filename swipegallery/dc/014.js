此善念滅無餘，敗壞無餘，知如真，以慧觀。
賢聖弟子如是行者，滅善念知如真。
所以者何？因正見故生正志，因正志故生正語，
因正語故生正業，因正業故生正命，因正命故生正方便，
因正方便故生正念，因正念故生正定。
賢聖弟子心如是定已，便解脫一切婬、
怒、癡。
^cb721c9「物主！賢聖弟子如是正心解脫已，便知一切生已盡，
梵行已立，所作已辦，不更受有，知如真。
是謂學見跡成就八支，漏盡阿羅訶成就十支。
物主！云何學見跡成就八支？
謂學正見至學正定，是謂學見跡成就八支。
物主！云何漏盡阿羅訶成就十支？
謂無學正見至無學正智，是謂漏盡阿羅訶成就十支。
物主！若有十支，我施設彼成就善、第一善、
無上士，得第一義、質直沙門。」
