若復比丘心意專正，彼比丘欲漏未生便不生，
已生便滅之；未生有漏使不生，已生令滅之；
未生無明漏使不生，已生令滅之。
若專念分別六入，終不墮惡道。
^cb723c14「云何六入為惡道？
眼觀此色，若好、若醜，見好則喜，見惡不喜；
若耳聞聲，若好、若醜，聞好則喜，聞不好則不喜；
鼻、口、身、意，亦復如是。
猶如有六種之虫，性行各異，所行不同。
若有人取繩纏縛之，取狗、野狐、獼猴、鱣魚、
蚖蛇、飛鳥，皆悉縛之，共繫一處而放之。
爾時，六種之虫各有性行。
爾時，狗意中欲赴趣村中；野狐意中欲趣赴塜間；
鱣魚意中欲趣水中；獼猴意中欲向山林之間；
毒蛇意中欲入穴中；飛鳥意中欲飛在空。
爾時，六種之虫各各有性行而不共同。
^cb723c25「設復有人取此六種之虫，繫著一處，而不得東、
西、南、北。是時，六種之虫雖復動轉，亦不離故處。
此內六情亦復如是，各各有所主，其事不同，
所觀別異，若好、若醜。
^cb723c29「爾時，比丘繫此六情而著一處。
是故，諸比丘！當念專精，意不錯亂，是時弊魔波旬終不得其便，
諸善功德皆悉成就。
如是，諸比丘！當念具足眼根，便得二果，
於現法中得阿那含果，若得阿羅漢果。
如是，諸比丘！當作是學。」
^cb724a5爾時，諸比丘聞佛所說，歡喜奉行。
