顏貌端政，面如桃華色，視瞻審諦，口作優鉢華香，身作栴檀香。
^cb724a14「是時，善目辟支佛到時，著衣持鉢，入波羅㮈城乞食。
漸漸至大長者家，在門外默然而立。
是時，長者女遙見有道士在門外立，端政無雙，
顏貌殊特，世之希有，口作優鉢華香，體作栴檀香，
便起欲心，向彼比丘所，便作是說：『汝今端政，
面如桃華色，世之希有！
我今雖處女人，亦復端政，可共合會。
