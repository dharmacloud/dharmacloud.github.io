憂、苦、惱。彼以思惟法義，則無愚惑、錯誤修行。
若見有鬪諍者，彼便作是念：『此諸賢士不修無常想，
不廣布無常想，故致此鬪訟耳。
彼以鬪諍，不觀其義；以不觀其義，則有迷惑之心；
彼以執此愚惑，而命終入三惡道，餓鬼、
畜生、地獄中。』是故，諸比丘！
當修，無常想，廣布無常想，便無瞋恚、愚惑之想，
亦能觀法，亦觀其義。
若命終之後，生三善處，生天上、人中、涅槃之道。
如是，諸比丘！當作是學。」
^cb717c17爾時，諸比丘聞佛所說，歡喜奉行。
^n3【三】
^cb717c18聞如是：
一時，佛在摩竭國憂迦支江水側。
^cb717c19爾時，世尊詣一樹下，躬自敷座而坐，正身正意，繫念在前。
^cb717c20爾時，有一梵志往至彼處。
是時，梵志見世尊脚跡為妙，見已，便生此念：「是何人之跡？
為是天、龍、鬼神、乾沓和、阿須倫、人、若非人？
為我先祖梵天耶？」
是時，梵志即逐跡前進，遙見世尊在一樹下坐，
正身正意，繫念在前，見已，作是語：「為是天耶？」
^cb717c26世尊告曰：「我非是天。」
「為乾沓和耶？」
世尊告曰：「我非乾沓和也。」
^cb717c27「為是龍子乎？」
對曰：「我非是龍也。」
^cb717c28「為閱叉也？」
佛報梵志：「我非閱叉。」
^cb717c29「為是祖父也。」
佛報曰：「我非祖父。」
^cb718a1是時，婆羅門問世尊曰：「汝今是誰？」
世尊告曰：「有愛者則有受，有受則有愛，
因緣合會，然後各各相生如此，如此五苦盛陰無有斷絕時。
以知愛已，則知五欲，亦知外六塵、內六入，即知此盛陰之本末。」
^cb718a5爾時，世尊便說此偈：
^gatha「世間有五欲，意為第六生，
^gatha以知內外六，當念盡苦際。
^cb718a9「是故，當求方便，滅內外六事。
如是，梵志！當作是學。」
^cb718a10爾時，彼梵志聞佛如是教，思惟翫習不去心懷，
即於坐上，諸塵垢盡，得法眼淨。
^cb718a12爾時，彼梵志聞佛所說，歡喜奉行。
^n4【四】
^cb718a13聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb718a14爾時，世尊告諸比丘：「我本為菩薩時，未成佛道中有此念：
『此世間極為勤苦；有生、有老、有病、有死，然此五盛陰不得盡本原。』
是時，我復作是念：『由何因緣有生、老、病、死？
復由何因緣致此災患？』
當思惟此時，復生此念：『有生則有老、病、死。』
爾時，當思惟是時，復更生念：『由何因緣有生？』
此由有而生。復生此念：『有者，何由而有？』
當思惟是時，便生此念：『此有由受而有。』
復念：『此受何由而有？』
爾時，以智觀之，由愛而有受。
復更思惟：『此愛何由而生？』
重觀察之，由痛而有愛。
復更思惟：『此痛何由而生？』
當作是觀察時，由更樂而有此痛。
復重思惟：『此更樂何由而有？』
我生此念時，緣六入而有此更樂。
時我重思惟：『此六入何由而有？』
觀察是時，由名色而有六入。
時我復作是念：『名色何由而有？』
觀察是時，復由識而有名色。
『此識何由而有？』觀察是時，由行生識。
時我復作是念：『行何由而生？』
觀察是時，行由癡而生。
無明緣行，行緣識，識緣名色，名色緣六入，
六入緣更樂，更樂緣痛，痛緣愛，愛緣受，
受緣有，有緣生，生緣死，死緣愁、憂、苦、惱，不可稱計。
如是名為苦盛陰所習。
^cb718b7「我爾時復作是念：『由何因緣滅生、老、病、死？』
我觀察是時，生滅，老、病、死滅。
時復生此念：『由何而無生？』
觀此生原，有滅，生則滅。
復念：『由何而無有？』
時生此念：『無受則無有。』
時我生此念：『由何滅受？』
觀察是時，愛滅，受則滅。
復生此念：『由何而滅愛？』
重更觀察，痛滅，愛則滅。
復思惟：『由何而滅痛？』
觀察是時，更樂滅，則痛滅。
復思惟：『更樂何由而滅？』
觀察是時，六入滅，則更樂滅。
復觀此六入何由而滅？
當觀察時，名色滅，則六入滅。
復觀名色何由而滅？
識滅，則名色滅。復觀察此識何由而滅？
行滅，則識滅。復觀此行何由而滅？
癡滅，則行滅。行滅，則識滅；識滅，則名色滅；
名色滅，則六入滅；六入滅，則更樂滅；更樂滅，
則痛滅；痛滅，則愛滅；愛滅，則受滅；受滅，
則有滅；有滅，則生滅；生滅，則老、病滅，老、
病滅，則死滅，是謂名為五盛陰滅。
^cb718b24「時我復生此念：『此識最為原首，令人致此生、
老、病、死；然不能知此生、老、病、死生之原本。』
猶如有人在山林中行，逐小徑道。
小復前行，見舊大道，古昔諸人在中行處。
是時，彼人便復此道，小復前進，見舊城郭、
園觀、浴池，皆悉茂盛，但彼城中無有居民。
此人見已，還歸本國，前白王言：『昨遊山林見好城郭，
樹木繁茂，但彼城中無有人民。
大王！可使人民在彼城止住。』
是時，國王聞此人語，即居止人民；然此城郭還復如故，
人民熾盛，快樂無比。
^cb718c5「諸比丘當知，我昔未成菩薩時，在山中學道，
見古昔諸佛所遊行處，便從彼道，即知生、
老、病、死所起原本，有生有滅，皆悉分別，
知生苦、生習、生盡、生道，皆悉了知，有、受、
愛、痛、更樂、六入、名色、識、行、癡，亦復如是。
無明起則行起，行所造者復由於識，我今以明於識，
今與四部之眾而說此本；皆當知此原本所起，
知苦、知習、知盡、知道，念使分明，以知六入，
則知生、老、病、死，六入滅則生、老、病、死滅。
是故，比丘！當求方便，滅於六入。
如是，諸比丘！當作是學。」
^cb718c15爾時，諸比丘聞佛所說，歡喜奉行。
^n5【五】
^cb718c17聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb718c18爾時，世尊與無央數百千萬眾而為說法。
爾時，阿那律在彼坐上。
是時，阿那律在眾中睡眠。
^cb718c20爾時，佛見阿那律睡眠，便說此偈：
^gatha「受法快睡眠，意無有錯亂，
^gatha賢聖所說法，智者之所樂。
^gatha猶如深淵水，澄清無瑕穢，
^gatha如是聞法人，清淨心樂受。
^gatha亦如大方石，風所不能動，
^gatha如是得毀譽，心無有傾動。」
^cb718c28是時，世尊告阿那律：「汝畏王法及畏盜賊而作道乎？」
^cb718c29阿那律報曰：「不也。
世尊！」
佛告阿那律：「汝何故出家學道？」
^cb719a1阿那律白佛言：「厭患此老、病、死、愁、憂、苦、
惱，為苦所惱，故欲捨之，是故出家學道。」
^cb719a3世尊告曰：「汝今，族姓子！
信心堅固，出家學道。
世尊今日躬自說法，云何於中睡眠？」
^cb719a5是時，尊者阿那律即從座起，偏露右肩，
長跪叉手，白世尊言：「自今已後形融體爛，終不在如來前坐睡。」
^cb719a7爾時，尊者阿那律達曉不眠，然不能除去睡眠，眼根遂損。
爾時，世尊告阿那律曰：「勤加精進者與調戲蓋相應，
設復懈怠與結相應，汝今所行當處其中。」
^cb719a11阿那律白佛：「前已在如來前誓，今不能復違本要。」
^cb719a12是時，世尊告耆域曰：「療治阿那律眼根。」
^cb719a13耆域報曰：「若阿那律小睡眠者，我當治目。」
^cb719a14世尊告阿那律曰：「汝可寢寐。
所以然者，一切諸法由食而存，非食不存。
眼者以眠為食，耳者以聲為食，鼻者以香為食，
舌者以味為食，身者以細滑為食，意者以法為食，我今亦說涅槃有食。」
^cb719a19阿那律白佛言：「涅槃者以何等為食？」
佛告阿那律：「涅槃者以無放逸為食，乘無放逸，得至於無為。」
^cb719a21阿那律白佛言：「世尊！
雖言眼者以眠為食，然我不堪睡眠。」
^cb719a22爾時，阿那律縫故衣裳。
是時，眼遂敗壞，而得天眼，無有瑕穢。
是時，阿那律以凡常之法而縫衣裳，不能得使縷通針孔中。
是時，阿那律便作是念：「諸世間得道羅漢，當與我貫針。」
^cb719a27是時，世尊以天耳清淨，聞此音聲：「諸世間得道阿羅漢者，當與我貫針。」
爾時，世尊至阿那律所而告之曰：「汝持針來，吾與貫之。」
^cb719b1阿那律白佛言：「向所稱說者，謂諸世間欲求其福者，與我貫針。」
^cb719b2世尊告曰：「世間求福之人無復過我，如來於六法無有厭足。
云何為六？一者施；二者教誡；三者忍，四者法說、
義說；五者將護眾生，六者求無上正真之道。
是謂，阿那律！如來於此六法無有厭足。」
^cb719b7阿那律曰：「如來身者真法之身，復欲更求何法？
如來已度生死之海，又脫愛著。
然今日故求為福之首。」
^cb719b10世尊告曰：「如是，阿那律！
如汝所說，如來亦知此六法為無厭足。
若當眾生知罪惡之原身、口、意所行者，
終不墮三惡趣；以其眾生不知罪惡之原，故墜墮三惡趣中。」
^cb719b13爾時，世尊便說此偈：
^gatha「世間所有力，遊在天人中，
^gatha福力最為勝，由福成佛道。
^cb719b17「是故，阿那律！當求方便，得此六法。
如是，諸比丘！當作是學。」
^cb719b18爾時，諸比丘聞佛所說，歡喜奉行。
^n6【六】
^cb719b20聞如是：
一時，佛在舍衛國祇樹給孤獨園。
^cb719b21爾時，有眾多比丘入舍衛城乞食，聞王波斯匿宮門外有眾多人民，
於中舉手喚呼皆稱怨：「國界有賊名鴦掘魔，
極為兇暴，殺害生類不可稱計，無慈悲於一切眾生，
國界人民無不厭患，日取人殺以指為鬘，故名為指鬘。
唯願大王當往共戰。」
^cb719b26是時，眾多比丘乞食已，還詣祇洹精舍。
收攝衣鉢，以尼師檀著肩上，往至世尊所，
頭面禮足，在一面坐。
爾時，眾多比丘白世尊言：「我等眾多比丘入舍衛城乞食，
見眾多人民在王宮門外，稱怨訴辭：『今王國界有賊名鴦掘魔，
為人兇暴，無有慈心，殺於一切眾生，人亡國虛皆由此人，又取人指以為華鬘。』」
^cb719c5爾時，世尊聞彼比丘語已，即從座起，默然而行。
^cb719c6是時，世尊尋到彼所，諸有取薪、負草、犁作之人，
及牧牛羊者，見世尊詣彼道，各白佛言：「沙門！
沙門！勿從彼道。所以然者，此路側有賊名鴦掘魔，於中止住。
諸有人民，欲就此道者，要集十人、或二十人、
或三十、四十、五十人，猶不得過，盡為鴦掘魔所擒獲。
然沙門瞿曇獨無有侶，為鴦掘魔所觸嬈者，於事不省。」
世尊雖聞此語，故進不住。
^cb719c14爾時，鴦掘魔母，持食詣鴦掘魔所。
是時，鴦掘魔便作是念：「吾指鬘為充數不乎？」
是時，即數指，未充數，復更重數，唯少一人指。
是時，鴦掘魔左右顧視，求覓生人，欲取殺之；
然四遠顧望，亦不見人，便作是念：「我師有教：
『若能害母者，必當生天。』
我今母躬來在此，即可取殺之，得指充數，生於天上。」
^cb719c21是時，鴦掘魔左手捉母頭，右手拔劍而語母言：
「小住，阿母。」是時，世尊便作是念：「此鴦掘魔當為五逆。」
即放眉間相光明，普照彼山林。
是時，鴦掘魔見光明已，復語母言：「此是何光明照此山林？
將非國王集諸兵眾，攻伐我身乎？」
^cb719c26是時母告曰：「汝今當知，此非日月火光，
亦非釋、梵天王光明。」
^cb719c28爾時，其母便說此偈：
^gatha「此非火光明，非日月釋梵，
^gatha鳥狩不驚怖，和鳴殊於常。
^gatha此光極清淨，使人悅無量，
^gatha必是尊最勝，十力至此間。
^gatha於天世人中，天眼覩世界，
^gatha故欲度汝身，世尊來至此。」
^cb720a6是時，鴦掘魔聞佛音響，歡喜踊躍，不能自勝，
便作是語：「我師亦有教誡而勅我曰：『設汝能害母，
并殺沙門瞿曇者，必生梵天上。』」
^cb720a9是時，鴦掘魔語母曰：「母！
今且住，我先取沙門瞿曇殺，然後當食。」
^cb720a10是時，鴦掘魔即放母而往逐世尊。
遙見世尊來，亦如金聚，靡所不照，見已，
並笑而說是語：「今此沙門定在我手，必殺不疑。
其有人民欲行此道者，皆集大眾而行此道；
然此沙門獨無伴侶，我今當取殺之。」
^cb720a15是時，鴦掘魔即拔腰劍，往逆世尊。
是時，世尊尋還復道，徐而行步，而鴦掘魔奔馳而逐，亦不能及如來。
是時，鴦掘魔白世尊言：「住，住，沙門！」
^cb720a18世尊告曰：「我自住耳，汝自不住。」
^cb720a19是時，彼鴦掘魔並走，遙說此偈：
^gatha「去而復言住，語我言不住，
^gatha與我說此義，彼住我不住。」
^cb720a23爾時，世尊以偈報曰：
^gatha「世尊言已住，不害於一切，
^gatha汝今有殺心，不離於惡原。
^gatha我住慈心地，愍護一切人，
^gatha汝種地獄苦，不離於惡原。」
^cb720a28是時，鴦掘魔聞此偈已，便作是念：「我今審為惡耶？
又師語我言：『此是大祠，獲大果報。
能取千人殺，以指作鬘者，果其所願；如此之人，
命終之後，生善處天上。
設取所生母及沙門瞿曇殺者，當生梵天上。』
是時，佛作威神，神識𭟙㸌寤，諸梵志書籍亦有此言：
『如來出世甚為難遇，時時億劫乃出。
彼出世時，不度者令度，不解脫者令得解脫。
彼說滅六見之法。
云何為六？言有我見者，即說滅六見之法；
無有我者，亦與說滅無有我見之法；言有我見、
無有我見，亦與說有我見、無我見之法，
復自觀察、說觀察之法；自說無我之法，
亦非我說、亦非我不說之法。
若如來出世，說此滅六見之法。』
又我奔走之時，能及象、馬、車乘，亦及人民。
然此沙門行不暴疾，然今日不能及此，必當是如來。」
^cb720b15是時，鴦掘魔便說此偈：
^gatha「尊今為我故，而說微妙偈，
^gatha惡者今識真，皆由尊威神。
^gatha即時捨利劍，投于深坑中，
^gatha今禮沙門跡，即求作沙門。」
^cb720b20是時，鴦掘魔即前白佛言：「世尊！
唯願聽作沙門。」
^cb720b21世尊告曰：「善來，比丘！」
即時鴦掘魔便成沙門，著三法衣。
^cb720b22爾時，世尊便說此偈：
^gatha「汝今以剃頭，除結亦當爾，
^gatha結滅成大果，無復愁苦惱。」
^cb720b25是時，鴦掘魔聞此語已，即時諸塵垢盡，得法眼淨。
爾時，世尊將鴦掘魔比丘還詣舍衛城祇洹精舍。
^cb720b27是時，王波斯匿集四部之眾，欲往攻伐賊鴦掘魔。
是時，王便作是念：「我今可往至世尊所，
以此因緣，具白世尊。
若世尊有所說者，當奉行之。」
爾時，王波斯匿即集四部之兵，往世尊所，
