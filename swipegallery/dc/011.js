作佛圖主，為人所宗，凶暴急弊，極為麤惡，
罵詈責數於諸比丘，因此故生地諸比丘皆捨離去，不樂住此。
生地諸優婆塞聞已，即共往詣尊者曇彌所，
驅逐曇彌，令出生地諸寺中去。
