所以者何？因瞋恚所覆，心不捨瞋恚故。
是謂第二怨家法而作怨家，謂男女輩瞋恚時來。
^cb617c7「復次，怨家者，不欲令怨家得大利。
所以者何？怨家者，不樂怨家得大利，人有瞋恚，
習瞋恚，瞋恚所覆，心不捨瞋恚。
彼應得利而不得利，應不得利而得利。
彼此二法更互相違，大得不利。
所以者何？因瞋恚所覆，心不捨瞋恚故。
是謂第三怨家法而作怨家，謂男女輩瞋恚時來。
^cb617c14「復次，怨家者，不欲令怨家有朋友。
所以者何？怨家者，不樂怨家有朋友，人有瞋恚，
習瞋恚，瞋恚所覆，心不捨瞋恚。
彼若有親朋友，捨離避去。
所以者何？因瞋恚所覆，心不捨瞋恚故。
是謂第四怨家法而作怨家，謂男女輩瞋恚時來。
^cb617c19「復次，怨家者，不欲令怨家有稱譽。
所以者何？怨家者，不樂怨家有名稱，人有瞋恚，
習瞋恚，瞋恚所覆，心不捨瞋恚。
彼惡名醜，聲周聞諸方。
所以者何？因瞋恚所覆，心不捨瞋恚故。
是謂第五怨家法而作怨家，謂男女輩瞋恚時來。
^cb617c25「復次，怨家者，不欲令怨家極大富。
所以者何？怨家者，不樂怨家極大富，人有瞋恚，
習瞋恚，瞋恚所覆，心不捨瞋恚。
彼作如是身、口、意行，使彼大失財物。
所以者何？因瞋恚所覆，心不捨瞋恚故。
是謂第六怨家法而作怨家，謂男女輩瞋恚時來。
^cb618a2「復次，怨家者，不欲令怨家身壞命終，必至善處，生於天上。
所以者何？怨家者，不樂怨家往至善處，
人有瞋恚，習瞋恚，瞋恚所覆，心不捨瞋恚，
身、口、意惡行，彼身、口、意惡行已，身壞命終，
必至惡處，生地獄中。
所以者何？因瞋恚所覆，心不捨瞋恚故。
是謂第七怨家法而作怨家，謂男女輩瞋恚時來。
此七怨家法而作怨家，謂男女輩瞋恚時來。」
^cb618a9於是，世尊說此頌曰：
^gatha「瞋者得惡色，眠臥苦不安，
^gatha應獲得大財，反更得不利。
^gatha親親善朋友，遠離瞋恚人，
^gatha數數習瞋恚，惡名流諸方。
^gatha瞋作身口業，恚纏行意業，
^gatha人為恚所覆，失一切財物。
^gatha瞋恚生不利，瞋恚生心穢，
^gatha恐怖生於內，人所不能覺。
^gatha瞋者不知義，瞋者不曉法，
^gatha無目盲闇塞，謂樂瞋恚人。
^gatha恚初發惡色，猶火始起烟，
^gatha從是生憎嫉，緣是諸人瞋。
^gatha若瞋者所作，善行及不善，
^gatha於後瞋恚止，煩熱如火燒。
^gatha所謂煩熱業，及諸法所纏，
^gatha彼彼我今說，汝等善心聽。
^gatha瞋者逆害父，及於諸兄弟，
^gatha亦殺姉與妹，瞋者多所殘。
^gatha所生及長養，得見此世間，
^gatha因彼得存命，此母瞋亦害。
^gatha無羞無慚愧，瞋纏無所言，
^gatha人為恚所覆，口無所不說。
^gatha造作癡罪逆，而自夭其命，
^gatha作時不自覺，因瞋生恐怖。
^gatha繫著自己身，愛樂無極已，
^gatha雖愛念己身，瞋者亦自害。
^gatha以刀而自刺，或從巖自投，
^gatha或以繩自絞，及服諸毒藥。
^gatha如是像瞋恚，是死依於恚，
^gatha彼彼一切斷，用慧能覺了。
^gatha小小不善業，慧者了能除，
^gatha當堪耐是行，欲令無惡色。
^gatha無恚亦無憂，除烟無貢高，
^gatha調御斷瞋恚，滅訖無有漏。」
^cb618b16佛說如是。彼諸比丘聞佛所說，歡喜奉行。
^lb^ck130【教曇彌經】大品教曇彌經第十四
^cb618b20我聞如是：
