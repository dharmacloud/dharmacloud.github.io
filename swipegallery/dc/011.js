令出生地諸寺中去，即攝衣持鉢遊行，
往詣舍衛國，展轉進至舍衛國，住勝林給孤獨園。
於是，尊者曇彌往詣佛所，稽首佛足，却坐一面，
白曰：「世尊！我於生地諸優婆塞無所污、
無所說、無所犯，然生地諸優婆塞橫驅逐我，令出生地諸寺中去。」
^cb618c9彼時世尊告曰：「止止曇彌！
何須說此？」
^cb618c10尊者曇彌叉手向佛再白曰：「世尊！
我於生地諸優婆塞，無所污、無所說、無所犯，
然生地諸優婆塞橫驅逐我，令出生地諸寺中去。」
^cb618c13世尊亦再告曰：「曇彌！
往昔之時，此閻浮洲有諸商人，乘船入海持視岸鷹行，
彼入大海不遠便放視岸鷹，若視岸鷹得至大海岸者，終不還船。
若視岸鷹不得至大海岸者，便來還船。
如是曇彌！為生地優婆塞所驅逐，令出生地諸寺故，便還至我所。
止止曇彌！何須復說此？」
^cb618c20尊者曇彌復三白曰：「世尊我於生地諸優婆塞，
無所污、無所說、無所犯，然生地諸優婆塞橫驅逐我，令出生地諸寺中去。」
^cb618c23世尊亦復三告曰：「曇彌！
汝住沙門法，為生地諸優婆塞所驅逐，令出生地諸寺耶？」
^cb618c25於是，尊者曇彌即從座起，叉手向佛，白曰：「世尊！
