妻子、奴婢、作使，亦供養沙門、梵志，令昇上與樂俱而受樂報，
生天長壽。得財物已，不染不著，不縛不繳，
不繳已染著，見災患，知出要而用者，此行欲人於諸行欲人為最第一、
最大、最上、最勝、最尊，為最妙也。
猶如因牛有乳，因乳有酪，因酪有生酥，
因生酥有熟酥，因熟酥有酥精，酥精者，
為最第一、最大、最上、最勝、最尊，為最妙也。
如是，居士！此行欲人於諸行欲人為最第一、
最大、最上、最勝、最尊，為最妙也。」
^cb615c20於是，世尊說此頌曰：
^gatha「若非法求財，及法非法求，
^gatha不供不自用，亦不施為福，
^gatha二俱皆有惡，於行欲最下。
^gatha若如法求財，自身懃所得，
^gatha供他及自用，亦以施為福，
^gatha二俱皆有德，於行欲最上。
^gatha若得出要慧，行欲住在家，
^gatha見災患知足，節儉用財物，
^gatha彼得出欲慧，於行欲最上。」
^cb616a2佛說如是。給孤獨居士及諸比丘，聞佛所說，歡喜奉行。
^lb^ck127【福田經】大品福田經第十一
^cb616a7我聞如是：
一時，佛遊舍衛國，在勝林給孤獨園。
^cb616a8爾時，給孤獨居士往詣佛所，稽首佛足，
