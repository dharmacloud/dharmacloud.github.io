說喻不住者，道跡不住者，如是此賢者不可共說，亦不可共論。
若此賢者於處、非處住者，所知住者，說喻住者，
道跡住者，如是此賢者可得共說，亦可得共論。
因所說時止息口行，捨已所見，捨怨結意，
捨欲、捨恚、捨癡、捨慢、捨不語、捨慳嫉、不求勝、
不伏他，莫取所失，說義說法。
說義說法已，教復教止，自歡喜、令彼歡喜，
