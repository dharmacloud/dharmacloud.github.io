不能自勝，前白大迦葉曰：「唯願梵志恒受我請，在此舍食。」
^cb589b11是時，大迦葉即受彼請，在彼處受彼食。
是時，婆羅門婦見迦葉食訖，更取一卑座，在迦葉前坐。
