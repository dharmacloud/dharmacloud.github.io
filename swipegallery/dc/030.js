須彌山王尚有無常之變，況復芥子之體，舍利弗比丘而免此患乎？
如來金剛之身，不久亦當取般涅槃，何況我身？
然汝等各修其法行，得盡苦際。」
^cb640b25是時，尊者舍利弗往詣精舍。
到已，收攝衣鉢，出於竹園，往詣本生住處。
