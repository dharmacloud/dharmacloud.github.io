可以醇酒，飲彼象醉。
清旦，沙門瞿曇必來入城乞食，當放此醉象，蹋蹈殺之。」
^cb590a16時，王阿闍世聞提婆達兜教，即告令國中：
「明日清旦，當放醉象，勿令人民在里巷遊行。」
