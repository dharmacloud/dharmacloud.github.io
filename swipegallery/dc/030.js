乃至二千世界，此名中千世界；乃至三千世界，
此名三千大千世界；滿其中伊羅鉢龍王，
猶不能動如來一毛，況復此象欲害如來哉？
終無是處。所以然者，如來神力不可思議，
如來出世，終不為人所傷害也。
汝等各歸所在，如來自當知此變趣。」
爾時，世尊與四部眾廣為說微妙之法。
時，優婆塞、優婆斯聞正法已，各從坐起，
頭面禮足，便退而去。
^cb590b23爾時，世尊清旦著衣持鉢，欲入羅閱城乞食。
是時，提頭賴吒天王將乾沓惒等，從東方來，侍從世尊。
是時，毘留勒王將拘槃茶眾，侍從如來。
西方毘留波叉將諸龍眾，侍從如來。
北方天王拘毘羅將羅剎鬼眾，侍從如來。
