向如來跪雙膝，投地以鼻舐如來足。
時𠇍時，世尊伸右手摩象頭，而作是說：
^gatha「瞋恚生地獄，亦作蛇蚖形；
^gatha是故當捨恚，更莫受此身。」
^cb591a1爾時，神尊諸天在虛空中，以若干百千種花散如來上。
是時，世尊與四部之眾、天、龍、鬼神說微妙法。
爾時，見降象男女六萬餘人諸塵垢盡，
得法眼淨，八萬天人亦得法眼淨。
時，彼醉象身中刀風起，身壞命終，生四天王宮。
^cb591a6爾時，比丘、比丘尼，諸優婆塞、優婆夷，及天、
