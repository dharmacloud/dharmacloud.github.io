彼既自不施，又見他施，甚懷瞋恚，以行瞋恚，
身壞命終，生地獄中。
猶如有人從闇至闇，從火焰至火焰，捨智就愚。
由是而言，此人可謂先闇而後闇，大王當知，故名此人從闇至闇。
^cb636b26「彼名何等人從明至明？
