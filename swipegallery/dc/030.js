一切諸行苦，是謂第二法本末，如來之所說。
一切諸行無我，是謂第三法本末，如來之所說。
涅槃為永寂，是謂第四法本末，如來之所說。
是謂，諸賢！四法本末，如來之所說。」
^cb640b19爾時，諸比丘咸共墮淚：「今舍利弗滅度，何速乃爾。」
^cb640b20爾時，尊者舍利弗告諸比丘：「止！
