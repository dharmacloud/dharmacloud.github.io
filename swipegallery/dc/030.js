婆羅門見婦顏色甚悅，非復常人。
時，婆羅門即問其婦，婦即以此因緣具向夫婿說之。
時，婆羅門聞是語已，便將其婦共詣精舍。
往至世尊所，時，婆羅門與世尊共相問訊，在一面坐。
婆羅門婦頭面禮世尊足，在一面坐。
時，婆羅門白世尊曰：「向有婆羅門來至我家，今為所在？」
^cb589c3爾時，尊者大迦葉去世尊不遠，結跏趺坐，
正身正意，思惟妙法。
^cb589c4爾時，世尊遙指示大迦葉曰：「此是尊長婆羅門也。」
^cb589c5婆羅門曰：「云何，瞿曇！
沙門即是婆羅門耶？
沙門與婆羅門豈不異乎？」
^cb589c7世尊告曰：「欲言沙門者，即我身是。
所以然者，我即是沙門。
諸有奉持沙門戒律，我皆已得。
如今欲論婆羅門者，亦我身是。
所以然者，我即是婆羅門也。
諸過去婆羅門，所持法行，吾已悉知。
欲論沙門者，即大迦葉是。
所以然者，諸有沙門律，迦葉比丘皆悉包攬。
欲論婆羅門者，亦是迦葉比丘。
所以然者，諸有婆羅門奉持禁戒，迦葉比丘皆悉了知。」
^cb589c15爾時，世尊便說此偈：
^gatha「我不說梵志，能知呪術者；
^gatha唱言生梵天，此則不離縛。
^gatha無縛無生趣，能脫一切結；
^gatha不復稱天福，即沙門梵志。」
^cb589c20爾時，婆羅門白世尊曰：「言結縛者，何等名為結乎？」
^cb589c21世尊告曰：「欲愛是結，瞋恚是結，愚癡是結。
如來者無此欲愛，永滅無餘，瞋恚、愚癡亦復如是。
如來無復此結。」
^cb589c23婆羅門曰：「唯願世尊說深妙法，無復有此諸結縛著。」
