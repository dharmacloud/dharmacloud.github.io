愚癡人有三相愚癡標、愚癡像，謂成就愚癡人說愚癡也。
云何為三？愚癡人思惡思、說惡說、作惡作，是以愚癡人說愚癡也。
若愚癡人不思惡思，不說惡說，不作惡作者，不應愚癡人說愚癡也。
以愚癡人思惡思、說惡說、作惡作故，是以愚癡人說愚癡也。
彼愚癡人於現法中，身心則受三種憂苦。
云何愚癡人身心則受三種憂苦耶？
愚癡人者，或有所行，或聚會坐，或在道巷，
或在市中，或四衢頭，說愚癡人相應事也。
愚癡人者，殺生、不與取、行邪婬、妄言，乃至邪見，及成就餘無量惡不善之法。
