一切知，一切見，無餘知，無餘見也。
我有策慮，有思惟，住策慮地，住思惟地，
有智慧，有辯才，我往問事，然彼不知。
瞿曇！是故我作是念：『此是何等耶？』
瞿曇！我復作是念：『若我當往詣沙門瞿曇所，
問過去事者，沙門瞿曇必能答我過去事也。
我當往詣沙門瞿曇所，問未來事者，沙門瞿曇必能答我未來事也。
復次，若我隨所問沙門瞿曇事者，沙門瞿曇必亦答我隨所問事。』」
^cb784a27世尊告曰：「優陀夷！
止！止！汝長夜異見、異忍、異樂、異欲、異意故，不得盡知我所說義。
優陀夷！我有弟子有因有緣，憶無量過去本昔所生，
謂一生、二生、百生、千生，成劫、敗劫、無量成敗劫，
眾生名某，我曾生彼，如是姓、如是字，如是生、
如是飲食、如是受苦樂、如是長壽、如是久住、如是壽訖。
此死生彼，彼死生此，我生在此，如是姓、
如是字、如是生、如是飲食、如是受苦樂、
如是長壽、如是久住、如是壽訖。
彼來問我過去事，我答彼過去事，我亦往問彼過去事，
彼亦答我過去事，我隨所問彼事，彼亦答我隨所問事。
^cb784b10「復次，優陀夷！我有弟子，謂清淨天眼出過於人，
見此眾生死時生時、好色惡色、妙與不妙，
往來善處及不善處，隨此眾生之所作業，見其如真。
若此眾生成就身惡行，成就口、意惡行，
誣謗聖人，邪見，成就邪見業，彼因緣此，
身壞命終，必至惡處，生地獄中。
若此眾生成就身妙行，成就口、意妙行，
不誣謗聖人，正見，成就正見業，彼因緣此，
身壞命終，必昇善處，得生天中。
彼來問我未來事，我答彼未來事，我亦往問彼未來事，
彼亦答我未來事，我隨所問彼事，彼亦答我隨所問事。」
