不邪命存命，彼如法求衣被，則以法也。
如法求飲食、床榻、湯藥、諸生活具，則以法也，是謂正命。
是為見邪命是邪命者，是謂正命。
見正命是正命者，亦謂正命。
彼如是知已，則便求學，欲斷邪命，成就正命，是謂正方便。
比丘以念斷於邪命，成就正命，是謂正念。
此三支隨正命從見方便，是故正見最在前也。
