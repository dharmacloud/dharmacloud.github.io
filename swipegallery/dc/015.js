阿難！若如是比丘觀時，則知者此五欲功德有欲有染，
彼已斷也，是謂正知。
^cb739b16「復次，阿難！有五盛陰，色盛陰，覺、想、行、識盛陰，
謂比丘如是觀興衰，是色、是色習、是色滅，
是覺、想、行、識，是識、是識習、是識滅。
若此五盛陰有我慢者，彼即滅也。
阿難！若有比丘如是觀時，則知五陰中我慢已滅，是謂正知。
阿難！是法一向可、一向樂、一向意念，無漏無受，
魔所不及，惡所不及，諸惡不善法、穢污、
當來有本、煩熱苦報生老病死因亦所不及，謂成就此不放逸也。
所以者何？因不放逸，諸如來、無所著、等正覺得覺，
因不放逸根，生諸無量善法。
若有隨道品，阿難！
是故汝當如是學。
我亦成就於不放逸，當學如是。
阿難！以何義故，信弟子隨世尊行奉事至命盡耶？」
