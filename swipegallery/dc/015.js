若有疲勞，因村想故，我無是也。
若有疲勞，因人想故，我亦無是。
唯有疲勞，因一無事想故。
若彼中無者，以此故，彼見是空，若彼有餘者，彼見真實有。
阿難！是謂行真實、空、不顛倒也。
