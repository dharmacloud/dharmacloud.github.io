自體沫出，還煮其身。
彼如是考治苦痛逼迫，歲數甚多，乃至百千，
受無量苦，極重甚苦，終不得死，要當至惡不善業盡，是謂地獄苦。
^cb761a5「比丘！云何地獄苦？
彼地獄中有獄，名六更樂，若眾生生彼中，
既生彼已，若眼見色，不憙不可，非是憙可，
意不潤愛，非是潤愛，意不善樂，非是善樂，
耳所聞聲、鼻所齅香、舌所嘗味、身所覺觸、
意所知法，不憙不可，非是喜可，意不潤愛，
非是潤愛，意不善樂，非是善樂，是謂地獄苦。
^cb761a12「比丘！我為汝等無量方便說彼地獄，說地獄事，
然此地獄苦不可具說，但地獄唯有苦。
比丘！若愚癡人或時從地獄出，生畜生者，畜生亦甚苦。」
^cb761a15「比丘！云何畜生苦？
若眾生生畜生中，謂彼闇冥中生，闇冥中長，
闇冥中死，彼為云何？
謂地生蟲。愚癡人者，以本時貪著食味，
行身惡行，行口、意惡行，彼行身惡行，行口、
意惡行已，因此緣此，身壞命終，生畜生中，
謂闇冥中生，闇冥中長，闇冥中死，是謂畜生苦。
^cb761a21「比丘！云何畜生苦？
若眾生生畜生中，謂身中生，身中長，身中死。
彼為云何？謂名瘡蟲。
愚癡人者，以本時貪著食味，行身惡行，
行口、意惡行，彼行身惡行，行口、意惡行已，
因此緣此，身壞命終，生畜生中，謂身中生，
身中長，身中死，是謂畜生苦。
^cb761a27「比丘！云何畜生苦？
若眾生生畜生中，謂水中生，水中長，水中死。
彼為云何？謂魚、摩竭魚、龜、鼉、婆留尼、提鼻、
提鼻伽羅、提提鼻伽羅。
愚癡人者，以本時貪著食味，行身惡行，
行口、意惡行。彼行身惡行，行口、意惡行已，
因此緣此，身壞命終，生畜生中，謂水中生，
水中長，水中死，是謂畜生苦。
^cb761b4「比丘！云何畜生苦？
若眾生生畜生中，謂齒齧者生草樹木食。
彼為云何？謂象、馬、駱駝、牛、驢、鹿、水牛及猪。
愚癡人者，以本時貪著食味，行身惡行，
行口、意惡行，彼行身惡行，行口、意惡行已，
因此緣此，身壞命終，生畜生中，謂齒齧生草樹木食，是謂畜生苦。
^cb761b10「比丘！云何畜生苦？
若眾生生畜生中，謂彼聞人大小便氣，
即走往趣彼，食彼食，猶如男女聞飲食香，
即便往趣彼，如是說彼食彼食。
如是，比丘！若眾生生畜生中，謂彼聞人大小便氣，
即走往趣彼，食彼食。
彼為云何？謂雞、猪、狗、犲、烏、拘樓羅、拘稜迦。
愚癡人者，以本時貪著食味，行身惡行，
行口、意惡行。彼行身惡行，行口、意惡行已，
因此緣此，身壞命終，生畜生中，謂食屎不淨，是謂畜生苦。
^cb761b19「比丘！我為汝等無量方便說彼畜生，說畜生事，
然此畜生苦不可具說，但畜生唯有苦。
^cb761b21「比丘！若愚癡人從畜生出，還生為人，極大甚難。
所以者何？彼畜生中不行仁義，不行禮法，
不行妙善，彼畜生者更相食噉，強者食弱，大者食小。
比丘！猶如此地，滿其中水，有一瞎𦟈龜，
壽命無量百千之歲，彼水上有小輕木板，
唯有一孔，為風所吹。
比丘！於意云何？彼瞎𦟈龜頭寧得入此小輕木板一孔中耶？」
^cb761b28比丘答曰：「世尊！或可得入，但久久甚難。」
^cb761b29世尊告曰：「比丘！或時瞎龜過百年已，從東方來而一舉頭，
彼小木板唯有一孔，為東風吹移至南方。
或時瞎龜過百年已，從南方來而一舉頭，彼一孔板為南風吹移至西方。
或時瞎龜過百年已，從西方來而一舉頭，彼一孔板為西風吹移至北方。
或時瞎龜從北方來而一舉頭，彼一孔板為北風吹隨至諸方。
比丘！於意云何？彼瞎龜頭寧得入此一孔板耶？」
^cb761c9比丘答曰：「世尊！或可得入，但久久甚難。」
^cb761c10「比丘！如是彼愚癡人從畜生出，還生為人，亦復甚難。
所以者何？彼畜生中不行仁義，不行禮法，
不行妙善，彼畜生者更相食噉，強者食弱，大者食小。
比丘！若愚癡人或時從畜生出，還生為人，
彼若有家，小姓下賤，弊惡貧窮，少有飲食，謂得食甚難。
彼為云何？謂獄卒家、工師家、巧手家、陶師家，
如是比餘下賤家，弊惡貧窮，少有飲食，謂得食甚難。
生如是家，既生彼已，或瞎或跛，或臂肘短，
或身傴曲，或用左手，惡色羊面，醜陋短壽，
為他所使，彼行身惡行，行口、意惡行。
彼行身惡行，行口、意惡行已，因此緣此，
身壞命終，還至惡處，生地獄中。
^cb761c22「猶如二人而共博戲，彼有一人始取如是行，
便失奴婢及失妻子，復取己身倒懸烟屋中，
彼作是念：『我不食不飲，然我始取如是行。』
便失奴婢及失妻子，復取己身倒懸烟屋中。
比丘！此行甚少，失奴婢、失妻子，復取己身倒懸烟屋中。
比丘！謂此行所可行，行身惡行，行口、意惡行。
彼行身惡行，行口、意惡行已，因此緣此，
身壞命終，還至惡處，生地獄中。
比丘！此諸行最不可愛，實不可樂，非意所念。
比丘！非為具足說愚癡法耶？」
^cb762a3比丘答曰：「唯然。世尊！
為具足說愚癡法也。」
^cb762a4世尊告曰：「云何智慧法？
彼智慧人有三相智慧標智慧像，謂成就智慧人說智慧也。
云何為三？智慧人者，思善思、說善說、作善作，是以智慧人說智慧也。
若智慧人不思善思，不說善說，不作善作者，不應智慧人說智慧也。
以智慧人思善思、說善說、作善作故，是智慧人說智慧也。
智慧人者，於現法中，身心則受三種喜樂。
云何智慧人於現法中身心則受三種喜樂也？
智慧人者，或有所行，或聚會坐，或在道巷，
或在市中，或四衢頭，說智慧人相應事也。
智慧人者，斷殺、離殺、不與取、邪婬、妄言，
乃至斷邪見得正見，及成就餘無量善法。
若成就無量善法者，他人見已，便稱譽之。
彼智慧人聞已，便作是念：『若成就無量善法，
他人見已稱譽者，我亦有是無量善法，
若他知者，亦當稱譽我。』
是謂智慧人於現法中，身心則受第一喜樂。
^cb762a22「復次，彼智慧人又見王人種種治賊，謂截手、
截足，并截手足，截耳、截鼻，并截耳鼻，或臠臠割，
拔鬚、拔髮，或拔鬚髮，或著檻中衣裹火燒，
或以沙壅草纏火𤋲，或內鐵驢腹中，
或著鐵猪口中，或置鐵虎口中燒，或安銅釜中，
或著鐵釜中煑，或段段截，或利叉刺，或以鉤鉤，
或臥鐵床以沸油澆，或坐鐵臼以鐵杵擣，
或毒龍蜇，或以鞭鞭，或以杖撾，或以棒打，
或活貫摽頭，或梟其首。
彼智慧人見已，便作是念：『若成就無量惡不善法者，
王知捉已，如是考治。
我無是無量惡不善之法，若王知者，終不如是苦治於我。』
是謂智慧人於現法中，身心則受第二喜樂。
^cb762b5「復次，彼智慧人行身妙行，行口、意妙行，
彼若時疾病，或坐臥床，或坐臥榻，或坐臥地，
或身生極苦甚重苦，乃至命欲斷。
彼所有身妙行，口、意妙行，彼於爾時懸向在上，
猶如晡時，日下高山，影懸向在地，如是彼所有身妙行，
口、意妙行，彼於爾時懸向在上。
彼作是念：『此是我身妙行，口、意妙行，懸向在上，
我於本時不作惡、多作福。
若有處不作惡者，不凶暴，不作無理事，
作福、作善、作恐怖，所歸命、所依怙，我至彼善處。
而不生悔，不生悔已，賢死善命終。』
是謂智慧人於現法中，身心則受第三憙樂。
^cb762b17「復次，彼智慧人行身妙行，行口、意妙行，
彼行身妙行，行口、意妙行已，因此緣此，
身壞命終，必昇善處，上生天中。
既生彼已，受於樂報，一向可愛、一向可樂而意可念，
若作是念：『一向可愛、一向可樂而意可念者，是說善處。
所以者何？彼善處者，一向可愛，一向可樂而意可念。』」
^cb762b23爾時，有一比丘即從坐起，偏袒著衣，叉手向佛，
白曰：「世尊！善處樂云何？」
^cb762b25世尊答曰：「比丘！善處不可盡說，所謂善處樂，但善處唯有樂。」
^cb762b27比丘復問曰：「世尊！
可得以喻現其義耶？」
世尊答曰：「亦可以喻現其義也。
猶如轉輪王成就七寶、四種人如意足。
比丘！於意云何？彼轉輪王成就七寶、四種人如意足，彼因是身心受極喜樂耶？」
^cb762c2比丘答曰：「世尊！成就一寶、一人如意足，
尚受極喜樂，況復轉輪王成就七寶、四種人如意足，非為受極喜樂耶？」
