云何為三？伽藍！謂貪是諸業，因習本有。
伽藍！恚及癡是諸業，因習本有。
伽藍！貪者為貪所覆，心無厭足，或殺生，
或不與取，或行邪婬，或知已妄言，或復飲酒。
伽藍！恚者為恚所覆，心無厭足，或殺生，
或不與取，或行邪婬，或知已妄言，或復飲酒。
