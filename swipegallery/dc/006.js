有一沙門梵志來詣伽藍！
但自稱歎己所知見，而呰毀他所知所見。
瞿曇！復有一沙門梵志來詣伽藍，亦自稱歎己所知見，而呰毀他所知所見。
瞿曇！我等聞已，便生疑惑：『此沙門梵志何者為實，何者為虛？』」
^cb438c12世尊告曰：「伽藍！汝等莫生疑惑。
所以者何？因有疑惑，便生猶豫。
伽藍！汝等自無淨智，為有後世，為無後世？
伽藍！汝等亦無淨智，所作有罪，所作無罪？
