住已而觀；或復有人出水而住，住已而觀，
觀已而渡；或復有人出水而住，住已而觀，
觀已而渡，渡已至彼岸；或復有人出水而住，
住已而觀，觀已而渡，渡已至彼岸，至彼岸已，謂住岸人。
如是，我當復為汝說七水喻人。
諦聽，諦聽！善思念之。」
時，諸比丘受教而聽。
^cb424a25佛言：「云何為七？或有人常臥；或復有人出已還沒；
或復有人出已而住；或復有人出已而住，
住已而觀；或復有人出已而住，住已而觀，
觀已而渡；或復有人出已而住，住已而觀，
觀已而渡，渡已至彼岸；或復有人出已而住，
住已而觀，觀已而渡，渡已至彼岸，至彼岸已，謂住岸梵志。
此七水喻人，我略說也。
如上所說，如上施設。
汝知何義？何所分別？
有何因緣？」
^cb424b5時，諸比丘白世尊曰：「世尊為法本，世尊為法主，
法由世尊，唯願說之，我等聞已，得廣知義。」
^cb424b7佛便告曰：「汝等諦聽！
善思念之。我當為汝分別其義。」
時，諸比丘受教而聽。
^cb424b9佛言：「云何有人常臥？
謂或有人為不善法之所覆蓋，染汙所染，
受惡法報，造生死本，是謂有人常臥。
猶人沒溺，臥于水中，我說彼人亦復如是，
是謂初水喻人，世間諦如有也。
