阿私羅仙人提鞞邏答曰：『諸賢！
汝等頗聞有阿私羅仙人提鞞邏耶？』
答曰：『聞有阿私羅仙人提鞞邏。』
復語曰：『我即是也。』
彼眾多仙人即共辭謝阿私羅仙人提鞞邏曰：
『願為忍恕，我等不知尊是阿私羅仙人提鞞邏耳。』
