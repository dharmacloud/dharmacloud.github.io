【陳雄曰】當來世者。如來滅後。像法末法之世也。此經者。載真空無相法之經也。此經當此世。非種善根者難可得值。設若能受持讀誦。不獨為口耳之學。抑亦究心學之妙。茲其所以為能也。能爾。則如來豈庸釋於我。必以無上知見而昭鑒之。則無量無邊功德。舉皆成就矣。非特利於一身。且將普施於群生。非特利於一時。且將徧及於千萬億劫。所謂無量無邊功德者此也。
