彼離受生色像寶，斷受生色像寶，彼於受生色像寶淨除其心。
彼離過中食，斷過中食，一食、不夜食、學時食，彼於過中食淨除其心。
^cb657b27「彼已成就此聖戒聚，復行極知足，衣取覆形，
食取充軀，隨所遊至，與衣鉢俱，行無顧戀，
猶如鷹鳥，與兩翅俱，飛翔空中。
彼已成就此聖戒聚，及極知足，復守護諸根，
常念閉塞，念欲明達，守護念心而得成就，恒起正知。
若眼見色，然不受想，亦不味色，謂忿諍故，守護眼根。
心中不生貪伺、憂慼、惡不善法，趣向彼故，守護眼根。
如是耳、鼻、舌、身，若意知法，然不受想，亦不味法，
謂忿諍故，守護意根。
心中不生貪伺、憂慼、惡不善法，趣向彼故，守護意根。
^cb657c9「彼已成就此聖戒聚，及極知足，守護諸根，
復正知出入，善觀分別，屈伸低仰，儀容庠序，
善著僧伽梨及諸衣鉢，行住坐臥，眠寤語默，皆正知之。
彼已成就此聖戒聚，及極知足，守護諸根，
正知出入，復獨住遠離，在無事處，或至樹下空安靖處、
山巖石室、露地穰積，或至林中，或在塚間。
彼已在無事處，或至樹下空安靜處，敷尼師檀，
結加趺坐，正身正願，返念不向，斷除貪伺，
心無有諍，見他財物諸生活具，不起貪伺，
欲令我得，彼於貪伺淨除其心。
