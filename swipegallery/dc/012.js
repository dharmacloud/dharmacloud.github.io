【法華經云】瞻仰尊顏。目不暫捨。心常精進。無有間斷也。受持讀誦者。行解相應謂之受。勇猛精進謂之持。心不散亂謂之讀。見性不逆謂之誦。為人解說者。謂已悟人能見自性。方便為人解說此經。令悟實相。成無上道。此人所施。無所住相。功德無有邊際。勝前百千萬億劫以身布施功德。百千萬倍。
