持戒、博聞、庶幾、智慧亦復失之。
梵志！有時此惡知識教滅善法，猶如月盡。
梵志！如是當觀惡知識猶如月也。」
^cb660a27生聞梵志復問曰：「瞿曇！
云何觀善知識？」
^cb660a28世尊答曰：「梵志！當觀善知識猶如月也。」
^cb660a29生聞梵志復問曰：「瞿曇！
云何當觀善知識猶如月耶？」
^cb660b1世尊答曰：「梵志！猶如月初生，少壯明淨，日日增長。
梵志！或時月十五日，其殿豐滿。
梵志！如是善知識於如來正法、律得信，
彼得信已，而於後時孝順恭敬，所行隨順，
立於正智，趣向法次法，彼增長信，持戒、
博聞、庶幾、智慧亦復增長。
梵志！有時彼善知識善法具足，如十五日月。
