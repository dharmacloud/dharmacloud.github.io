有覺、有觀，離生喜、樂，逮初禪成就遊。
梵志！是謂如來所屈，如來所行，如來所服。
然彼不以此為訖，世尊、如來、無所著、等正覺，
世尊所說法善，如來弟子聖眾善趣。
彼覺觀已息，內靖、一心，無覺、無觀，定生喜、
樂，逮第二禪成就遊。
梵志！是謂如來所屈，如來所行，如來所服。
然彼不以此為訖，世尊、如來、無所著、等正覺，
世尊所說法善，如來弟子聖眾善趣。」
^cb658a1「彼離喜欲，捨無求遊，正念正智而身覺樂，
謂聖所說聖所捨、念、樂住室，逮第三禪成就遊。
梵志！是謂如來所屈，如來所行，如來所服。
然彼不以此為訖，世尊、如來、無所著、等正覺，
世尊所說法善，如來弟子聖眾善趣。
彼樂滅、苦滅，喜、憂本已滅，不苦不樂、捨念、
清淨，逮第四禪成就遊。
梵志！是謂如來所屈，如來所行，如來所服。
然彼不以此為訖，世尊、如來、無所著、等正覺，
世尊所說法善，如來弟子聖眾善趣。」
^cb658a10「彼已得如是定心，清淨無穢，無煩柔軟，
善住得不動心，趣向漏盡智通作證。
彼知此苦如真，知此苦習，知此苦滅，知此苦滅道如真。
知此漏如真，知此漏習，知此漏滅，知此漏滅道如真。
彼如是知、如是見，欲漏心解脫，有漏、無明漏心解脫，
解脫已，便知解脫，生已盡，梵行已立，所作已辦，
不更受有，知如真。
梵志！是謂如來所屈，如來所行，如來所服。
彼以此為訖，世尊。
如來。無所著。等正覺，世尊所說法善，如來弟子聖眾善趣。
梵志！於意云何？如是象跡喻善作具足耶？」
^cb658a21生聞梵志答曰：「唯然。
瞿曇！如是象跡喻善作具足。」
