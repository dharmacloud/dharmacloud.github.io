【李文會曰】不住相故。即非善法。無漏福故。是名善法。
【法華經云】初善中善後善者。初謂發善心時。須是念念精進。不生疑惑懈怠之心。中謂常修一切善法。令悟真性。不著諸法相也。後謂即破善法。直教一切善惡凡聖。無取捨憎愛之心。平常無事。故云即非善法是名善法也。
【古德云】了取平常心是道。飢來喫飯困來眠。又云。常平等心。如此廣大。妙觀察智。如日光明。軆用及此。是佛境界。
