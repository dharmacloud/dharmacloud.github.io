汝等實生惡見：「梵志種勝，餘者不如；梵志種白，
餘者皆黑；梵志得清淨，非梵志不得清淨；
梵志梵天子，從彼口生，梵梵所化。」』
彼諸仙人答曰：『如是。』
阿私羅復問諸仙人曰：『汝等為自知己父耶？』
彼諸仙人答曰：『知也。
彼梵志取梵志婦，非非梵志，彼父復父，
乃至七世父，彼梵志取梵志婦，非非梵志。』
阿私羅復問諸仙人曰：『汝等為自知己母耶？』
彼諸仙人答曰：『知也。
彼梵志取梵志夫，非非梵志，彼母復母，
乃至七世母，彼梵志取梵志夫，非非梵志。』
