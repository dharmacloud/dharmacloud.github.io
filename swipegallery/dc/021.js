所以者何？如來、應供、等正覺知人見人，
常為記說：「我之正法，寄在國王，及我比丘僧等。」』
而說偈曰：
^gatha「『世雄人中尊，正勝妙大法，
^gatha寄付於大王，及我比丘僧。』
^cb166a23「時，王白優波崛曰：『我已建正法。』
而說偈曰：
^gatha「『我已造諸塔，莊嚴諸國界，
^gatha種種興供養，幡幢及諸寶，
^gatha廣布佛舍利，遍於閻浮提，
^gatha我興如是福，意願悉已滿，
^gatha自身及妻兒，珍寶及此地，
^gatha今已悉捨施，供養賢聖塔。』
^cb166b1「時，尊者優波崛讚王言：『善哉！
善哉！大王應行如是法。』
而說偈言：
^gatha「『捨身及財命，世世無所憂，
^gatha受福無有窮，必得無上覺。』
^cb166b5「時，王請尊者優波崛入城，設種種座，請尊者就座，眾僧令往鷄雀精舍。
白尊者曰：『尊者顏貌端正，身體柔軟，而我形體醜陋，肌膚麁澁。』
^cb166b8「尊者說偈曰：
^gatha「『我行布施時，淨心好財物，
^gatha不如王行施，以沙施於佛。』
^cb166b11「時，王以偈報曰：
^gatha「『我於童子時，布施於沙土，
^gatha今獲果如是，何況餘妙施。』
