婦便生子，名曰為海。
如是在海十有餘年，採諸重寶，還到本鄉，
道中值五百賊，殺於商主，奪彼寶物。
^cb164a7「爾時，商主之子見父傷死，及失寶物，厭世間苦故，
於如來法中出家學道，還其本土，遊行諸國，次至巴連弗邑。
過此夜已，晨朝著衣持鉢，入城次第乞食，誤入屠殺舍中。
^cb164a11「時，彼比丘遙見舍裏，見火車鑪炭等治諸眾生，
如地獄中，尋生恐怖，衣毛皆竪，便欲出門。
時，兇惡即往，執彼比丘言：『入此中者，無有得出，汝今於此而死。』
比丘聞其所說，心生悲毒，泣淚滿目。
兇主問曰：『汝云何如小兒啼？』
^cb164a16「爾時，比丘以偈答曰：
^gatha「『我不恐畏死，志願求解脫，
^gatha所求不成果，是故我啼泣，
^gatha人身極難得，出家亦復然，
^gatha遇釋師子王，自今不重覩。』
^cb164a22「爾時，兇主語比丘曰：『汝今必死，何所憂惱？』
比丘復以哀言答云：『乞我少時生命，可至一月。』
彼兇不聽。如是日數漸減，止於七日，彼即聽許。
^cb164a25「時，此比丘知將死不久，勇猛精進，坐禪息心，終不能得道。
至於七日，時，王宮內人有事至死，送付兇惡之人，令治其罪。
兇主將是女人著臼中，以杵擣之，令成碎末。
^cb164a29「時，比丘見是事，極厭惡此身：『嗚呼！
苦哉！我不久亦當如是。』
而說偈言：
^gatha「『嗚呼大悲師，演說正妙法，
^gatha此身如聚沫，於義無有實。
^gatha向者美女色，今將何所在，
^gatha生死極可捨，愚人而貪著。
^gatha係心緣彼處，今當脫鏁木，
^gatha令度三有海，畢竟不復生。
^gatha如是勤方便，專精修佛法，
^gatha斷除一切結，得成阿羅漢。』
^cb164b10「時，彼凶惡人語此比丘：『期限已盡。』
比丘問曰：『我不解爾之所說。』
彼兇答曰：『先期七日，今既已滿。』
比丘以偈答曰：
^gatha「『我心得解脫，無明大黑闇，
^gatha斷除諸有蓋，以殺煩惱賊，
^gatha慧日今已出，鑒察心意識，
^gatha明了見生死，今者愍人時，
^gatha隨順修聖法，我今此身骸，
^gatha任爾之所為，無復有悋惜。』
^cb164b19「爾時，彼兇主執彼比丘著鐵鑊油中，足與薪火，
火終不然，假使然者，或復不熱。
兇主見火不然，打拍使者，而自然火，火即猛盛；
久久，開鐵鑊蓋，見彼比丘鐵鑊中蓮華上坐，
生希有心，即啟國王，王即便嚴駕，將無量眾，來看比丘。
^cb164b24「時，彼比丘調伏時至，即身昇虛空，猶如鴈王，
示種種變化，如偈所說。
^gatha「王見是比丘，身昇在虛空，
^gatha心懷大歡喜，合掌觀彼聖：
^gatha「『我今有所白，意中所不解，
^gatha形體無異人，神通未曾有，
^gatha為我分別說，修習何等法，
^gatha令汝得清淨，為我廣敷演，
^gatha令得勝妙法，我了法相已，
^gatha為汝作弟子，畢竟無有悔。』
^cb164c5「時，彼比丘而作是念：『我今伏是王，多有所導，
攝持佛法，當廣分布如來舍利，安樂無量眾生，
於此閻浮提，盡令信三寶。』
以是因緣故，自顯其德。
而向王說偈言：
^gatha「『我是佛弟子，逮得諸漏盡，
^gatha又復是佛子，不著一切有，
^gatha我今已調伏，無上兩足尊，
^gatha息心得寂靜，生死大恐怖，
^gatha我今悉得脫，有離三有縛，
^gatha如來聖法中，獲得如是利。』
^cb164c15「時，阿育王聞彼比丘所說，於佛所生大敬信，
又白比丘言：『佛未滅度時，何所記說？』
比丘答言：『佛記大王：「於我滅後，過百歲之時，
於巴連弗邑，有三億家，彼國有王，名曰阿育，
當王此閻浮提，為轉輪王，正法治化；又復廣布我舍利，於閻浮提立八萬四千塔。」
佛如是記大王。然大王今造此大地獄，
殺害無量民人，王今宜應慈念一切眾生，
施其無畏，令得安隱，佛之所記大王者，王當如法修行。』
而說偈言：
^gatha「『當行哀愍心，莫惱諸群生，
^gatha當修習佛法，廣布佛舍利。』
^cb164c27「時，彼阿育王於佛所極生敬信，合掌向比丘作禮：
『我得大罪，今向比丘懺悔，我之所作甚為不可，
願為佛子，受我懺悔。
捨心勿復責，我愚人今復歸命。』
而說偈言：
^gatha「『我今歸依佛，無上勝妙法，
^gatha比丘諸眾尊，我今盡命歸，
^gatha我今當勇猛，奉受世尊勅，
^gatha於此閻浮提，普立諸佛塔，
^gatha種種諸供養，懸繒及幡幢，
^gatha莊嚴世尊塔，妙麗世希有。』
^cb165a8「時，彼比丘度阿育王已，乘空而化。
「時，王從地獄出，兇主白王言：『王不復得去。』
王曰：『汝今欲殺我耶？』
彼曰：『如是。』王曰：『誰先入此中？』
答曰：『我是。』王曰：『若然者，汝先應取死。』
王即勅人，將此兇主著作膠舍裏，以火燒之，
又勅壞此地獄，施眾生無畏。
^cb165a13「時，王欲建舍利塔，將四兵眾，至王舍城，
取阿闍世王佛塔中舍利，還復修治此塔，與本無異。
如是取七佛塔中舍利，至羅摩村中。
時，諸龍王將是王入龍宮中，王從龍索舍利供養，龍即與之。
王從彼而出，如偈所說：
^gatha「『羅摩羅村中，所有諸佛塔，
^gatha龍王所奉事，守護而供養，
^gatha王從龍索分，諸龍開懷與，
^gatha即持此舍利，漸進於餘方。』
^cb165a23「時，王作八萬四千金、銀、琉璃、頗梨篋，盛佛舍利；
又作八萬四千四寶瓶，以盛此篋，又作無量百千幡幢繖蓋，
使諸鬼神各持舍利供養之具，勅諸鬼神言：
『於閻浮提，至於海際，城邑聚落滿一億家者，為世尊立舍利塔。』
^cb165a28「時，有國名著叉尸羅，三十六億家，彼國人語鬼神言：
『三十六篋舍利與我等，起立佛塔。』
王作方便，國中人少者，令分與彼，令滿家數，而立為塔。
^cb165b2「時，巴連弗邑有上座，名曰耶舍，王詣彼所，
白上座曰：『我欲一日之中，立八萬四千佛塔，
遍此閻浮提，意願如是。』
如偈讚曰：
^gatha「『大王名阿育，於先八塔中，
^gatha各取其舍利，於此閻浮提，
^gatha建立諸佛塔，八萬及四千，
^gatha縱廣殊妙勝，一日都使畢。』
^cb165b10「時，彼上座白王言：『善哉！
大王！剋後十五日月食時，令此閻浮提起諸佛塔。』
如是乃至一日之中，立八萬四千塔，世間民人，
興慶無量，共號名曰法阿育王。
如偈讚曰：
^gatha「『王聖種孔雀，安樂世間人，
^gatha於此閻浮提，建立勝妙塔，
^gatha本名為惡王，今造勝妙業，
^gatha共號名法王，相傳至於後。』
^cb165b18「王已建八萬四千塔，歡喜踊躍，將諸群臣往詣鷄雀精舍，
白耶舍上座曰：『更有比丘，佛所授記，當作佛事不？
我當往詣彼所供養恭敬。』
^cb165b21「上座答曰：『佛臨般涅槃時，降伏阿波羅龍王、
陶師、旃陀羅瞿波梨龍。』
詣摩偷羅國，告阿難曰：『於我般涅槃後，
百世之中，當有長者，名瞿多，其子名曰優波崛多，
當出家學道，無相佛教授於人，最為第一，當作佛事。』
佛告阿難曰：『遙見彼山不？』
阿難白佛：『見也。世尊！』
佛告阿難：『此山名優留曼茶，是阿蘭若處名那茶婆低，隨順寂靜。
而偈讚曰：
^gatha「『優波崛比丘，教授最第一，
^gatha名聞振四方，最勝之所記，
^gatha於我滅度後，當得作佛事，
^gatha度諸眾生類，其數無有限。』
^cb165c5「時，王問上座曰：『尊者優波崛今已出世不？』
上座答曰：『已出世，出家學道，降伏煩惱，
是阿羅漢，共諸無量比丘眷屬一萬八千，
住在優留曼茶山中阿蘭若處，哀愍眾生，
如佛說淨妙法，度無量諸天及人，令入甘露城。』
王聞已，歡喜踊躍，即勅群臣，速辦嚴駕，
將無量眷屬往詣彼所，修敬供養優波崛多。
^cb165c12「時，臣白王言：『彼聖既在王國，宜當遣信奉迎之，彼自當來。』
王答臣曰：『不宜遣信至彼所，應當自往，彼不宜來也。』
而說偈曰：
^gatha「『汝得金剛舌，那能不斷壞，
^gatha諫我莫往彼，親近田舍人。』
^cb165c17「王即遣信，往彼尊者所言：『某日當來尊所。』
