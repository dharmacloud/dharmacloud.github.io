於其中間，開安舟航，於航懸諸幢蓋。
^cb165c22「時，尊者優波崛愍念王故，將一萬八千阿羅漢眾，隨於水道徑至王國。
時，國中人啟王言：『尊者優波崛將一萬八千比丘眾來至。』
^cb165c25「王聞，大歡喜踊躍，即脫瓔珞，價直千萬，而授與之。
王將諸大臣眷屬，即出往尊者所，即為下食，
五體投地，向彼作禮，長跪合掌，而作是言：
『我今領此閻浮提，受於王位，不以為喜；
今覩尊者，踊躍無量，如來弟子，乃能如是，如覩於佛。』
而說偈言：
^gatha「『寂滅已度世，汝今作佛事，
^gatha世間愚癡滅，如日照佛世，
^gatha為世作導師，說法中第一，
^gatha眾生可依怙，我今大歡喜。』
^cb166a7「時，王勅使者宣令國界：『尊者優波崛比丘今來此國。』
如是唱言：
^gatha「『欲得富貴者，遠離貧窮苦，
^gatha常處天上樂，解脫涅槃者，
^gatha當值優波崛，修敬今供養，
^gatha未見諸佛者，今覩優波崛。』
^cb166a13「時，王嚴飾國界，平治道路，懸繒幡蓋，燒香散華，及諸伎樂。
