阿育王四門中，二門安二力士，第三門安大臣，自守東門。
^cb163b8「時，阿㝹樓陀大臣機關木象，又作阿育王像，
像即騎象，安置東門外。
又作無烟火坑聚，以物覆之。
修師摩既來到時，阿㝹樓陀大臣語修師摩王子：
『欲作王者，阿育在東門，可往伐之，能得此王者，自然得作王。』
時，彼王子即趣東門，即墮火坑，便即死亡。
^cb163b15「爾時，有一大力士，名曰跋陀羅由陀，聞修師摩終亡，
厭世，將無量眷屬，於佛法中出家學道，
加勤精進，逮得漏盡，成阿羅漢道。
^cb163b18「阿育王正法治化。
時，諸臣輩我等共立阿育為王故，輕慢於王，
不行君臣之禮，王亦自知諸臣輕慢於我。
^cb163b20「時，王語諸臣曰：『汝等可伐花果之樹，植於刺棘。』
諸臣答曰：『未甞見聞却除華果而植刺樹，而見除伐刺樹而植果實。』
乃至王三勅令伐，彼亦不從。
^cb163b24「爾時，國王忿諸大臣，即持利劍，殺五百大臣。
又時，王將婇女眷屬，出外園中遊戲，見一無憂樹，華極敷盛。
王見已，此華樹與我同名。
心懷歡喜。王形體醜陋，皮膚麁澁，諸婇女輩，
心不愛王，憎惡王故，以手毀折無憂華樹。
王從眠覺，見無憂樹華狼藉在地，心生忿怒，
繫諸婇女，以火燒殺。
王行暴惡，故曰暴惡阿育王。
^cb163c2「時，阿㝹樓陀大臣白王言：『王不應為是法。
云何以手自殺人，諸臣婇女？
王今當立屠殺之人，應有所殺，以付彼人。』
王即宣教立屠殺者。
彼有一山，名曰耆梨，中有一織師家。
織師有一子，亦名耆梨，兇惡撾打繫縛小男小女，
及捕水陸之生，乃至拒逆父母，是故世人傳云兇惡耆梨子。
^cb163c9「時，王諸使語彼：『汝能為王斬諸兇人不？』
彼答曰：『一切閻浮提有罪者，我能淨除，況復此一方？』
^cb163c11「時，諸使輩還啟王言：『彼人已得兇惡者。』
王言：『覓將來也！』諸使呼彼，彼答言：『小忍，先奉辭父母。』
具說上事，父母言：『子不應行是事。』
如是三勅，彼生不仁之心，即便殺父母已，然後乃至。
^cb163c15「諸使問曰：『何以經久不速來也？』
時，彼兇惡具說上事。
諸使者以是事具啟王。
王即勅彼：『我所有罪人，事應至死，汝當知之。』
彼啟王言：『為我作舍。』
王乃至為其作舍屋室，極為端嚴，唯開一門，
門亦極精嚴，於其中間，作治罪之法羅列，
狀如地獄，彼獄極為勝好。
時，彼兇人啟王言：『今從王乞願，若人來入此中者，不復得出。』
王答言：『如汝所啟乞願，當以與汝。』
^cb163c24「時，彼屠主往詣寺中，聽諸比丘說地獄事。
時，有比丘講地獄經：『有眾生生地獄者，
地獄即執彼罪人，以熱鐵鉗鉗開其口，以熱鐵丸著其口中。
次融銅灌口，次復鐵斧斬截其體，次復杻械枷鏁檢繫其身，
次復火車鑪炭，次復鐵鑊，次復灰河，次復刀山劍樹。』
具如天五使經所說。
彼屠主具聞比丘說是諸事，開其住處，
所作治罪之法如彼所說，案此法而治罪人。
